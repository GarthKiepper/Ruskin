      <div class="title">George Gordon, Lord Byron (<span class="date-1788-1824">1788&#x2013;1824</span>)
      </div>
      
      <p>In <span class="date-1834-01">January 1834</span>, when <span class="persName-JR">Ruskin</span>&#x02BC;s <span class="persName-JJR">father</span> wrote in reply to 
                    <a href="../notes/hogg_james_note.html" target="_blank" class="inactive"><span class="persName-JH">James Hogg</span> (ca. <span class="date-1770-1835">1770&#x2013;1835</span>)</a>, 
                    who had asked for an account of <span class="persName-JR">Ruskin</span>&#x02BC;s progress in using his wonderful abilities, <span class="persName-">John James</span> included a sample of <span class="persName-JR">John</span>&#x02BC;s rhymes, possibly taken from 
                    <a href="../apparatuses/account_of_a_tour_on_the_continent_apparatus.html" target="_blank" class="inactive">Account of a Tour on the Continent</a>, which <span class="persName-JR">Ruskin</span> was composing at that time. 
                    <span class="persName-JH">Hogg</span>&#x02BC;s critique of the lines, which is known only from <span class="persName-">Ruskin</span>&#x02BC;s reply to <span class="persName-JH">Hogg</span>, dwelled on the boy&#x02BC;s imitation of <span class="persName-WS">Scott</span> and <span class="persName-GGB">Byron</span>, 
                    presumably intending to encourage greater originality. 
                    <span class="persName-">Ruskin</span>&#x02BC;s comment identifies the boy&#x02BC;s sense of how these two poets related to one another: 
                    &#x201C;I have read <span class="persName-GGB">Byron</span> with wonder, and <span class="persName-WS">Scott</span> with delight, they have caused me many a day-dream and night-dream&#x201D; 
                    (<a href="../bibiliography/bibliography.html#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 277).
                
      </p>
      
