      <div class="title"> <span class="persName-WF">William</span> and <span class="persName-EFF">Edward Francis Finden</span> 
      </div>
      
      <p>Engravers, based in <span class="placeName-LONDON">London</span>. The brothers worked jointly, overseeing a large workshop employing many assistants and pupils of the trade. 
                    They specialized in landscape, although they also undertook many portraits. 
                    <span class="persName-WF">William</span>&#x02BC;s achievements in steel&#x2010;engraving include the vignettes after <span class="persName-TSP">Stothard</span> for <span class="persName-SR">Samuel Roger</span>&#x02BC;s <i> <span class="title-book-ROGERSITALY">Italy</span> </i> (<span class="date-1830">1830</span>) 
                    and <span class="persName-">Rogers</span>&#x02BC;s <i> <span class="title-book-ROGERSPOEMS1834">Poems</span> </i> (<span class="date-1834">1834</span>), complementing <span class="persName-EG">Edward Goodall</span>&#x02BC;s work after <span class="persName-JMWT">Turner</span> for those volumes. 
                    <span class="persName-EG">Edward</span>&#x02BC;s well&#x2010;known works include the plates for <span class="persName-WBI">William Brockedon</span>&#x02BC;s 
                    <i> <span class="title-book-BROCKEDONILLUSTRATIONS">Illustrations of the Passes of the Alps</span> </i> (<span class="date-1827-1829">1827&#x2010;29</span>). 
                    The brothers enjoyed a long relationship with the firm of <span class="persName-JM">John Murray</span>, for example contributing significantly to the <span class="date-1841">1841</span> edition of 
                    <span class="persName-GGB">Byron</span>&#x02BC;s <i> <span class="title-book-BYRONPILGRIMAGE">Childe Harold&#x02BC;s Pilgrimage</span> </i>.
                
      </p>
      
      <p>The Findens attempted publishing in their own right, 
                    issuing for example <i> <span class="title-book-FINDENSILLUSTRATIONSBYRON">Finden&#x02BC;s Landscape Illustrations to Mr. Murray&#x02BC;s First Complete and Uniform Edition of the Life and Works of Lord Byron</span> </i> 
                    (<span class="date-1832-1834">1832&#x2010;34</span>). Because these projects proved so ambitious, however, the brothers ultimately relied on collaborations with other publishers. Such was the case, for example, 
                    with <i> <span class="title-book-FINDENSILLUSTRATIONSBIBLE">Finden&#x02BC;s Landscape Illustrations to the Bible</span> </i> (<span class="date-1834-1837">1834&#x2010;37</span>).
                
      </p>
      
