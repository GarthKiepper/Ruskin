   <p>And this is the birthplace of <span class="persName-PPR">Rubens</span>! Sink these French bad roads. 
                    A long day&#x02BC;s journey over them under a burning sun, together with a 
                    perambulation on a damp evening at <span class="placeName-AIXLACHAPELLE">Aix&#x2010;la&#x2010;Chapelle</span> so knocked me up 
                    that I was forced to diet and quiet it, and could not stir out to see 
                    <span class="persName-PPR">Rubens</span>&#x02BC; last picture, the masterpiece of the master, the <span class="title-artwork-RUBENSCRUCIFIXIONSTPETER">Crucifixion of 
                    <div id="" class="pb"></div>
         <span class="persName-STPETER">St. Peter</span></span>, bequeathed by him at his death to his native city, and, yet more, 
                    his birth&#x2010;chamber.
   </p>
   
   <p>There is in many, in most, of the pictures of <span class="persName-PPR">Rubens</span>, and that even 
                    in his most sacred subjects, magnificent as they are viewed as paintings 
                    only, an unholiness, a cast of Bacchanalian revelry, to say the least, an 
                    unpleasingness, that does him dishonour. But there are a few, a chosen few, 
                    of his pictures which the master hath poured his whole soul into, and the 
                    production of one of which were enough to repay a lifetime of labour with 
                    immortality. There is a picture, I neither know where it is, nor what it 
                    is, but there is a picture curtained up in one of the royal palaces of 
                    <span class="placeName-FRANCE">France</span>, the <span class="title-artwork-RUBENSSTAMBROSIUS"><span class="persName-STAMBROSE">St. Ambrosius</span></span>, I think, kneeling before a crucifix. There is 
                    one single ray of yellow light falling faintly upon the grey hairs and holy 
                    features of the venerable saint, the rest is in obscurity; there is nothing more, 
                    nothing to disturb either the eye or the mind, and you feel calmed and 
                    subdued when you look upon that one solitary figure, as if in the presence 
                    of a superior being. It is impossible to see that picture, the reality is 
                    too striking, and a reality so hallowed and so beautiful, that when the 
                    curtain is again drawn over the picture, you feel as if awaking from a 
                    dream of heaven. It is by such pictures as this that <span class="persName-PPR">Rubens</span> has gained 
                    his immortality; and it was, I believe, such a picture as this that I did not 
                    see at <span class="placeName-COLOGNE">Cologne</span>. Then the disappointment made me worse, and I could 
                    not stir out to see the room in which he was born. But it don&#x02BC;t signify 
                    talking. Reader, beware of the <span class="placeName-RHEINBERGHOTEL">Grosser Rheinberg hotel</span> at <span class="placeName-COLOGNE">Cologne</span>. Art 
                    thou a poet, a painter, or a romancer? Imagine the <span class="geogName-RHINE">Rhine</span>, the beautiful, 
                    the mighty, the celebrated <span class="geogName-RHINE">Rhine</span>, fouler than the <span class="geogName-THAMES">Thames</span> at 
                    <span class="placeName-LONDONBRDG">London Bridge</span>, compressed into almost as narrow a channel, washing dirty coal 
                    wharfs on the one side; bogs, marshes, and coke manufactories on the 
                    other, yellow with mud from beneath, black with tar and coal&#x2010;dust from 
                    above, loaded with clumsy barges and dirty shipping; in short, a vile, sordid, 
                    mercenary river, fit only for traffic, high Germans and low Dutchmen, 
                    and you will have some idea of the <span class="geogName-RHINE">Rhine</span>, as seen from the bedroom 
                    windows of the Grosser Rheinberg.<span class="glosses"><a href="../glosses/cologne_glosses.php#GROSSERRHEINBERGGLOSS" target="_blank">1</a></span> Oh, if thou wouldest see the Rhine as 
                    it may be seen, as it ought to be seen, shut your eyes, sleep your time 
                    away, do anything but look about you, till you get to <span class="placeName-BONN">Bonn</span>, then walk<div id="" class="pb"></div>
                    out upon the terrace which looks forth over the swell of the deep waters 
                    to the dim outline of the <span class="geogName-SIEBENGEBIRGE">seven mountains</span>,<span class="glosses"><a href="../glosses/cologne_glosses.php#SEVENMOUNTAINSGLOSS" target="_blank">2</a></span> and there gaze and dream and 
                    meditate. Secondly, Art thou an epicure? Imagine mutton&#x2010;chops which 
                    ought to have been tough, but which age had made tender, accompanied 
                    by circular cakes of congealed fat, denominated gravy, together with a 
                    kind of brown ashes, apparently moistened with whale oil (which, I think, 
                    they called fried potatoes), as an addition to your feast, and you have an 
                    idea of a dinner of the Grosser Rheinberg. I have omitted one thing, 
                    however, which was really capital&#x2014;the vinegar. They called it Hock wine, 
                    certainly, but that don&#x02BC;t signify; you must not be led astray by names in 
                    this part of the world. However, good vinegar would not make up for 
                    the want, or worse than want, of everything else; and although the waiters 
                    made a point of not appearing until the bell had been rung seven times, 
                    we at last made them understand that we neither liked their mode of 
                    waiting, nor the contents of their larder, and so, according to their 
                    deserts, deserted them.
   </p>
   
   <p>The <span class="placeName-STPETERCATHEDRAL">cathedral</span> is the richest in fretwork and carving, in the delicate 
                    finish of every shaft, and buttress, and pinnacle, that I saw on the journey, 
                    except <span class="placeName-MILAN">Milan</span>. They showed us, in a little Gothic chapel, three skulls, 
                    which they told us were those of the Magi. They were set in framework 
                    of gold, and covered with jewels, but the pomp became not the dry 
                    bones. The soul&#x2010;less eye and fleshless cheek looked not the less horrible 
                    though a diamond beamed through the one and a bar of gold bound the 
                    other.<span class="glosses"><a href="../glosses/cologne_glosses.php#MAGIGLOSS" target="_blank">3</a></span> Returned home, and the next morning departed from <span class="placeName-COLOGNE">Cologne</span> 
