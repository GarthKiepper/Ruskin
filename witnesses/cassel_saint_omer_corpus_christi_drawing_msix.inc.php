   Pen and ink, approx. 6.2 &#x00D7; 7 cm (image only). The editors of the <a href="../notes/library_edition_note.html" target="_blank" class="inactive">Library Edition</a> 
                        describe the image as a &#x201C;sketch of a bishop beneath a canopy, with other figures&#x201D; 
                        (<a href="../bibiliography/bibliography.html#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:343 n. 1). 
                        The vignette refers to the <span class="orgName-ROMANCATHOLICCHURCH">Roman Catholic</span> procession mentioned in the preceding poem, 
                        <a href="../witnesses/cassel_poem_msix.html" target="_blank" class="inactive">&#x201C;The way was long, and yet &#x02BC;twas sweet&#x201D; &#x005B;&#x201C;Cassel,&#x201D; poem&#x005D;</a>. 
                        The procession that the Ruskins witnessed at <span class="placeName-CASSEL">Cassel</span> celebrated Ascension Day, 
                        but details in the drawing suggest that <span class="persName-JR">Ruskin</span> may have conflated this event with a procession of Corpus Christi, in which 
                        the priest carries a monstrance beneath a canopy borne by attendants 
                        (see <span class="glosses"><a href="../glosses/cassel_poem_glosses.html" target="_blank"" class="inactive">1</a></span>).
                    
   
   
