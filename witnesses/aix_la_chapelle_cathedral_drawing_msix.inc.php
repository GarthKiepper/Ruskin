                        drawing is in the manner of <a href="../notes/prout_samuel_note.php" target="_blank"><span class="persName-SP">Samuel Prout</span> (<span class="date-1783-1852">1783&#x2013;1852</span>)</a>, 
                        and seems clearly enough intended to depict <span class="placeName-building-AACHENCATHEDRAL">Aachen Cathedral</span>, 
                        although <span class="persName-JR">Ruskin</span> distorted the proportions of the building, making it seem more vertical than broad, 
                        by over&#x2010;emphasizing the Gothic structures that encircle the Carolingian core of the building. 
                        <span class="persName-JR">Ruskin</span> orients the viewer from the southwest. <span class="persName-JR">Ruskin</span>
                        On the left of the drawing, rises the cathedral&#x02BC;s distinctive octagonal <span class="placeName-building-PALATINECHAPEL">Palatine Chapel</span> 
                        (which <span class="persName-JR">Ruskin</span> represents as too narrow and spire&#x2010;like), capped by its baroque cupola. Outside the frame on the left, 
                        he has omitted from view the cathedral&#x02BC;s west&#x02BC;work (the neo&#x02BC;Gothic tower that presently defines the western end was added to the 
                        Carolingian base <span class="date-1879-1884">1879&#x2013;84</span>). On the right of the drawing extends the fifteenth&#x2010;century choir.
   
   
