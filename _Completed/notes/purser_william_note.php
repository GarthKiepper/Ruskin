      <div class="title">William Purser (ca. 1790&#x2013;1852)</div>
      
      <p>Painter, architect. He exhibited landscapes and architectural subjects at the <span class="orgName-ROYALACADEMY">Royal Academy</span>, 1805&#x2013;34. 
                    He traveled in <span class="placeName-ITALY">Italy</span> and <span class="placeName-GREECE">Greece</span> with the architect, 
                    <span class="persName-GLT">George Ledwell Taylor</span> (1788&#x2013;1873), in <span class="date-1817-1820">1817&#x2013;20</span>, and in 
                    <span class="placeName-TURKEY">Turkey</span> in the <span class="date-1820-1839">1820s&#x2013;30s</span> 
                    (catalogue note on William Purser, 
                    <a href="../notes/bibliography.php#SOTHEBYCAT2011" target="_blank" class="inactive">Sotheby  &#x0026; Co., <span class="title-reference-SOTHEBYCAT2011-italic">Catalogue of Old Master and British Drawings . . . Walter Brandt</span></a>). 
                    The <span class="orgName-VAM">Victoria and Albert Museum</span> and the <span class="orgName-BRITISHMUSEUM">British Museum</span> hold numerous orientalist watercolor drawings and prints 
                    by or after <span class="persName-WPPAINTER">Purser</span> of subjects set in <span class="placeName-TURKEY">Turkey</span> and <span class="placeName-INDIA">India</span>.
                
      </p>
      
      <p>The editor of <a href="../notes/friendships_offering_note.php" target="_blank"><span class="title-magazine-FRIENDSHIPSOFFERING-italic">Friendship&#x02BC;s Offering</span></a> 
                    between <span class="date-1828">1828</span> and his death in <span class="date-1834">1834</span>, 
                    <a href="../notes/pringle_thomas_note.php" target="_blank"><span class="persName-TP">Thomas Pringle</span></a>, engaged <span class="persName-JR">Ruskin</span> to compose a poem, 
                    <a href="../apparatuses/saltzburg_apparatus.php" target="_blank"><span class="title-poem-">&#x201C;Saltzburg&#x201D;</span></a>, 
                    to accompany an engraving by <a href="../notes/goodall_edward_note.php" target="_blank" class="inactive"><span class="persName-EG">Edward Goodall</span> (1795&#x2013;1870)</a> 
                    after <span class="persName-WPPAINTER">Purser</span>&#x02BC;s view of that city. See also 
                    <a href="../apparatuses/account_of_a_tour_on_the_continent_apparatus.php#DISCUSSION" target="_blank">Account of a Tour on the Continent: Discussion</a>.
                
      </p>
      
      
