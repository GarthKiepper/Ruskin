      <div class="title"><span class="persName-">Walter Scott</span> (<span class="date-1771-1832">1771&#x2013;1832</span>)
      </div>
      
      <p>Scottish poet and novelist. <span class="persName-">Scot</span>t is a source for some of <span class="persName-">Ruskin</span>'s earliest imitations of imaginative writing.
                
      </p>
      
      <p>In <span class="date-1834-01">January 1834</span>, when <span class="persName-">Ruskin's father</span> wrote in reply to 
                    <a href="../notes/hogg_james_note.html" target="_blank" class="inactive"><span class="persName-">James Hogg</span> (ca. <span class="date-1770-1835">1770&#x2013;1835</span>)</a>, 
                    who had asked for an account of <span class="persName-">Ruskin</span>'s progress in using his wonderful abilities, <span class="persName-">John James</span> included a sample of <span class="persName-">John</span>'s rhymes, possibly taken from 
                    <a href="../apparatuses/account_of_a_tour_on_the_continent_apparatus.html" target="_blank" class="inactive">Account of a Tour on the Continent</a>, which Ruskin was composing at that time. 
                    <span class="persName-">Hogg</span>'s critique of the lines, which is known only from <span class="persName-">Ruskin</span>'s reply to <span class="persName-">Hogg</span>, dwelled on the boy's imitation of <span class="persName-">Scott</span> and <span class="persName-">Byron</span>, 
                    presumably intending to encourage greater originality. 
                   <span class="persName-"> Ruskin</span>'s comment identifies the boy's touchstone in <span class="persName-">Scott</span>'s poetry: 
                    "If I could write one piece of poetry in my lifetime at all resembling the description of the battle of Flodden field, 
                    I think toward the conclusion of Marmion, it would be enough honour for me" 
                    (<a href="../bibiliography/bibliography.html#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 277).
                
      </p>
      
