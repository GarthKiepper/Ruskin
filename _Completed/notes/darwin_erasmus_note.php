      <div class="title">Erasmus Darwin (<span class="date-1731-1802">1731&#x2013;1802</span>)
      </div>
      
      <p>Scientist and poet. In the juvenilia, <span class="persName-JR">Ruskin</span> shows his exposure to <span class="persName-ED">Darwin</span>&#x02BC;s poetry in 
                    <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank"><span class="title-poem-THESTEAMENGINE">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</span></a>, 
                    which he based on lines from <span class="persName-ED">Darwin</span>&#x02BC;s <span class="title-poem-"><i>The Botanic Garden</i></span> 
                    (<span class="date-1789-1791">1789&#x02BC;91</span>).
      </p>
      
      <p>Apart from this early adaptation of <span class="persName-ED">Darwin</span>&#x02BC;s lines on the steam engine, <span class="persName-JR">Ruskin</span> 
                    appears never again to have taken any interest in <span class="persName-ED">Darwin</span>&#x02BC;s writing. He may have inherited a prejudice against 
                    <span class="persName-ED">Darwin</span> owing to opposition to his ideas by the philosopher and physician, <span class="persName-TB">Thomas Brown</span> 
                    (<span class="date-1778-1820">1778&#x2013;1820</span>), who was a friend and mentor of both <span class="persName-JJR">John James Ruskin</span> and his 
                    cousin, <span class="persName-MR">Margaret</span>, in their <span class="placeName-EDINBURGH">Edinburgh</span> days. In <span class="date-1798">1798</span>, 
                    <span class="persName-TB">Brown</span> challenged the &#x201C;materialist&#x201D; foundations of evolutionary theory in <span class="persName-ED">Darwin</span>&#x02BC;s 
                    <span class="title-book-ZOONOMIA"><i>Zoonomia</i></span> (<span class="date-1794">1794</span>, <span class="date-1796">1796</span>)&#x2014;a book 
                    that decisively turned the tide of critical opinion against <span class="persName-ED">Darwin</span> in the increasingly reactionary years 
                    following the outbreak of the French Revolution and the rise of Napoleonic France 
                    (<a href="../bibiliography/bibliography.php#GARFINKLEDARWIN" target="_blank" class="inactive">Garfinkle, &#x201C;Critical Response to the Work of Erasmus Darwin&#x201D;</a>; 
                    <a href="../bibiliography/bibliography.php#SCOTTISHHERITAGE" target="_blank" class="inactive">Viljoen, <i>Ruskin&#x02BC;s Scottish Heritage</i></a>, 120&#x2013;22).
      </p>
      
