      <div class="title">Tours of 1822&#x2013;25</div>
      
      <p>Prior to 1822, the Ruskins would have traveled little for pleasure. In January 1822, <span class="persName-JJR">John James</span>
                    wrote with pleasure to <span class="persName-MR">Margaret</span> about their journey together as a newly married couple in 1818, 
                    coming from <span class="placeName-LEICESTER">Leicester</span> to <span class="placeName-STAMFORD">Stamford</span>, where they stayed at the ancient <span class="placeName-">George Inn</span>.
                    In reply, <span class="persName-MR">Margaret</span> referred briefly to a holiday in the <span class="persName-KENT">Kent</span> seaside town of <span class="placeName-SANDGATE">Sandgate</span> 
                    (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive"><i>Ruskin Family Letters</i></a>, 104, 105n, 107; 
                    and see <a href="../notes/seaside_resorts_note.php" target="_blank" class="inactive"><span class="placeName-SEASIDE">Seaside Resorts</span></a> for the Ruskins&#x02BC; attraction to these places).
      </p>
      
      <p>The earliest family tours to be reflected directly in <span class="persName-JR">Ruskin</span>&#x02BC;s juvenilia
                    are discussed in <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>; the present note
                    concerns tours known mainly by family tradition.
      </p>
      
      <p>In an 1822 letter to <span class="persName-MR">Margaret Ruskin</span>, <span class="persName-JJR">John James Ruskin</span>
                    eloquently describes the charms of the &#x201C;beautiful Ride . . . , differing from if not excelling all other Rides,&#x201D; between
                    <span class="placeName-WARWICK">Warwick</span> and <span class="placeName-COVENTRY">Coventry</span> and including included <span class="placeName-KENILWORTH">Kenilworth</span>, which he
                    hopes &#x201C;to view . . . with my Love some summers day&#x201D; (A.L., John James Ruskin to Margaret Ruskin, 23&#x2013;24 February 1822, in
                    <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 118). That the family did undertake a tour during the
                    summer is supported, as <span class="persName-VAB">Van Akin Burd</span> points out, by <span class="persName-JR">Ruskin</span>&#x02BC;s
                    remark to <a href="../notes/prout_samuel_note.php" target="_blank"><span class="persName-SP">Samuel Prout</span></a>, albeit made two decades later,
                    that he had been &#x2018;born <i>again</i>, at three years old,&#x2019; among the
                    <span class="geogName-CUMBERLANDHLS">Cumberland Hills</span>, adding a religious association to experiences that his <span class="persName-JJR">father</span>&#x02BC;s letter characterized in literary and
                    picturesque terms (A.L., John Ruskin to Samuel Prout, 29 April 1844, in <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 38:339).
      </p>
      
      <p>If a tour comprising the <span class="geogName-MIDLANDS">Midlands</span> and <span class="placeName-CUMBERLAND">Cumberland</span> was ambitious enough for a family with a three&#x2010;year&#x2010;old, the itinerary may have extended, 
                    as <span class="persName-VAB">Burd</span> points out, to <span class="placeName-SCOTLAND">Scotland</span>, which <span class="persName-JR">Ruskin</span> declares in <i>Praeterita</i> that he reached by sea:
                    &#x201C;I had gone to <span class="placeName-SCOTLAND">Scotland</span> in <span class="persName-CS">Captain Spinks</span>&#x02BC;s cutter, then a regular passage boat, when I was only
                    three years old&#x201D; (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:105; 
                    see <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 119&#x2013;20 n. 1).
      </p>
      
