      <div class="title">Hill of Kinnoull (Place)</div>
      
      <p>A hill in <span class="placeName-PERTH">Perth</span>, <span class="placeName-SCOTLAND">Scotland</span>, with a <span class="geogName-KINNOULLCRAG">dramatic cliff face</span> overlooking the <span class="geogName-TAYRIV">Tay River</span>.
      </p>
      
      <p>In <a href="../bibiliography/bibliography.php#MEMORABILIAOFPERTH" target="_blank" class="inactive"><span class="title-anthology-MEMORABILIAOFPERTH"><i>Memorabilia of the City of Perth</i></span> (1806)</a>, two
                        excursions were recommended respecting the <span class="geogName-KINNOULLHL">hill</span>, one at its base, and the other to its summit. The excursion around its base started &#x201C;by a path which leads
                        entirely round the <span class="geogName-MONCRIEFFHL">Hill of Moncrieff</span>. As far as we ride eastward, we accompany the
                        <span class="geogName-TAYRIV">Tay</span>, which flows smoothly under the sublime <span class="geogName-KINNOULLCRAG">Crag of Kinnoull</span>, with which it unites to
                        form a scene of uncommon grandeur. As the road turns round the hill, the view, formerly confined, now magnificently expands.&#x201D; Formerly located &#x201C;at
                        the bottom of the hill, on the bank of the <span class="geogName-TAYRIV">river</span> opposite the <span class="placeName-SINCH">South Inch</span>,&#x201D;
                        stood the <span class="placeName-KINNOULCAS">Castle of Kinnoul</span>, &#x201C;but the plough has now removed every vestige of this edifice&#x201D; (pp. 36, 43).
      </p>
      
      <p>The excursion to the summit was to be reserved for a full afternoon, but not because the climb was onerous: &#x201C;The road
                        to the summit is no where steep or rugged enough to overpower the most delicate inhabitant of drawing&#x2010;rooms. <span class="placeName-MONTAGUE">Montague Walk</span>, winding through the wood nearly one half
                        of the way, is too pleasant to permit any idea of fatigue. But, should any slight sensation of that kind be experienced, the view from the summit will repay such an
                        expence a thousand fold.&#x201D; After quoting <span class="name--EDGARSHAKESPEARELEAR">Edgar</span>&#x02BC;s description in act 4 of <span class="title-SHAKESPEARELEAR">King Lear</span> 
                        of the dizzy view from the <span class="geogName-DOVERCLF">Cliffs of Dover</span>, the writer declares that, 
                        to complete the comparison, only the &#x201C;noble river,&#x201D; the <span class="geogName-TAYRIV">Tay</span>, need be subsituted for the ocean in <span class="persName-WSP">Shakespeare</span>&#x02BC;s lines (pp. 42&#x2013;43).
      </p>
      
