      <div class="title">The System of Title Citation for Works</div>
      
      <p><span class="persName-JR">Ruskin</span>&#x02BC;s practice of writing a given title in manuscript is explained in the &#x201C;Title&#x201D; section of each Works Apparatus Page and can be observed
                    in the facsimile of the manuscript, if available (see <a href="../notes/plan_of_archive_note.php" target="_blank">The Plan of the Archive</a>). But
                    <span class="persName-JR">Ruskin</span>&#x02BC;s practices of entitling texts are so inconsistent that the reader would be confused by routine citation of literally transcribed titles.
                    For example, because in some instances <span class="persName-JR">Ruskin</span> capitalized important words in titles but in other instances did not, the latter would, when cited out of
                    context, be indistinguishable from first&#x2010;line citations; and at the same time, first&#x2010;line citation is necessary for texts that otherwise have no titles.
                    Therefore, for purposes of routine citation throughout the <i>Early Ruskin Manuscripts</i>, each text is assigned a regularized title according
                    to the following set of conventions.
                    
         <ul>
            
            <li><span class="persName-JR">Ruskin</span>&#x02BC;s own titles for poetry and prose are distinguished by quotation marks. First and important words are capitalized, and
                            spelling regularized, regardless of <span class="persName-JR">Ruskin</span>&#x02BC;s own practice. Generally, <span class="persName-">Ruskin</span>&#x02BC;s complete titles are given, except when
                            very long titles prove impractical for routine purposes of hyperlinking; and in those cases, some reasonable short form is substituted, such as
                            <span class="title-lesson-HARRYANDLUCYVOL1-bold">&#x201C;Harry and Lucy,&#x201D; Vol. 1</span> replacing (were the rule being followed strictly) 
                            <span class="title-lesson-HARRYANDLUCYVOL1-bold">&#x201C;Harry and Lucy Concluded, Being the Last Part of Early Lessons, in Four Volumes, Vol. 1, with Copper Plates, Printed and Composed by a Little Boy and Also Drawn.&#x201D;</span></li>
            
            <li>Untitled poems are cited by their first lines, which are likewise placed in quotation marks, but with only the first word and proper nouns capitalized.</li>
            
            <li>When <span class="persName-JR">Ruskin</span> assigned more than one and differing titles to witnesses of the same work, rule no. 1 applies, and the separate titles are joined by
                            <i>and</i>, thus: <b>&#x201C;The Shipwreck&#x201D; <i>and</i> &#x201C;A Shipwreck.&#x201D;</b>  Hyperlinks to such works are, however, for
                            convenience, named by only one of these joined titles&#x2014;the link taking the reader to the &#x201C;Title&#x201D; section of the Works Apparatus Page of the work in
                            question, where the complexity is fully explained.
            </li>
            
            <li>When <span class="persName-JR">Ruskin</span> assigned the same title to different works (e.g. there are more than one poem named
                            &#x201C;A Fragment&#x201D;; more than one named &#x201C;A Psalm&#x201D;), rule no. 1 applies, and the titles (if of poems)
                            are followed by first&#x2010;line citations in parentheses, following the conventions in rule no. 2.
            </li>
            
            <li>When rules 1&#x2013;4 fail to establish clarity in particular instances, square brackets are used following the main title to supply
                            the necessary distinctions. For example:
                            
               <ul>
                  
                  <li>Titles that were invented by later editors (e.g., by <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> 
                                    in <span class="title-anthology-POEMS4D1891-italic">Poems</span> &#x005B;1891&#x005D;) are cited inside square brackets following <span class="persName-JR">Ruskin</span>&#x02BC;s title, with quotation marks and with first and important words
                                    capitalized. The use of square brackets for this purpose was established by <span class="persName-WGC">Collingwood</span> 
                                    (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><span class="title-anthology-POEMS4D1891-italic">Poems</span> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1: xxv; 
                                    <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><span class="title-anthology-POEMS8D1891-italic">Poems</span> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1: xii), 
                                    and the editors of the <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a> 
                                    followed suit (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <span class="title-anthology-JRWORKSLE-italic">Works</span></a>, 2: xxxvi). 
                                    Both editions, however, proved inconsistent in maintaining this practice.
                  </li>
                  
                  <li>Works that share the same or similar titles but that cannot be distinguished by citation of a first line (e.g., because they
                                    are not poems) are given distinguishing information in square brackets. For example, prose works with similar titles, such as
                                    &#x201C;A Sermon,&#x201D; are followed by the opening words of the text in square brackets, thus: 
                                    <b>&#x201C;A Sermon&#x201D; &#x005B;&#x201C;beautiful allegory&#x201D;&#x005D;</b>. Similarly, drawings with similar titles
                                    are expanded with distinguishing information in square brackets, thus: 
                                    <b>&#x201C;Frontispiece&#x201D; <i>and</i> &#x005B;Plate&#x005D; &#x201C;1,&#x201D; &#x005B;<span class="title-lesson-HARRYANDLUCYVOL1-italic">&#x201C;Harry and Lucy,&#x201D; Vol. 1</span>&#x005D;</b>.
                  </li>
                  
                  <li>A date in square brackets following the main title distinguishes between works that share a common title <i>and</i> 
                                    a common first line, yet are not necessarily witnesses of the same work (e.g., <b><span class="title-poem-THESHIP1827">&#x201C;The Ship&#x201D;</span> &#x005B;1827&#x005D; and <span class="title-poem-THESHIP182829">&#x201C;The Ship&#x201D;</span> &#x005B;1828&#x2013;29&#x005D;</b>).
                  </li>
                  
               </ul>
               
            </li>
            
            <li>When <span class="persName-JR">Ruskin</span> assigned no title but a first&#x2010;line citation is impractical or impossible&#x2014;for example, in the case of a prose work, 
                            or in the case of a long poem that lacks a main title but that consists of multiple parts, each part carrying its own title or needing to be cited
                            by the first line&#x2014;these works are given titles without quotation marks, and with first and important words capitalized. For example:
                            <span class="title-anthology-RUSKINACCOUNTTOUR-bold">Account of a Tour of the Continent</span>. Where possible, these descriptive titles are drawn from <span class="persName-JR">Ruskin</span>&#x02BC;s habitual way of referring to
                            the works, albeit not on the manuscript itself (e.g., in his letters, <span class="persName-JR">Ruskin</span> refers to the &#x201C;Mineralogical Dictionary,&#x201D; although no such
                            title appears on the manuscript of that work). Another kind of example calling for this kind of descriptive title is a poem that has been all but
                            obliterated, rendering illegible both <span class="persName-JR">Ruskin</span>&#x02BC;s own title (if any existed) along with the first line that might have served by rule no. 2. For example: 
                            <b>Poem on Mr. Rowbotham</b>.
                        
            </li>
            
            <li>The preceding rules apply also to titles of groups.</li>
            
         </ul>
         
      </p>
      
