      <div class="title">Tour of 1825</div>
      
      <p>In <span class="date-1825-05">May 1825</span>, the Ruskin family made its first visit to the <span class="geogName-CONTINENT">Continent</span>, 
                    a tour that was limited to <span class="placeName-WATERLOO">Waterloo</span> Field, <span class="placeName-BRUSSELS">Brussels</span>, 
                    and <span class="placeName-PARIS">Paris</span> (including <span class="placeName-VERSAILLES">Versailles</span>). As such, the journey was characteristically post&#x2010;Napoleonic. 
                    In his <a href="../bibiliography/bibliography.php#RFMS33A" target="_blank" class="inactive">Diary, 1833&#x2013;46</a>, <span class="persName-JJR">John James Ruskin</span> reflects back on this <span class="date-1825">1825</span> journey, 
                    comparing their experiences during the much more ambitious Continental <a href="../notes/tour_of_1833_note.html" target="_blank" class="inactive">Tour of <span class="date-1833">1833</span></a>. 
                    He notes that <span class="placeName-PARIS">Paris</span> on their present visit was &#x201C;not so gay as in <span class="date-1825-05">May 1825</span>&#x201D; 
                    (<a href="../bibiliography/bibliography.html#RFMS33A" target="_blank" class="inactive">Diary of <span class="persName-JJR">John James Ruskin</span>,<span class="date-1833-1846">1833&#x2013;46</span></a>, 72, 73).
                
      </p>
      
      <p>The journey may seem ambitious for a middle&#x2010;class <span class="placeName-LONDON">London</span> family, only recently established in their suburban home, and with a young boy; 
                    however, <span class="geogName-ENGLISHCHANNEL">Channel</span> crossings limited to the nearby and comparatively inexpensive destination of <span class="placeName-BELGIUM">Belgium</span> were common in the <span class="date-1820-1829">1820s</span>, 
                    and trips expressly to view the <span class="placeName-WATERLOO">Waterloo</span> battlefield
                    had been popular since as early as the weeks following the battle itself. The popularity had been enhanced by personal visits paid by such well&#x2010;known literary figures as 
                    <span class="persName-WS">Walter Scott</span>, <span class="persName-RS">Robert Southey</span>, and <span class="persName-GGB">Lord Byron</span> 
                    (<a href="../bibiliography/bibliography.html#FRANCOIS1815" target="_blank" class="inactive">Fran√ßois, &#x201C;If It&#x02BC;s 1815, This Must Be <span class="placeName-BELGIUM">Belgium</span>&#x201D;</a>, 74&#x2013;75, 89). 
                    The year of the Ruskins&#x02BC; crossing is noted as a watershed for middle&#x2010;class British travel by historians studying the opening of <span class="placeName-NETHERLANDS">Netherlands</span>, <span class="placeName-BELGIUM">Belgium</span>, and northern <span class="placeName-FRANCE">France</span>, 
                    more rapid crossings having been made possible by the rise of steam packet lines. The steam lines greatly increased the efficiency, reliability, and carrying capacity 
                    for travelers who could take holidays from employment 
                    only for definite periods and on a limited budget. 
                    Presumably, the traveling party included <span class="persName-JR">Ruskin</span>&#x02BC;s nurse, 
                    <a href="../notes/strachan_anne_note.html" target="_blank" class="inactive"><span class="persName-ASB">Anne Strachan</span></a>, who later accompanied the family also on the <span class="date-1833">1833</span> journey.
      </p>
      
