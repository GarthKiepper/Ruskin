   <div class="note">
      
      <div class="title"><span class="persName-SP">Samuel Prout</span> (<span class="date-1783">1783</span>&#x2013;<span class="date-1852">1852</span>)
      </div>
      
      <p>Painter, best known for his picturesque views of architectural subjects,
                    primarily in the form of drawings, watercolors, oils, and prints. <span class="persName-SP">Prout</span> was a personal friend of the Ruskin family,
                    and it is easy to imagine why. Besides his evidently engaging personality, and
                    the artistic interests that he shared with the family, <span class="persName-SP">Prout</span>&#x02BC;s religious biography paralleled that of the Ruskins, 
                    having been born into a Dissenter family
                    (<span class="orgName-CONGREGATIONALISM">Congregationalist</span>) and migrated to the <span class="orgName-CHURCHOFENGLAND">Established Church</span>. 
                    His persistently poor health would have drawn the sympathy of the Ruskins, who themselves tended
                    to be preoccupied with ailments (see <a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 18&#x2013;20).
      </p> 
      
      <p>Even before this personal friendship developed, <span class="persName-SP">Prout</span>&#x02BC;s work held an affectionate place in the <span class="placeName-HERNE">Herne Hill</span> household, 
                    a <span class="persName-SP">Prout</span> drawing, <span class="title-drawing-PROUTCOTTAGE-italic">An English Cottage</span>, having been &#x201C;bought, I believe, by my grandfather,&#x201D; 
                    <span class="persName-JR">Ruskin</span> wrote in <span class="date-1879">1879</span>, 
                    and &#x201C;hung in the corner of our little dining parlour . . . as early as I can remember,&#x201D; 
                    exercising &#x201C;a most fateful and continual power over my childish mind.&#x201D; 
                    The watercolor drawing in fact depicted a cottage near <span class="placeName-BRIDGEENDVILLAGE">Bridge End</span> in <span class="placeName-PERTH">Perth</span>, 
                    explaining the provenance reaching back to <span class="persName-JTR">Ruskin&#x02BC;s grandfather</span> 
                    (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <span class="title-anthology-JRWORKSLE-italic">Works</span></a>, 14: 385; 
                    and see <a href="../bibiliography/bibliography.php#HEWISONFATHERANDSON" target="_blank" class="inactive">Hewison, <span class="title-collected_art-HEWISONFATHERANDSON">&#x201C;Father and Son&#x201D;</span></a>, 3; and 
                    <a href="../bibiliography/bibliography.php#RUSKINBEMBRIDGEBRANTWOOD" target="_blank" class="inactive">Dearden, <span class="title-book--italic">Ruskin, Bembridge, and Brantwood</span></a>, 185).
                
      </p>
      
      <p>In the front of <span class="persName-SP">Prout</span>&#x02BC;s <span class="date-1833">1833</span> volume of lithographs, 
                    <a href="../bibiliography/bibliography.php#PROUTSKETCHES" target="_blank" class="inactive">Prout, <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles of Sketches Made in <span class="placeName-FLANDERS">Flanders</span> and <span class="placeName-GERMANY">Germany</span></span></a>, 
                    <span class="persName-JJR">John James Ruskin</span> is listed as a subscriber along with numerous nobility and prominent persons, a position that must have made <span class="persName-JJR">John James</span> proud. 
                    The story in <span class="title-autobiography-PRAETERITA-italic">Praeterita</span> that the family&#x02BC;s acquisition of this volume in <span class="date-1833-03-20-1833-06-21">Spring 1833</span> prompted their 
                    <a href="../notes/tour_of_1833_note.php" target="_blank">Tour of <span class="date-1833">1833</span></a> requires contextualization, but <span class="persName-JR">Ruskin</span>&#x02BC;s many admiring accounts of <span class="persName-SP">Prout</span> 
                    over the years, although changing in tone and emphasis along with Ruskin&#x02BC;s interests, seem always to revolve around verifiable truths about these early associations with the artist 
                    (see <a href="../apparatuses/account_of_a_tour_on_the_continent_apparatus.php#DISCUSSION" target="_blank">Account of a Tour on the Continent: Discussion</a>). 
                
      </p>
      
      <p>An adventurous traveler, <span class="persName-SP">Prout</span> drew his scenes for the <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span> as well as for the earlier <span class="title-collected_sketches-PROUTILLUSTRATIONS-italic">Illustrations of the <span class="geogName-RHINE">Rhine</span></span> (<span class="date-1822">1822</span>&#x2013;<span class="date-1826">26</span>) from 
                    visits to parts of the <span class="geogName-CONTINENT">Continent</span> that were then largely unexplored by English artists. His <span class="date-1821">1821</span> itinerary through northern <span class="placeName-FRANCE">France</span>, <span class="placeName-BELGIUM">Belgium</span>, and <span class="placeName-GERMANY">Germany</span> resembles the tour taken later by the Ruskins 
                    (see <a href="../notes/tour_of_1833_note.php" target="_blank">Tour of <span class="date-1833">1833</span></a>). Other tours of the <span class="date-1820-1829">1820s</span> are likewise reflected in views drawn later for the <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span>: 
                    in <span class="date-1822">1822</span>, <span class="placeName-YPRES">Ypres</span>, <span class="placeName-GHENT">Ghent</span>, <span class="placeName-MALINES">Malines</span>, <span class="placeName-ANTWERP">Antwerp</span>, <span class="placeName-UTRECHT">Utrecht</span>; 
                    in <span class="date-1823">1823</span>, <span class="placeName-FRANKFURT">Frankfurt</span>, <span class="placeName-WURZBURG">Würzburg</span>, <span class="placeName-NUREMBUERG">Nuremberg</span>, <span class="placeName-REGENSBURG">Regensburg</span>, <span class="placeName-MUNICH">Munich</span>, 
                    <span class="placeName-AUGSBURG">Augsburg</span>, <span class="placeName-ULM">Ulm</span>, <span class="placeName-STRASBOURG">Strasbourg</span>, <span class="placeName-BASLE">Basle</span>, <span class="placeName-METZ">Metz</span>, <span class="placeName-TRIER">Trier</span>; 
                    in <span class="date-1824">1824</span>, an <span class="placeName-ITALY">Italian</span> tour (with probable returns to <span class="placeName-ITALY">Italy</span> later in the decade); and in <span class="date-1829">1829</span>, <span class="placeName-FRANKFURT">Frankfurt</span>, <span class="placeName-WURZBURG">Würzburg</span>, <span class="placeName-BAMBERG">Bamberg</span>, <span class="placeName-KARLSBAD">Karlsbad</span>, <span class="placeName-PRAGUE">Prague</span>, and <span class="placeName-DRESDEN">Dresden</span> 
                    (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 53&#x2013;55). In some cases, therefore, many years separated initial on&#x2010;site sketches 
                    and the lithographs presented in the <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span>, although in the interim <span class="persName-SP">Prout</span> exhibited watercolors based on these tours.
                
      </p>
      
      <p>Along with his pioneering destinations for sketching tours, <span class="persName-SP">Prout</span> was significant for exploring new media. 
                    Having developed his skills as a lithographer throughout the <span class="date-1820-1829">1820s</span>, <span class="persName-SP">Prout</span> 
                    &#x201C;could claim to have been the first important lithographic artist in <span class="placeName-ENGLAND">England</span>&#x201D; 
                    (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 45). This mastery was first cultivated 
                    through association with <span class="persName-RAP">Rudolf Ackermann</span> (<span class="date-1764">1764</span>&#x2013;<span class="date-1834">1834</span>), 
                    whose many entrepreneurial innovations included helping to introduce lithography to <span class="placeName-BRITAIN">Britain</span>. An early lithographic 
                    project was <span class="persName-SP">Prout</span>&#x02BC;s <span class="title-collected_sketches-PROUTLIGHTSHADOW-italic">Hints on Light and Shadow</span> (<span class="date-1838">1838</span>), 
                    which linked <span class="persName-RAP">Ackermann&#x02BC;s</span> sponsorship of the new medium to the firm&#x02BC;s founding mission of art instruction 
                    and the manufacture of art materials (<a href="../bibiliography/bibliography.php#FORDACKERMANN" target="_blank" class="inactive">Ford, <span class="title-biography-FORDACKERMANN-italic">Ackermann</span></a>, 103, and see 21&#x2013;28). 
                    Likewise, the novelty of the <span class="title-collected_sketches-PROUTILLUSTRATIONS-italic">Illustrations of the <span class="geogName-RHINE">Rhine</span></span> as well as the 
                    <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span> lay partly in the lithographic medium, even as late as the <span class="date-1830-1839">1830s</span>. 
                    <span class="persName-DRP">David Roberts</span>&#x02BC;s (<span class="date-1796">1796</span>&#x2013;<span class="date-1864">1864</span>) lithographic views of the Holy Land, for example, 
                    lay a decade in the future. Already by <span class="date-1823">1823</span>, <span class="persName-RAP">Ackermann</span> was turning over <span class="persName-SP">Prout</span>&#x02BC;s 
                    and other artists&#x02BC; lithographic projects to <span class="persName-CH">Charles Hullmandel</span> (<span class="date-1789">1789</span>&#x2013;<span class="date-1850">1850</span>) for printing, 
                    as <span class="persName-RAP">Ackermann</span> could not maintain quality along with supporting other projects, such as the mass production of 
                    <span class="placeName-BRITAIN">Britain</span>&#x02BC;s first successful 
                    <a href="../notes/annuals_note.php" target="_blank">annual</a>, <span class="title-periodical-RAPFORGETMENOT-italic">Forget Me Not</span> (Ford, Ackermann, 64; and see 
                    <a href="../notes/friendships_offering_note.php" target="_blank"><span class="title-magazine-FRIENDSHIPSOFFERING-italic">Friendship&#x02BC;s Offering</span></a>).
                
      </p>
      
      <p>For the Ruskins in <span class="date-1833">1833</span>, the dramatic size (22 by 15 inches) of <span class="persName-SP">Prout</span>&#x02BC;s lithographic prints, 
                    combined with the delicate watercolor&#x2010;like effects, must have formed a striking contrast with the intaglio engraved landscape scenes that the young 
                    <span class="persName-JR">Ruskin</span> encountered in the small formats of the illustrated literary annuals or the <span class="date-1830">1830</span> illustrated 
                    edition of <span class="title-poem-ROGERSITALY-italic">Italy</span> by <a href="../notes/rogers_samuel_note.php" target="_blank"><span class="persName-SR">Samuel Rogers</span> (<span class="date-1763">1763</span>&#x2013;<span class="date-1855">1855</span>)</a>. 
                    <span class="persName-SP">Prout</span>&#x02BC;s <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span> was produced by  
                    <span class="persName-CH">Hullmandel</span>, 300 copies being printed on gray paper, which sold for 5 guineas, and 150 copies on India paper, which sold for 6 guineas 
                    (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 74).
                
      </p>
      
      <p><span class="persName-SP">Prout</span> was also familiar to a wide public through the medium of line engraving on steel, appearing frequently in the literary annuals. 
                    Between <span class="date-1827">1827</span> and <span class="date-1830">1830</span> (i.e., published from <span class="date-1826-09-1826-12">late 1826</span> to 
                    <span class="date-1829-09-1829-12">late 1829</span>), a half&#x2010;dozen of his drawings of better&#x2010;known Continental scenes 
                    (mostly in <span class="placeName-ITALY">Italy</span>), both interiors and monuments and more expansive city views, were engraved for the earliest established of the 
                    British annuals, <span class="persName-RAP">Ackermann</span>&#x02BC;s <span class="title-periodical-RAPFORGETMENOT-italic">Forget Me Not</span> (see the index of artists in 
                    <a href="../bibiliography/bibliography.php#HARRISFORGETMENOT" target="_blank" class="inactive">Harris, ed., <span class="title-archive-HARRISFORGETMENOT-italic"><span class="title-periodical-RAPFORGETMENOT-italic">&#x201C;Forget Me Not&#x201D;</span> Archive</span></a>).<span id=""><span class="persName-RLA">Lockett</span> 
                        mistakenly attributes the <span class="title-periodical-RAPFORGETMENOT-italic">Forget Me Not</span> for <span class="date-1826">1826</span> to <span class="persName-SP">Prout</span>&#x02BC;s illustration 
                        (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 77).</span><br><br> 
         <span class="persName-RLA">Lockett</span> lists several other annuals that published <span class="persName-SP">Prout</span>&#x02BC;s views, most importantly the first two volumes in the series, 
                    <span class="title-periodical--italic">The Landscape Annual</span> published by <span class="persName-RJP">Robert Jennings</span> 
                    for <span class="date-1830">1830</span> and for <span class="date-1831">1831</span> (i.e., available in <span class="date-1829-09-1829-12">late 1829</span> and 
                    <span class="date-1830-09-1830-12">late 1830</span>, respectively), which showcased <span class="persName-SP">Prout</span> exclusively. These engravings anticipated 
                    the <span class="persName-SWITZERLAND">Swiss</span> and <span class="placeName-ITALY">Italian</span> views that would be lithographed for the <span class="date-1839">1839</span> 
         <span class="title-collected_sketches-PROUTFSISKETCH-italic">Sketches in <span class="placeName-FRANCE">France</span>, <span class="placeName-SWITZERLAND">Switzerland</span>, and <span class="placeName-ITALY">Italy</span></span> 
                    (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 77&#x2013;80, 175).<span id=""><span class="persName-RLA">Lockett</span> remarks that the third <span class="title-periodical--italic">Landscape Annual</span> 
                        (for <span class="date-1832">1832</span>) would have allowed <span class="persName-SP">Prout</span> to complete use of his sketches from his <span class="date-1824">1824</span> Italian tour, but that the commission was taken from him by <span class="persName-JDH">J. D. Harding</span> (<span class="date-1797">1797</span>&#x2013;<span class="date-1863">1863</span>), causing a rift 
                        between the two artists (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <span class="title-biography-LOCKETTPROUT-italic">Samuel Prout</span></a>, 79).</span><br><br> 
                    Despite his popularity as an illustrator of the engraved annuals, <span class="persName-SP">Prout</span> 
                    played third fiddle as a contributor to the <span class="date-1830">1830</span> illustrated edition of <span class="title-poem-ROGERSITALY-italic">Italy</span> 
                    by <a href="../notes/rogers_samuel_note.php" target="_blank"><span class="persName-SP">Samuel Rogers</span> (1763&#x2013;1855)</a>, 
                    scarcely credited by the poet even for his few drawings 
                    (see <a href="../bibiliography/bibliography.php#HERRMANNTURNERPRINTS" target="_blank" class="inactive">Herrmann, <span class="title-collected_sketches-HERRMANNTURNERPRINTS-italic">Turner's Prints</span></a>, 183).
      </p>
      
      <p>Exploiting <span class="persName-SP">Prout</span>&#x02BC;s reputation for exploring exotic corners of <span class="geogName-EUROPE">Europe</span>, a latecomer to the annuals market, 
                    <span class="title-periodical-CONTINENTALANNUAL1832-italic">The Continental Annual, and Romantic Cabinet, for <span class="date-1832">1832</span></span>, published by <span class="orgName-SMITHELDER">Smith, Elder</span>, sought to distinguish itself from &#x201C;a class of periodicals&#x201D; 
                    that &#x201C;present too uniform a resemblance to each other&#x201D;: &#x201C;the wish to give all the effect in our power to the graphic designs 
                    of Mr. <span class="persName-SP">Prout</span>, has induced us to draw upon the resources of natives of the countries that supply the scenes illustrated,&#x201D; by drawing on traditional 
                    &#x201C;Romantic narratives&#x201D; of &#x201C;German, French, <span class="placeName-NETHERLANDS">Dutch</span>, <span class="placeName-ITALY">Italian</span>, and even <span class="placeName-DENMARK">Danish</span> genius,&#x201D; as well as commissioning original narratives by an &#x201C;accomplished foreigner&#x201D; 
                    (<a href="../bibiliography/bibliography.php#CONTINENTALANNUAL1832" target="_blank" class="inactive">Kennedy, ed., <span class="title-periodical-CONTINENTALANNUAL1832-italic">Continental Annual</span></a>, v, vi; and see 
                    <a href="../notes/annuals_note.php#LANDSCAPEANNUAL" target="_blank">Annuals and Other Illustrated Books: The Landscape Annual</a>).  
                    The locales of <span class="persName-SP">Prout</span>&#x02BC;s drawings engraved for the volume&#x2014;<span class="placeName-ANTWERP">Antwerp</span>, 
                    <span class="placeName-IGEL">Igel</span>, <span class="placeName-BRUSSELS">Brussels</span>, <span class="placeName-GHENT">Ghent</span>, 
                    <span class="placeName-NUREMNBERG">Nuremberg</span>, <span class="placeName-METZ">Metz</span>, <span class="placeName-TREVES">Treves</span>, 
                    <span class="placeName-DRESDEN">Dresden</span>, <span class="placeName-COMO">Como</span>, <span class="placeName-PADUA">Padua</span>, <span class="placeName-PRAGUE">Prague</span>, 
                    <span class="placeName-ROUEN">Rouen</span>, and <span class="placeName-CAEN">Caen</span>&#x2014; while 
                    anticipating the tour that forms the <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span> published the following year, are exhibited in entirely different scenes than those shown in the lithographs 
                    (with the exception of the <span class="placeName-IGEL">Igel</span> plate, which depicts a subject almost identical to <span class="title-sketch-PROUTPILLARIGEL">&#x201C;<span class="placeName-ROME">Roman</span> Pillar at <span class="placeName-IGEL">Igel</span>&#x201D;</span> in <span class="title-collected_sketches-PROUTSKETCHES-italic">Facsimiles</span>).
      </p>
      
