   Pen and ink, approx. 11 &#x00D7; 10.3 cm (image only). The editors of the <a href="../notes/library_edition_note.html" target="_blank" class="inactive">Library Edition</a> 
                        describe the image as &#x201C;a sketch of <span class="placeName-CALAISSQUARE">Calais Square</span>, or market&#x2010;place, with two figures&#x2014;a man and woman, 
                        and a child (? <span class="persName-JJR">J.J.R.</span>, <span class="persName-MR">M.R.</span>, and <span class="persName-JR">J.R.</span>)&#x2014;evidently 
                        British, at whom a Frenchman, who is wheeling a barrow near them, 
                        looks in amazement. The child has its hands uplifted in wonder, 
                        and is looking at the quaint buildings.&#x201D; (The editors also gloss the poem and prose sections of &#x201C;Calais𠇍 with a comparison of 
                        <span class="placeName-FRANCE">France</span> and <span class="placeName-ENGLAND">England</span> in 
                        <span class="title-book-JRPOETRYOFARCHITECTURE1893"><i>The Poetry of Architecture</i></span>, §16.) 
                        See <a href="../bibiliography/bibliography.html#JRWORKSLE" target="_blank" class="inactive">Ruskin, <span class="title-book-JRWORKSLE"><i>Works</i></span></a>, 2:342 n. 2, and see 1:14). 
                        In the editors&#x02BC; description, the second dash should be moved to follow &#x201C;woman,&#x201D; as there are obviously three British family figures.
                        As the third figure, the child, <span class="persName-JR">Ruskin</span> cannot have intended himself at age 14, since the child wears skirts and a bonnet. 
                        The child figure may be meant as his cousin, <a href="../notes/richardson_mary_note.html" target="_blank" class="inactive"><span class="persName-MRC">Mary Richardson</span> (1815&#x2013;49)</a>, 
                        but she was a young woman at this date. More likely, the drawing satirically depicts an English family emblematically, 
                        rotund and naive, contrasting with the skinny Frenchman pushing the wheelbarrow, and thus drawing on such nationalistic tropes as found in 
                        <span class="persName-WHPAINTER">William Hogarth</span>&#x02BC;s (<span class="date-1697-1764">1697&#x2013;1764</span>) 
                        <i>The Gate of Calais; or, The Roast Beef of Old England</i> (<span class="date-1748-1749">1748/49</span>),                      
                        yet the satire may bear some self-mockery.
                    
   
   
