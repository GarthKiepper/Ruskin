   <p>How much has been said of <span class="placeName-CALAIS">Calais</span>. Every one who has ever set his 
                    foot on the French shore, from poor <span class="name--">Yorick</span> to the veriest scribbler ever 
                    blotted paper, has written half a volume upon <span class="placeName-CALAIS">Calais</span>. And no marvel. 
                    <span class="placeName-CALAIS">Calais</span>&#x2014;the busy&#x2014;the bustling, the&#x2014;I had almost said the beautiful, for 
                    beautiful it was to me, and I believe to every one who enters it as a 
                    vestibule&#x2014;an introduction to <span class="placeName-FRANCE">France</span>, and to the French.<span class="glosses"><a href="../glosses/calais_glosses.php#INTRODUCTIONTOFRANCEGLOSS" target="_blank">1</a></span> 
                    See <span class="placeName-CALAIS">Calais</span>, 
                    and you can see no more, though you should perambulate <span class="placeName-FRANCE">France</span> from 
                    the <span class="geogName-ATLANTIC">Atlantic</span> to the <span class="geogName-MEDITERRANEAN">Mediterranean</span>. 
                    It is a little <span class="placeName-FRANCE">France</span>, a miniature<div id="" class="pb"></div>
                    picture, but not the less a resemblance. Stand on the pier and look round 
                    you. The sky is a French sky, it is a very turquoise, the sea is a French 
                    sea in everything but its want of motion, the air is French air, none of 
                    your English boisterous sea puffs that blow the dust in your eyes when 
                    you wish to be particularly clear sighted. No, it is a mere breath, you 
                    can&#x02BC;t call it a breeze, yet bearing a delicious, a balmy coolness, and a 
                    little, a very little smell of the sea. Look at the fishing boats, they are 
                    peculiarly French, and particularly clumsy. The red, tattered, shapeless 
                    sail, the undistinguishable resemblance of stem to stern, the porpoise like 
                    manner in which the vessel labours through the water, the incorrigible 
                    disorder that reigns on board, the confusion of fish out of water with 
                    men&#x2014;that are at least out of their element, would mark a French fishing&#x2010;boat 
                    whatever quarter of the world it might happen to be driven to.
   </p>
   
   <p>And look at the town; the chimneys are entirely vapourless, and have 
                    that peculiarly awkward look incident to all useless things. And look at 
                    the people; the countenance, the costume, the <i>tout ensemble</i> is altogether 
                    different from anything you ever saw in <span class="placeName-ENGLAND">England</span>, and yet <span class="placeName-ENGLAND">England</span>&#x02BC;s 
                    cliffs are on the horizon, half&#x2010;an&#x2010;hours&#x02BC; might see you beneath them.<span class="glosses"><a href="../glosses/calais_glosses.php#FOURHOURSGLOSS" target="_blank">2</a></span> It 
                    is most extraordinary.
