   Pen and ink, approx. 8 &#x00D7; 12.9 cm (image only). The editors of the <a href="../notes/library_edition_note.html" target="_blank" class="inactive">Library Edition</a> 
                        describe the image as a &#x201C;sketch of trees in the foreground on either side, and a town with three windmills in the distance&#x201D; 
                        (<a href="../bibiliography/bibliography.html#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:342 n. 3). 
                        Between the coulisses of the foreground foliage, the distant town sits atop an eminence, which suggests <span class="geogName-">Cassel Hill</span>, 
                        mentioned in the poem following the illustration. 
                        Two small figures occupy the foreground, one seated and one standing and pointing at the distant scene.
                    
   
   
