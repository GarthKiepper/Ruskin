   <div class="title" style="text-align:center">look at that ship</div>
   <br/>
   <span class="lg-stanza">
      <span class="l">her flying streamer and her mountain sides,<span class="glosses"><a href="../glosses/look_at_that_ship_glosses.php#BASELINEPUNCTUATION" target="_blank"" class="inactive">1</a></span></span><br/>
