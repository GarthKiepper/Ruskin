      <span class="l">of water hanging to her high buil<span class="unclear-faded-CWB"></span><span class="glosses"><a href="../glosses/look_at_that_ship_glosses.php#BUIL" target="_blank"" class="inactive">2</a></span> side</span><br/>
      <span class="l">glittering like diamonds dangling in the sun.</span><br/>
      <span class="l">yet soon it lessens lessens more and more,</span><br/>
      <span class="l">till to a speck reduced it vanishes complete</span><br/>
      <span class="l">and to the dangers of the sea exposed</span><br/>
      <span class="l"><span class="subst-del"><span class="gap-illegible" style="padding-left: 8em"></span></span><span class="subst-add-overwriting">the sailors look with</span> horror at the waves,<span class="glosses"><a href="../glosses/look_at_that_ship_glosses.php#THESAILORSLOOKWITH" target="_blank"" class="inactive">3</a></span></span><br/>
