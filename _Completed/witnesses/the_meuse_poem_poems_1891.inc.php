   <div class="title" style="text-align:center"><span class="caps">THE MEUSE</span>.
   </div>
   <span class="lg-stanza">
      <span class="l">T<span class="smallcaps">HE</span> sky was clear, the morn was gay</span><br/>
      <span class="l">In promise of a cloudless day.</span><br/>
      <span class="l">Fresh flew the breeze, with whose light wing</span><br/>
      <span class="l">Aspen and oak were quivering:</span><br/>
      <span class="l">From flow&#x02BC;ret dank it dashed the dew,&#x2014;</span><br/>
      <span class="l">The harebell bent its blossom blue,&#x2014;</span><br/>                     
      <span class="l">And from the <span class="geogName-MEUSE">Meuse</span> the mist&#x2010;wreaths<span class="glosses"><a href="../glosses/the_meuse_glosses.php#MISTWREATHSGLOSS" target="_blank">1</a></span> grey</span><br/>  
      <span class="l">That morning breeze had swept away,</span><br/>
      <span class="l">Showing such scenes as well might seem</span><br/>
      <span class="l">The fairy vision of a dream.<span class="glosses"><a href="../glosses/the_meuse_glosses.php#FAIRYVISIONGLOSS" target="_blank">2</a></span></span><br/> 
      <span class="l">For changing still, and still as fair</span><br/> 
      <span class="l">Rock, wave, and wood were mingled there;</span><br/> 
      <span class="l">Peak over peak, fantastic ever,</span><br/>                 
      <span class="l">The lofty crags deep chasms sever:</span><br/>
      <span class="l">And, grey and gaunt, their lichened head</span><br/> 
      <span class="l">Rose sheerly from the river&#x02BC;s bed,<span class="glosses"><a href="../glosses/the_meuse_glosses.php#PEAKOVERPEAKGLOSS" target="_blank">3</a></span></span><br/>
      <span class="l">Whose mantling wave, in foamy sheet,</span><br/>
      <span class="l">Their stern, projecting bases beat;</span><br/> 
      <span class="l">And, lashed to fury in his pride,</span><br/> 
      <span class="l">In circling whirlpools swept the tide,</span><br/>
      <span class="l">As threatening, on some future day,</span><br/>
      <span class="l">Those mighty rocks to tear away,&#x2014;</span><br/>
      <div id="" class="pb"></div>
      <span class="l">What though their front should seem to be</span><br/>
      <span class="l">A barrier, to eternity!<span class="glosses"><a href="../glosses/the_meuse_glosses.php#BARRIERGLOSS" target="_blank">4</a></span></span><br/>
      <span class="l">And on its side, the cliffs between,</span><br/>
      <span class="l">Were mazy forests ever seen,</span><br/>
      <span class="l">That the tall cliff&#x02BC;s steep flanks so grey</span><br/>
      <span class="l">Were clothed in mantle green and gay.</span><br/>
      <span class="l">Long time along that dell so deep,</span><br/>
      <span class="l">Beside the river&#x02BC;s bed, we sweep;</span><br/> 
      <span class="l">So steep the mighty crests inclined,</span><br/>
      <span class="l">None other pathway you might find;</span><br/>
      <span class="l">Till the tall cliff&#x02BC;s gigantic grace</span><br/>
      <span class="l">To undulating hills gave place,</span><br/>
      <span class="l">And vineyards clothe the bending brow,</span><br/>
      <span class="l">&#x02BC;Stead of the clinging copsewood now.</span><br/>
      </span>
   
</div>


