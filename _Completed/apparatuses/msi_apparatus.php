         <div id="TITLE" class="section">
            <span class="head-section">Title</span>
            
            <p>MS I</p>
            
            <p><a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> assigned
                    roman numeral I to this <a href="../notes/red_book_note.php" target="_blank">Red Book</a> in
                    his &#x201C;Preliminary Note on the Original MSS. of the Poems&#x201D;
                    (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>,
                    1:262; <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:263).
            </p>
            
            <p>Two decades earlier, <span class="persName-JR">Ruskin</span> himself had set the precedent for numbering the 
                    <a href="../notes/red_book_note.php" target="_blank">Red Books</a>, including this one: at
                    the bottom of <a href="../drawings/D4.jpg" target="_blank" class="inactive">&#x201C;Frontispiece&#x201D; &#x005B;Plate&#x005D; &#x201C;1&#x201D; &#x005B;&#x201C;Harry and Lucy&#x201D; Vol 1&#x005D;</a>,
                    he wrote &#x201C;Red book. No. 3. <span class="date-1870-09-08">Sept 8<sup>th</sup> 1870</span>.&#x201D; 
                    On the same day, he docketed <a href="../apparatuses/msiv_apparatus.php" target="_blank" class="inactive">MS IV</a> as 
                    &#x201C;<a href="../notes/red_book_note.php" target="_blank">Red book</a> No. 1,&#x201D; and 
                    <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a> as 
                    &#x201C;<a href="../notes/red_book_note.php" target="_blank">Red book</a> No. 2.&#x201D; For the meaning of this
                    numbering, see <a href="../notes/red_book_note.php" target="_blank">Red Books</a>.
            </p>
            
            <p>Apart from these later numerical designations, <span class="persName-JR">Ruskin</span>&#x02BC;s boyhood title&#x2014;intended
                    originally, it appears, for MS I in its entirety as well as for its main
                    work&#x2014;is &#x201C;<span class="caps">Harry and Lucy / Concluded / Being the Last / Part of / Early Lessons</span>
                    / in four volumes / vol I / with copper / plates / <span class="caps">Printed</span> and
                    composed by a little boy / and also drawn&#x201D; (see <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php#TITLE" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1: Title</a>). 
                    This title does not take account of the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a> at the end of
                    the manuscript, a feature in addition to &#x201C;Harry and Lucy&#x201D; that
                    <span class="persName-JR">Ruskin</span> may not have envisioned when he devised the title page. See 
                    <a href="#DISCUSSION">Discussion</a>, however, for
                    <span class="persName-JR">Ruskin</span>&#x02BC;s addition, following the anthology, of a colophon that does
                    account for the contents of MS I as a totality.
            </p>
            
            <p>See also <a href="../notes/title_citation_for_major_mss_note.php" target="_blank">System of Title Citation for Manuscripts</a>.
            </p>
            
