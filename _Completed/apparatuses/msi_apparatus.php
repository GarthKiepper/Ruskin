         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <div id="HAND" class="subsection">
               <span class="head-subsection">Hand</span>
               
               <p>MS I is written entirely in large penciled print, more childish than that in any other
                    <a href="../notes/red_book_note.php" target="_blank">Red Book</a>; nonetheless, <span class="persName-JR">Ruskin</span>
                    was already carefully imitating published books by justifying the margins;
                    devising a title page, frontispiece, and colophon; labeling his drawings as
                    &#x201C;plates&#x201D;; and filling out the volume with the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a>, which he
                    entitled &#x201C;Poetry.&#x201D;
               </p>
            </div>
            
            <div id="COMPSOURCES" class="subsection">
               <span class="head-subsection">Composition and Sources</span>
               
               <p>MS I shows more uniform control over the entirety of its contents than tended to
                    be the case in later <a href="../notes/red_book_note.php" target="_blank">Red Books</a>,
                    which grew more miscellaneous with successive uses at different times. For
                    example, the title page appears more nearly to serve for the entire manuscript than is the case in 
                    <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a> and <a href="../apparatuses/msiiia_apparatus.php" target="_blank" class="inactive">MS IIIA</a>, which likewise begin with volumes of
                    &#x201C;Harry and Lucy,&#x201D; but which also contain much else that is
                    altogether unrelated to the opening narrative.
               </p>
               
               <p>Similarly, the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a>,
                    which fills out MS I, appears less haphazard than poetry miscellanies in the other Red Books. The first of
                    these poems, <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a>,
                    condenses a portion of <i>The Botanic Garden</i> by <a href="../notes/darwin_erasmus_note.php" target="_blank"><span class="persName-ED">Erasmus Darwin</span></a>, thus
                    sustaining the <span class="date-1637-1815">Enlightenment</span>, entrepreneurial spirit of the immediately
                    preceding <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>, which imitates works by associates of <span class="persName-ED">Erasmus Darwin</span>, 
                    <a href="../notes/edgeworth_maria_note.php" target="_blank" class="inactive"><span class="persName-ME">Maria Edgeworth</span></a> and <a href="../notes/joyce_jeremiah_note.php" target="_blank" class="inactive"><span class="persName-JJ">Jeremiah Joyce</span></a>.
               </p>
               
               <p>As a visual artifact, MS I imitates the appearance of books <span class="persName-JR">Ruskin</span> would have
                    known: he modeled the title page directly on <a href="../bibiliography/bibliography.php#HARRYANDLUCYCONCLUDED" target="_blank" class="inactive">Maria Edgeworth, <i>Harry and Lucy Concluded</i></a>; 
                    and in the &#x201C;plates,&#x201D; he imitated any
                    number of books in the Ruskin family library (<span class="persName-ME">Edgeworth</span>&#x02BC;s volumes were
                    not illustrated). Most of <span class="persName-JR">Ruskin</span>&#x02BC;s &#x201C;plates&#x201D; bear no
                    apparent relation with the text, but the frontispiece,
                    <a href="../drawings/D4.jpg" target="_blank" class="inactive">&#x201C;Frontispiece&#x201D; &#x005B;Plate&#x005D; &#x201C;1&#x201D; &#x005B;&#x201C;Harry and Lucy&#x201D; Vol 1&#x005D;</a>, 
                    which features &#x201C;a rainbow,&#x201D; may be
                    complemented by the last of the poems in the anthology, <a href="../apparatuses/on_the_rainbow_apparatus.php" target="_blank">&#x201C;On the Rainbow: In Blank Verse.&#x201D;</a> The
                    rainbow drawing, moreover, could refer to Harry&#x02BC;s vision of the Witch of
                    the Alps at the end of <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>. <span class="persName-JR">Ruskin</span> could have added the rainbow to the
                    frontispiece as an afterthought: we cannot know for certain whether the textual
                    rainbow suggested the graphic one, or vice versa, or whether the two are
                    definitely related at all. But it seems likely that he arranged the contents in
                    order to frame the book with the theme of the rainbow as both promise and
                    closure.
               </p>
               
               <p>Another sign of control over the entirety of the contents appears at the end of
                    the Red Book, on p. 106, where <span class="persName-JR">Ruskin</span> placed a colophon:
                    &#x201C;The end / <span class="placeName-HERNE">hernhill</span> / <span class="placeName-FOUNTAINST">fountain street</span> / <span class="del">end of the poems</span> / juvenile library <span class="placeName-FOUNTAINST">fountain street</span>.&#x201D; The
                    colophon is followed by <a href="../drawings/D1.jpg" target="_blank" class="inactive">&#x201C;Heights of Wisdom, Depth of Fools&#x201D; &#x005B;Miscellaneous Drawing, MS I&#x005D;</a>, in
                    which the concave shape of the narrow valley appears to answer to the convex
                    form of the rainbow in the frontispiece. The colophon could be based on 
                    <a href="../bibiliography/bibliography.php#HARRYANDLUCYCONCLUDED" target="_blank" class="inactive">Maria Edgeworth, <i>Harry and Lucy Concluded</i></a>, which concludes in the fourth volume with
                    &#x201C;<span class="smallcaps">The End.</span>&#x201D; and, below that,
                    &#x201C;<span class="smallcaps"><span class="placeName-LONDON">London</span>:</span> / <span class="smallcaps">Printed by <span class="persName-CW">Charles Wood</span>,</span> / <span class="placeName-POPPINSCT">Poppin&#x02BC;s Court</span>, <span class="placeName-FLEETST">Fleet Street</span>.&#x201D;
               </p>
               
               <p><span class="persName-JR">Ruskin</span> evidently meant the colophon to unify the entire contents of MS I&#x2014;<a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>, 
                    along with the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a>&#x2014;but the
                    deletion within the colophon of &#x201C;end of the poems&#x201D; points to how his
                    attempts at symmetry and closure were ongoing decisions. It appears that the deleted phrase originally formed part of a colophon that <span class="persName-JR">Ruskin</span> meant to apply only to the poetry anthology, 
                    but that, by deleting the phrase, he caused to refer to the entire Red Book, not just the
                    poems (reading, in effect, &#x201C;The end / <span class="placeName-HERNE">hernhill</span> / <span class="placeName-FOUNTAINST">fountain street</span> / juvenile library <span class="placeName-FOUNTAINST">fountain street</span>&#x201D;). Another, slightly different scenario is possible: the fact that the final two
                    poems of the anthology fall after <span class="persName-MR">Margaret</span>&#x02BC;s annotation dating the whole of
                    MS I may indicate that <span class="persName-JR">Ruskin</span> added these poems (on pp.
                    103&#x2013;6) after his <span class="persName-MR">mother</span>&#x02BC;s dating, and as he did so, he deleted &#x201C;end of
                    the poems&#x201D; on p. 106 so as not to belie his addition. But this possibility seems contradicted by the placement of the colophon following all of the poems. 
                    It is also possible, of course, whatever the scenario, that the deletion was not made by <span class="persName-JR">Ruskin</span>, but by someone
                    else.
               </p>
               
            </div>
            
            <div id="DOMESTICSCENE" class="subsection">
               <span class="head-subsection">Domestic Scene</span>
               
               <p>MS I is the only <a href="../notes/red_book_note.php" target="_blank">Red Book</a> to locate
                    its imaginary publisher, the &#x201C;juvenile library,&#x201D; in
                    &#x201C;<span class="placeName-HERNE">hernhill</span> / <span class="placeName-FOUNTAINST">fountain street</span>,&#x201D; other Red Books mentioning simply <a href="../notes/herne_hill_note.php" target="_blank" class="inactive"><span class="placeName-HERNE">Herne Hill</span></a>. 
                    Could <span class="persName-JR">Ruskin</span> be referring to what <i>Praeterita</i> records as an early memory, the water
                    carts being filled in front of the house (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:21)?
                    In <i>Praeterita,</i> <span class="persName-JR">Ruskin</span> leaves unclear whether he observed
                    the carts from the windows of <span class="placeName-HERNE">Hern Hill</span> or of the <a href="../notes/hunter_street_note.php" target="_blank" class="inactive"><span class="placeName-HUNTERST">Hunter Street</span></a>
                    house&#x2014;where the family lived prior to <span class="placeName-HERNE">Herne Hill</span>, until <span class="date-1823-03">March 1823</span>
                    (see <a href="../bibiliography/bibliography.php#RUSKINSCAMBERWELL" target="_blank" class="inactive">Dearden, <i>Ruskin&#x02BC;s Camberwell</i></a>, 1). By the time <span class="persName-JR">Ruskin</span> first wrote about this
                    memory in <span class="date-1871">1871</span> (in <i>Fors Clavigera</i> 
                    &#x005B;<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>,
                    27:169&#x005D;), he might well have confused memories of the two locations; or the
                    colophon itself might have suggested such a memory to him, when he doubtless
                    reviewed his juvenilia for those later autobiographical writings. Unfortunately,
                    attempts have been unsuccessful to gain information about either a <span class="placeName-FOUNTAINST">Fountain Street</span>
                    or water mains in the vicinities of the two houses.
               </p>
