         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1826-09-1827-03-21">September 1826&#x2013;21 March 1827</span>.
            </p>
            
            <p>Toward the end of MS I, <a href="../notes/ruskin_margaret_note.php" target="_blank" class="inactive"><span class="persName-MR">Margaret Ruskin</span></a> 
                    wrote, &#x201C;this book begun about <span class="date-1826-09-1826-10">Sep<sup>t</sup> or Oct 1826</span> / finished about <span class="date-1827-01">Jan<sup>y</sup> 1827</span>&#x201D;
                    (see <a href="../apparatuses/mr_gloss_dating_msi_apparatus.php" target="_blank">Gloss on the Dating of MS I</a>). Since she inserted her annotation toward the
                    end, within the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a>, 
                    it is probable that <span class="persName-JR">Ruskin</span> devoted approximately the <span class="date-1827-01">first month of 1827</span>
                    to fair&#x2010;copying the poems (and possibly also composing some of them), whereas
                    he had spent the <span class="date-1826-09-22-1826-12-31">fall/winter 1826</span> composing and fair&#x2010;copying 
                    <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>, along with drawing its plates.
            </p>
            
            <p>Evidence for this sequence of events is supported by <span class="persName-JR">Ruskin</span>&#x02BC;s likely mimicry of
                    his <span class="persName-MR">mother</span>&#x02BC;s form of dating MS I, when he dated a 1&#x2010;leaf presentation copy of 
                    <a href="../apparatuses/the_ship_apparatus.php" target="_blank" class="inactive">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at That Ship&#x201D; &#x005B;1827&#x005D;</a> as
                    &#x201C;<span class="date-1827-02-1827-03"><span class="sic">febuary &#x005B;sic&#x005D;</span> or march 1827</span>,&#x201D; despite another hand, on
                    the reverse side, being confident in specifying the date as &#x201C;<span class="date-1827-02-16">16 Feb<sup>y</sup> 1827</span>&#x201D; 
                    (see <a href="../apparatuses/the_ship_apparatus.php#DATE" target="_blank" class="inactive">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at That Ship&#x201D; &#x005B;1827&#x005D;: Date</a>). 
                    It can easily be imagined that, on this manuscript in <span class="date-1827-02-10-1827-02-20">mid-February 1827</span>, <span class="persName-JR">Ruskin</span>
                    mimicked his <span class="persName-MR">mother</span>&#x02BC;s manner from only a few weeks earlier of dating MS I.
            </p>
            
            <p><span class="persName-MR">Margaret</span>&#x02BC;s annotation, depending on how one interprets it, possibly
                    suggests also that at least one of the poems, <a href="../apparatuses/the_needless_alarm_apparatus.php" target="_blank">&#x201C;The Needless Alarm,&#x201D;</a> was composed earlier than its
                    extant witness in MS I, while the rest of the poems may be dated no earlier than their
                    witnesses here (see <a href="../apparatuses/the_needless_alarm_apparatus.php#DATE" target="_blank">&#x201C;The Needless Alarm&#x201D;: Date</a>; and 
                    <a href="../apparatuses/the_steam_engine_apparatus.php#DATE" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;: Date</a>).
            </p>
            
            <p>Probably a few months after <span class="persName-MR">Margaret</span> entered her note dating the entire 
                    <a href="../notes/red_book_note.php" target="_blank">Red Book</a>, <span class="persName-JR">Ruskin</span> added 
                    <a href="../drawings/D1.jpg" target="_blank" class="inactive">&#x201C;Heights of Wisdom, Depth of Fools&#x201D; &#x005B;Miscellaneous Drawing, MS I&#x005D;</a> 
                    to the back inside endboard and dated the drawing <span class="date-1827-03-21">21 March</span>, although no year is given. This appears to establish the
                    latest date of <span class="persName-JR">Ruskin</span>&#x02BC;s work on the manuscript.
            </p>
            
            <p>Thus, in summary, while <span class="persName-JR">Ruskin</span> probably composed or at least fair&#x2010;copied the contents of the
                    manuscript <span class="date-1826-09-1827-03">between September 1826 and March 1827</span>, he may have composed some of
                    the contents earlier. In particular, <a href="../apparatuses/the_needless_alarm_apparatus.php" target="_blank">&#x201C;The Needless Alarm&#x201D;</a> may date from <span class="date-1826-01-1826-04">early 1826</span>; and
                    <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>, probably at least describes some events that occurred before <span class="date-1826-09-22-1826-12-21">fall 1826</span>,
                    if not actually drafted earlier. See the individual works for further discussion.
            </p>
            
            <p>See <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
            </p>
            
