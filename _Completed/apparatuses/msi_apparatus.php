         <div id="DESCRIPTION" class="section">
            <span class="head-section">Description</span>
            
            <p><a href="../notes/red_book_note.php" target="_blank">Red Book</a>. 9.5 &#x00D7; 15.2 cm. 42 leaves.
            </p>
            
            <div id="COLLATION" class="subsection">
               <span class="head-subsection">Collation</span>
               
               
               <p><a href="../apparatuses/harry_and_lucy_vol1_apparatus.php#TITLE" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1: Title</a> 
                    (inside front endboard) &#x002B; <a href="../drawings/D4.jpg" target="_blank" class="inactive">&#x201C;Frontispiece,&#x201D; &#x005B;Plate&#x005D; &#x201C;1,&#x201D; &#x005B;&#x201C;Harry and Lucy,&#x201D; Vol. 1&#x005D;</a> 
                    (1r) &#x002B; pp. (starting on 1v) 1&#x2013;29, &#x005B;30&#x2013;31&#x005D; (missing), 32&#x2013;43, 44&#x005B;<i>a</i>&#x005D;, 45, 44&#x005B;<i>b</i>&#x005D; 
                    (i.e., a second p. 44 given for expected p. 46), 47, 48&#x005B;<i>a</i>&#x005D;, 49&#x005B;<i>a</i>&#x005D;, 48&#x005B;<i>b</i>&#x005D;, 49&#x005B;<i>b</i>&#x005D;
                    (i.e., p. 48 and p. 49 each given twice&#x005D;, 50&#x2013;59, 80&#x2013;86 (p. 87 not missing, but page numbering skipped), 88&#x2013;106 &#x002B;
                    <a href="../drawings/D1.jpg" target="_blank" class="inactive">&#x201C;Heights of Wisdom, Depth of Fools&#x201D; &#x005B;Miscellaneous Drawing, MS I&#x005D;</a> (inside back endboard).
               </p>
               
               <p>A loose leaf (not counted in the collation) was inserted between the title page and 1r; it holds a clipping from <a href="../bibiliography/bibilography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> (8<sup>o</sup>, 1891)</a> 
                    describing MS I, along with brief remarks possibly in the hand
                    of <a href="../notes/wedderburn_note.php" target="_blank" class="inactive"><span class="persName-AW">Alexander Wedderburn</span></a>. It is
                    not clear in this case whether, for this purpose, an existing notebook leaf was used (e.g.,
                    whether <span class="persName-JR">Ruskin</span>&#x02BC;s &#x201C;frontispiece&#x201D; originally began on 2r,
                    following this leaf), or whether a piece of paper was trimmed to the 
                    <a href="../notes/red_book_note.php" target="_blank">Red Book</a>&#x02BC;s size and inserted
                    here. The latter is more probable, since similar insertions in other Red Books 
                    (e.g., <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a>) certainly formed no part of the original notebook.
               </p>
               
               <p>Despite <span class="persName-JR">Ruskin</span>&#x02BC;s erratic page numbering, no leaves appear to be missing,
                    except for pp. 30&#x2013;31, which cause a gap in the text. This missing leaf
                    probably included a &#x201C;plate,&#x201D; <a href="../drawings/D5.jpg" target="_blank" class="inactive">&#x005B;Plate 4, &#x201C;Harry and Lucy,&#x201D; Vol. 1&#x005D;</a>.
                    The leaf containing plate 5, <a href="../drawings/D6.jpg" target="_blank" class="inactive">&#x201C;Lucy&#x02BC;s Drawing Room,&#x201D; &#x005B;Plate&#x005D; &#x201C;5,&#x201D; &#x005B;&#x201C;Harry and Lucy,&#x201D; Vol. 1&#x201D;&#x005D;</a> (pp.
                    36&#x2013;37) was likewise torn out, although this leaf was reattached, using a
                    glued, perforated sticker (since the tear line fits the remaining stub, this
                    leaf was evidently not moved here from elsewhere). The gummed strip used to mend
                    this leaf&#x2014;resembling the border from a sheet of postage stamps&#x2014;resembles
                    one put to a similar use in proof sheets for <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a> (<a href="../bibiliography/bibliography.php#RFMS70" target="_blank" class="inactive">RF MS 70</a>). On this evidence,
                    one infers that <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> had a hand preserving MS I, an inference
                    coincident with his preparing the first bibliography of these manuscripts, the
                    &#x201C;Preliminary Note on the Original MSS. of the Poems&#x201D; 
                    (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1:261&#x2013;67; 
                    <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:262&#x2013;68).
               </p>
               
               <p><span class="persName-JR">Ruskin</span>&#x02BC;s erratic page numbering through p. 50 probably arose from
                    his attempt to correct earlier misnumbering. Erased earlier numbering remains
                    visible starting on the present p. 21 (&#x201C;19&#x201D; is the erased
                    number, an error since p. 19 had already appeared) and continuing consecutively
                    through p. 49&#x005B;<i>b</i>&#x005D; (i.e., the <i>second</i>
                    p. 49). Since the latter was written over top of &#x201C;47,&#x201D; it
                    appears that, at about this point, <span class="persName-JR">Ruskin</span> realized an error, went back to the
                    present p. 21, and began renumbering; however, he made some new errors and
                    possibly deliberately repeated pp. 48&#x2013;49 in an attempt to bring his
                    numbering in sync with following pages. The erratic numbering after p. 50
                    appears to have resulted from <span class="persName-JR">Ruskin</span> writing &#x201C;80&#x201D; for
                    &#x201C;60,&#x201D; and carrying on without correction. The missing p. 87 is
                    probably a mere oversight.
               </p>
               
            </div>
            
