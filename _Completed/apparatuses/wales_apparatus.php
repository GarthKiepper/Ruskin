         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>This poem is corroborated by other references to an <span class="date-1827">1827</span> Welsh journey, which occur in 
                        <a href="../apparatuses/ragland_castle_apparatus.php" target="_blank"><span class="title-poem-RAGLANDCASTLE">&#x201C;Ragland Castle&#x201D;</span></a> and 
                        <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2</a>. These works document the family&#x02BC;s visit 
                        to the <span class="placeName-WYE">Wye Valley</span>, where they toured 
                        <span class="placeName-TINTERN">Tintern Abbey</span> and <span class="placeName-RAGLAN">Raglan Castle</span> near <span class="placeName-MONMOUTH">Monmouth</span>. 
                        The poem, <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span>, cannot reflect impressions gathered during this journey, 
                        since it forms part of a series of letters addressed to <span class="persName-JJR">John James Ruskin</span> during his business travels extending from 
                        <span class="date-1827-04-25-1827-05-02">late April through mid&#x2010;May and possibly into early June 1827</span>
                        (see <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 157 n. 1, 167). 
                        Rather, <span class="persName-JR">Ruskin</span> must have meant the poem to anticipate this pleasure journey. By writing in <span class="date-1827-05">May 1827</span>, 
                        and pairing the poem with 
                        <a href="../apparatuses/spring_blank_verse_apparatus.php" target="_blank"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>, 
                        he was remarking the slipping away of spring without the family&#x02BC;s usual departure on a holiday following <span class="persName-JJR">John James</span>&#x02BC;s 
                        <span class="date-1827-05-10">May 10 birthday</span>. 
                        The reference in <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span> to &#x201C;whitened land&#x201D; 
                        perhaps suggests the chill of a long <span class="date-1827-12-21-1827-03-20">winter</span> giving way to spring. 
                        See <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>.
            </p>
            
            <p>The trip was delayed owing to toilsome business, which in <span class="date-1826-1827">1826&#x2013;27</span> had yielded scantier profits than usual, 
                        so that <span class="persName-JJR">John James</span> was kept traveling for orders until long after his birthday. 
                        He, too, was afflicted with wanderlust, looking to &#x201C;<span class="date-1827-03-20-1827-06-21">Spring</span> &#x0026; <span class="date-1827-06-21-1827-09-22">Summer</span> 
                        for new pleasures&#x201D; (see <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 152). 
                        Meanwhile, <span class="persName-MR">Margaret</span> was perhaps too ill for travel, although she looked forward to &#x201C;air and exercise&#x201D; as &#x201C;the best as well as the cheapest physicians.&#x201D; 
                        She was also busied with making arrangements for <span class="persName-JR">Ruskin</span>&#x02BC;s Scottish cousin, 
                        <a href="../notes/richardson_mary_note.php" target="_blank" class="inactive">Mary Richardson (1815&#x2013;49)</a>, 
                        who was staying at <span class="placeName-HERNE">Herne Hill</span> for an extended visit, although not yet permanently settled in the household 
                        (see <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 152).
            </p>
            
