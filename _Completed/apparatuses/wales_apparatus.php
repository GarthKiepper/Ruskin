         <div id="COMPPUB" class="section">
            <span class="head-section">Composition and Publication</span>
            
            <p><span class="persName-JR">Ruskin</span> gave the <span class="date-1827-05">May 1827</span> letter containing the poem to <span class="persName-CEN">Charles Eliot Norton</span>, 
                        enclosing the letter inside a note to <span class="persName-CEN">Norton</span> dated <span class="date-1869-02">February 1869</span>, 
                        and quipping that, &#x201C;while not a <span class="persName-GWASHINGTON">Washington</span> autograph,&#x201D; the boyhood manuscript represents 
                        &#x201C;evidently the first sketch of the Moral Theory of his work by the great author of &#x2018;Modern Painters.&#x2019;&#x201C; 
                        <span class="persName-CEN">Norton</span> included the letter and its covering note in a four&#x2010;part series, 
                        <span class="title-collected_letters-JRLETTERS">&#x201C;Letters of John Ruskin&#x201D;</span>, 
                        published in volumes 93&#x2013;94 (<span class="date-1904">1904</span>) of the <i>Atlantic Monthly</i> 
                        (<a href="../bibiliography/bibliography.php#JRLETTERS" target="_blank" class="inactive">&#x201C;Letters of John Ruskin,&#x201D; ed. Norton</a>, 94:164). 
                        Simultaneously, the letter and covering note appeared in 
                        <a href="../bibiliography/bibliography.php#JRLETTERSNORTON" target="_blank" class="inactive">Norton, ed., <i>Letters of John Ruskin to Charles Eliot Norton</i></a>, 1:197&#x2013;98. 
                        Published also in <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 36:2; 
                        and re&#x2010;edited using original punctuation in <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 158.
            </p>
            
