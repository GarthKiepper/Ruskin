         <div id="TITLE" class="section">
            <span class="head-section">Title</span>
            
            <p>&#x201C;Wales&#x201D;</p>
            
            <p><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span> in a copy included in a <span class="date-1827-05">May 1827</span> 
                        letter to his <span class="persName-JJR">father</span> (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 158); 
                        and as <span class="title-poem-WALES">&#x201C;WALES&#x201D;</span> in <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a>. See
                        <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
         </div>
         
         <div id="GENRE" class="section">
            <span class="head-section">Genre</span>
            
            <p>Poem; <a href="../notes/topographical_poetry_note.php" target="_blank" class="inactive">topographical poem</a>.
            </p>
            
            <p>Although no stanzas are indicated in manuscript, the 9&#x2010;line poem appears to work as two quatrains (<i>abab</i> and <i>ddee</i>)
                        turning on an intervening unrhymed line. The intervening line also shifts the meter from pentameter to quadrameter. Thus, the verse elements, like the
                        landscape elements <span class="persName-JR">Ruskin</span> describes &#x201C;seem as if they all did lock / into each other.&#x201D; 
                        Perhaps because he came to recognize this subtle invention, <span class="persName-CEN">Charles Eliot Norton</span> stepped back from his initial impulse 
                        to impose a clear division of stanzas on the poem (see 
                        <span class="glosses"><a href="../glosses/wales_glosses.php#LINE4STANZABREAK" target="_blank">1</a></span>).
            </p>
            
         </div>
         
         
