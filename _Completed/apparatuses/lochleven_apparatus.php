         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>The poem refers to the ruins of <span class="placeName-LOCHLEVENCAS">Loch Leven Castle</span>, which stand on one of the <span class="geogName-LOCHLEVEN">lake</span>&#x02BC;s islands, opposite
                    <span class="placeName-KINROSS">Kinross</span>. The Ruskins regularly passed near <span class="placeName-LOCHLEVEN">Loch Leven</span>, 
                    journeying north from <span class="placeName-EDINBURGH">Edinburgh</span> to <span class="placeName-Perth">Perth</span>, 
                    the home of <span class="persName-JR">Ruskin</span>&#x02BC;s <span class="persName-JRP">Aunt Jesse Richardson</span> and his Scottish cousins 
                    (as opposed to his English cousins by the same last name, Richardson, who lived in <span class="placeName-CROYDON">Croydon</span>). 
                    Between <span class="placeName-LOCHLEVEN">Loch Leven</span> and <span class="placeName-Perth">Perth</span>, 
                    the family passed through <a href="../notes/glenfarg_place_note.php" target="_blank"><span class="geogName-GLENFARG">Glenfarg</span></a>.
            </p>
            
            <p>This historic setting had recently been made famous by <span class="persName-WS">Walter Scott</span>&#x02BC;s novel, 
                    <span class="title-SCOTTABBOT">The Abbot</span> (<span class="date-1820">1820</span>), which used the events surrounding the imprisonment of 
                    <span class="persName-MQS">Mary, Queen of Scots</span>, in <span class="placeName-LOCHLEVENCAS">Loch Leven Castle</span> in <span class="date-1567">1567</span>,
                    and her forced abdication to her infant son, the future <span class="persName-KJ6">James VI</span>, followed by the queen&#x02BC;s 
                    dramatic escape from the castle in <span class="date-1568">1568</span>. According to <span class="persName-WS">Scott</span>&#x02BC;s annotation for the <span class="title-SCOTTMAGNUMOPUS">magnum opus</span> 
                    edition of the novel, <span class="persName-MQS">Mary</span> escaped in <span class="date-1568-05">May 1568</span>, aided by a young man, 
                    <span class="persName-WD">William Douglas</span>, who was probably related to the lords of the <span class="placeName-LOCHLEVENCAS">castle</span>. 
                    <span class="persName-WD">Douglas</span> stole the keys and freed the <span class="persName-MQS">queen</span> and a waiting
                    woman, escorting them &#x201C;out of the tower itself, &#x005B;and&#x005D; embarked with them in a small skiff, and rowed them to the shore&#x201D; 
                    (<a href="../bibiliography/bibliography.php#SCOTTWORKSCALEDONIAN" target="_blank" class="inactive">Scott, <i>Works</i>, Caledonian Edition</a>, 20:325). In the novel,
                    the role of <span class="persName-WD">Douglas</span> is taken by the young page, <span class="name--ROLANDSCOTTABBOT">Roland Graeme</span>. A tourist&#x02BC;s 
                    guide book of the period pictures the historical scene more melodramatically: &#x201C;under a load of misery which might have subdued a mind more masculine than 
                    <span class="sic">her&#x02BC;s &#x005B;sic&#x005D;</span>, <span class="persName-MQS">Mary</span> exerted the potent witchery of her charms upon the heart of young <span class="persName-WD">Douglas</span>, 
                    who, intoxicated with a romantic passion and ambitious hopes, sacrificed his duty and family interests at the shrine of all&#x2010;powerful love&#x201D; 
                    (<a href="../bibiliography/bibliography.php#SCOTTISHTOURIST" target="_blank" class="inactive"><i>The Scottish Tourist, and Itinerary</i></a>, 126).
            </p>
            
            <p>If <span class="persName-JR">Ruskin</span>&#x02BC;s poem reflects the family&#x02BC;s engagement with <span class="persName-WS">Scott</span>&#x02BC;s novel, 
                    the allusion predates <span class="persName-JR">Ruskin</span>&#x02BC;s versification of <span class="persName-WS">Scott</span>&#x02BC;s <span class="title-SCOTTMONASTERY">The Monastery</span> (<span class="date-1820">1820</span>),
                    the novel to which <span class="title-SCOTTABBOT">The Abbot</span> forms a sequel (see <a href="../apparatuses/the_monastery_apparatus.php" target="_blank" class="inactive"><span class="title-poem-THEMONASTERY">&#x201C;The Monastery&#x201D;</span></a>). 
                    <span class="persName-JJR">John James Ruskin</span> first recorded a purchase of <span class="title-SCOTTABBOT">The Abbot</span> in <span class="date-1829">1829</span> 
                    (too early for the version of the novel for the <span class="title-SCOTTMAGNUMOPUS">magnum opus</span> edition 
                    &#x005B;<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>,
                    188 n. 4; <a href="../bibiliography/bibliography.php#MILLGATESCOTTSLASTEDITION" target="_blank" class="inactive">Millgate, <i>Scott&#x02BC;s Last Edition</i></a>, 24&#x005D;), but this proves nothing about the family&#x02BC;s first reading of the story. 
                    Together, the novels rest on a historiography pointing favorably to the <span class="date-1707-01-16">Union of <span class="placeName-SCOTLAND">Scotland</span> and <span class="placeName-ENGLAND">England</span></span>
                    under a <span class="persName-KG1">Protestant king</span>. Kindred British sentiment is perhaps expressed in
                    <span class="persName-JR">Ruskin</span>&#x02BC;s poem, <a href="../apparatuses/the_defiance_of_war_msi_apparatus.php" target="_blank" class="inactive">&#x201C;The Defiance of War&#x201D;</a>.
            </p>
            
            <p>For a Romantic view of the <span class="placeName-LOCHLEVENCAS">castle</span> taken near the time of <span class="persName-JR">Ruskin</span>&#x02BC;s poem, see the engraving <i>Loch Leven Castle</i> by <span class="persName-JS">Joseph Swan</span>
                    after <span class="persName-JF">John Fleming</span> for 
                    (&#x005B;<span class="date-1839">1839</span>&#x005D;, 207 opp.), which shows, according to the letterpress by <span class="persName-JL">John Leighton</span>
                    (pp. 207&#x2013;12), the great square keep, adjoined by the entrance to the courtyard and the ruins of the chapel. In the distance, the sun sets over the
                    shore of the <span class="geogName-LOCHLEVEN">lake</span> and the <span class="placeName-OCHILS">Ochil Hills</span>, perhaps suggestive of <span class="persName-MQS">Mary</span>&#x02BC;s escape.
            </p>
            
            <p><span class="persName-JL">Leighton</span> also comments on the <span class="geogName-LOCHLEVEN">lake</span>&#x02BC;s celebrated trout, which accounts for
                    <span class="persName-JR">Ruskin</span>&#x02BC;s interest in fishing boats in his poem, an attraction almost as
                    great as the legends about <span class="persName-MQS">Queen Mary</span>.
            </p>
            
