            <div id="FAIRCOPY" class="subsection">
               <span class="head-subsection">Fair Copy (MS IX, MS VII, MS XI)</span>
               
               <p>For details about the content and layout of the composite&#x2010;genre version of the &#x201C;Account&#x201D; that <span class="persName-JR">Ruskin</span> realized in the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, 
                            see <a href="#SOURCES">Discussion: Sources and Influences</a>. 
                            <span class="persName-JR">Ruskin</span> assembled verse and prose from his MS IA and MS VIII draft, to form topographical units describing major destinations 
                            along the family&#x02BC;s tour. Each unit typically consists of a poem in the picturesque mode, followed by a prose piece in a comic or anecdotal mode, 
                            and with the writing encased in illustrations mimicking the appearance of engravings or lithographs, which <span class="persName-JR">Ruskin</span> drew on separate slips and pasted into place. 
                            He modeled the layout on the <span class="date-1830">1830</span> edition of the topographical poem, <i>Italy</i>, 
                            by <span class="persName-SR">Samuel Rogers</span>, an edition illustrated by engraved vignettes after drawings primarily 
                            by <span class="persName-JMWT">J. M. W. Turner</span> for landscapes, and by <span class="persName-TSP">Thomas Stothard</span> for figures, 
                            and also including minor illustrations by <span class="persName-SP">Samuel Prout</span> and others. 
                            In his own illustrations, <span class="persName-JR">Ruskin</span> imitates the appearance of these steel engravings, 
                            in some cases adopting <span class="persName-JMWT">Turner</span>&#x02BC;s manner for landscapes and <span class="persName-TSP">Stothard</span>&#x02BC;s for figures, according to his purposes in the &#x201C;Account.&#x201D; 
                            <span class="persName-JR">Ruskin</span> also imitates <span class="persName-SP">Samuel Prout</span>&#x02BC;s style of architectural drawing along with the watery appearance of lithography, 
                            a new medium that he discovered in <span class="persName-SP">Samuel Prout</span>&#x02BC;s <span class="date-1833">1833</span> <i>Facsimiles of Sketches Made in Flanders and Germany</i>. 
                            The text is lettered in a careful copperplate&#x2010;style hand, justified left and right.
               </p>
               
               <p>As argued in <a href="#DRAFTMSVIII">Draft: MS VIII</a>, <span class="persName-JR">Ruskin</span> started the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> no later than <span class="date-1834-01">January 1834</span>, 
                            and perhaps earlier, sometime during the <span class="date-1834-10-1834-12">final quarter of 1833</span>&#x2014;earlier, in any case, 
                            than <span class="date-1834-02">February 1834</span>, when he started on the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">draft of the &#x201C;Account&#x201D; in MS VIII</a>. As he 
                            worked on that draft from <span class="date-1834-02-1834-04">February through April</span>, leading up to his father&#x02BC;s <span class="date-1834-05-10">10 May 1834</span> birthday, 
                            and possibly somewhat beyond that date, the content of the draft gradually converged with the pace of <span class="persName-JR">Ruskin</span>&#x02BC;s fair&#x2010;copying in MS IX. 
                            While the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft of the &#x201C;Account&#x201D;</a> 
                            contains much that he never fair&#x2010;copied, <span class="persName-JR">Ruskin</span> apparently came to a point when he was almost simultaneously drafting the section of the &#x201C;Account&#x201D; 
                            that he was in process of fair&#x2010;copying. He had arrived at that point, when he broke off work altogether on the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>.
               </p>
               
               <p>This break occurred when <span class="persName-JR">Ruskin</span> had completed fair&#x2010;copying the poem, <a href="../showcase/heidelberg_poem_msix.php" target="_blank" class="inactive">&#x201C;Heidelberg&#x201D;</a>, 
                            and had started fair&#x2010;copying the first few lines of the corresponding prose section, <a href="../showcase/heidelberg_prose_msix.php" target="_blank" class="inactive">&#x201C;Heidelberg&#x201D;</a>. 
                            The draft of this prose piece occurs intermittently in the section of MS VIII that mingles draft of the &#x201C;Account&#x201D; with droft of birthday poems for <span class="persName-JJR">John James</span> 
                            (<a href="../apparatuses/msviii_apparatus.php#LISTB2" target="_blank">MS VIII: Contents, <i>b</i>.2</a>; 
                            see the drafts, <a href="../showcase/heidelberg_prose1_msviii.php" target="_blank" class="inactive">&#x201C;Cont. Heidelberg&#x201D; &#x005B;&#x201C;Heidelberg&#x201D; prose, part 1&#x005D;</a>, and 
                            <a href="../showcase/heidelberg_prose2_msviii.php" target="_blank" class="inactive">&#x201C;All has yielded to it from time immemorial&#x201D;  &#x005B;&#x201C;Heidelberg&#x201D; prose, part 2&#x005D;</a>). 
                            Just so, <span class="persName-JR">Ruskin</span>&#x02BC;s draft of the poem, <a href="../showcase/heidelberg_poem_msix.php" target="_blank" class="inactive">&#x201C;Heidelberg&#x201D;</a>, 
                            the last of the <i>completed</i> fair&#x2010;copied texts in MS IX, 
                            occurs intermittently toward the end of the section of of MS VIII that is devoted solely to draft of the &#x201C;Account&#x201D; 
                            (i.e., preceding the section that mingles draft of the &#x201C;Account&#x201D; with poetry in the making for <span class="persName-JJR">John James</span>&#x02BC;s birthday, 
                            <a href="../apparatuses/msviii_apparatus.php#LISTB1" target="_blank">MS VIII: Contents, <i>b</i>.1</a>;
                            see the drafts, <a href="../showcase/heidelberg_poem1_msviii.php" target="_blank" class="inactive">&#x201C;Smiling from those bright rays kiss&#x201D; &#x005B;&#x201C;Heidelberg&#x201D; poem, part 1&#x005D;</a>, and 
                            <a href="../showcase/heidelberg_poem2_msviii.php" target="_blank" class="inactive">&#x201C;Continuation Heidelberg&#x201D; &#x005B;&#x201C;Heidelberg&#x201D; poem, part 2&#x005D;</a>. 
                            In this same section of MS VIII, <a href="../apparatuses/msviii_apparatus.php#LISTB1" target="_blank">Contents, <i>b</i>.1</a>, 
                            <span class="persName-JR">Ruskin</span> was also composing the poem, <a href="../showcase/ehrenbreitstein_poem_msix.php" target="_blank" class="inactive">&#x201C;Ehrenbreitstein&#x201D;</a>, 
                            along with the poem, <a href="../showcase/heidelberg_poem_msix.php" target="_blank" class="inactive">&#x201C;Heidelberg&#x201D;</a>, 
                            indicating that at that point he was readying materials for fair&#x2010;copying the trio of sections that precede the &#x201C;Heidelberg&#x201C; section&#x2014;&#x201C;Andernacht,&#x201D; 
                            &#x201C;Ehrenbreitstein,&#x201D; and &#x201C;St. Goar.&#x201D;
               </p>
               
               <p>The cause of <span class="persName-JR">Ruskin</span>&#x02BC;s abruptly ceasing fair&#x2010;copying in MS IX is readily evident in the
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft of the &#x201C;Account&#x201D;</a>, in which 
                            <a href="../apparatuses/msviii_apparatus.php#LISTB2" target="_blank">MS VIII: Contents, <i>b</i>.2</a> runs up against draft for 
                            <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a>. <span class="persName-JR">Ruskin</span> did not so much abandon the &#x201C;Account&#x201D; 
                            as segue the project into his first professional publications, the poems for 
                            <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a>.
               </p>
               
               <p>Several blank spaces in the manuscript mark places for drawings, and one cannot determine the difference between a gap where a drawing has gone missing 
                            and one that never held anything. In <span class="date-1885">1885</span>, commenting on the manuscript in <i>Praeterita</i>, <span class="persName-JR">Ruskin</span> said both reasons were accountable 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:81). Moreover, 
                            there appears no way of knowing whether more drawings were lost (or restored) between <span class="date-1885-1903">1885 and 1903</span>, 
                            when <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> described the manuscript for the Library Edition.
               </p>
               
               <p>Folowing the recto (48r) containing the prose fragment for <a href="../showcase/heidelberg_prose_msix.php" target="_blank" class="inactive">&#x201C;Heidelberg&#x201D;</a>, 
                            no more text appears for the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, 
                            but on the following 7 rectos (49r&#x2013;55r) are pasted the 8 drawings described in 
                            <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:364 n. 1. 
                            All of these drawings depict mountain scenes. The blank versos facing each of these 7 pages would not have been adequate 
                            to contain verse and/or prose of the length exhibited in earlier sections. It appears, therefore, 
                            that someone used these blank rectos following the cessation of <span class="persName-JR">Ruskin</span>&#x02BC;s fair copying as a space to exhibit a gallery of vignettes, 
                            without the expectation that intervening text would ever be fair&#x2010;copied, 
                            even though draft for text exists in MS VIII relating to these kinds of mountain scenes&#x2014;and must have existed in the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence</a> prior to assembling this gallery of drawings.
               </p>
               
