               <div id="MENTORS" class="sub-subsection">
                  <span class="head-sub-subsection">Mentors</span>
                  
                  <p>In forming the persona <span class="persName-pen_name-JR">J.R.</span>, <span class="persName-JR">Ruskin</span> was influenced not only by literary and artistic sources, 
                                but also by living poets and artists 
                                whom he was fortunate to meet or correspond with during his conceptualization and composition of the &#x201C;Account.&#x201D; These contacts became especially important 
                                as <span class="persName-JR">Ruskin</span> was &#x201C;brought forward&#x201D; as a professional contributor to 
                                <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a> 
                                (see <a href="../bibiliography/bibliography.php#HANSONRUSKIN1830S" target="_blank" class="inactive">Hanson, &#x201C;Ruskin in the 1830s&#x201D;</a>). 
                                Two of these men present a striking contrast with one another&#x2014;<a href="../notes/rogers_samuel_note.php" target="_blank">Samuel Rogers (1763&#x2013;1855)</a> and 
                                <a href="../notes/hogg_james_note.php" target="_blank">James Hogg (ca. 1770&#x2013;1835)</a>. <span class="persName-JR">Ruskin</span> also had some contact at this time with 
                                <a href="../notes/loudon_j_c_note.php" target="_blank" class="inactive">John Claudius Loudon (1783&#x2013;1843)</a>.
                  </p>
                  
                  <p>The personae of <span class="persName-JH">Hogg</span> and <span class="persName-SR">Rogers</span> each exhibited multiple and even contradictory sides, and the Ruskins must have been selective 
                                about what aspects of these public figures they hoped to cultivate in the patronage of their precocious son. 
                                Respecting <span class="persName-JH">Hogg</span>, the Ruskins thought of him in his role as the <span class="persName-JH">Ettrick Shepherd</span>, who was romantically imagined as having received his call to poetry 
                                when he was shepherd in Ettrick Forest in the <span class="placeName-">Scottish Borders</span>, 
                                and who still resided in georgic retirement on his farm in the <span class="geogName-YARROWVALLEY">Yarrow Valley</span>. 
                                The Ruskins would have played down <span class="persName-JH">Hogg</span>&#x02BC;s colorful and risqu&#x00E9; character in the &#x201C;Noctes Ambrosianae&#x201D; 
                                in <i>Blackwood&#x02BC;s Edinburgh Magazine</i>. This side of <span class="persName-JH">Hogg</span> appealed to the Brontë siblings, 
                                who imitated the bravado and swagger of these dialogues in their own collaborative and competitive writing, 
                                but <span class="persName-JR">Ruskin</span>&#x02BC;s parents would hardly have considered such a persona as an appropriate model 
                                for <span class="persName-JR">Ruskin</span>&#x02BC;s solitary and decorous authorship.
                  </p>
                  
                  <p>When the Ruskins first met <span class="persName-JH">Hogg</span> personally in <span class="date-1832">1832</span>, he was undertaking his first visit to the metropolis of <span class="placeName-LONDON">London</span>, 
                                where was lionized by <span class="placeName-LONDON">London</span> society. <span class="persName-JH">Hogg</span>&#x02BC;s introduction to the Ruskins came by way of the community of expatriate Scottish literati in <span class="placeName-LONDON">London</span> 
                                who surrounded the publishing firms of <span class="orgName-MURRAY">John Murray</span> and <span class="orgName-SMITHELDER">Smith, Elder</span>. The latter, in particular, as the publisher of 
                                <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a>, 
                                served as the hub for these connections: <span class="persName-JH">Hogg</span> as a contributor to the annual; 
                                <a href="../notes/pringle_thomas_note.php" target="_blank">Thomas Pringle (1789&#x2013;1834)</a>, 
                                as editor, and longtime friend of <span class="persName-JH">Hogg</span>; and even <span class="persName-JR">Ruskin</span>&#x02BC;s cousin, 
                                <a href="../notes/richardson_charles_note.php" target="_blank" class="inactive">Charles Thomas Richardson (1811&#x2013;34)</a>, as a shopboy with the firm, 
                                who was the first to present <span class="persName-JR">Ruskin</span> with a copy of <i>Friendship&#x02BC;s Offering</i> (probably the <span class="date-1829">1829</span> 
                                or <span class="date-1830">1830</span> volume, both of which contained contributions by <span class="persName-JH">Hogg</span>).
                  </p>
                  
                  <p><span class="persName-SR">Rogers</span>, in sharp contrast with <span class="persName-JH">Hogg</span>, was the urbane &#x201C;man of taste,&#x201D; whose townhouse in <span class="placeName-STJAMESPLACE">St. James Square</span> 
                                was a showplace of exquisitely curated Regency neoclassicism, and the scene of his famous breakfasts in which <span class="persName-SR">Rogers</span> acted the part of social arbiter 
                                of the current <span class="placeName-LONDON">London</span> artistic and literary scene. In his most positive light, <span class="persName-SR">Rogers</span> might have appeared to the Ruskins as a splendid embodiment 
                                of <span class="persName-JJR">John James</span>&#x02BC;s tastes and aspirations. A former associate of <span class="persName-GGB">Byron</span>, and a habitu&#x00E9; of at least the byways of fame, 
                                <span class="persName-SR">Rogers</span> had earned a sound reputation for his topographical poem, <i>Italy</i>, 
                                which may have paled in comparison to the popular glare of <i>Childe Harold&#x02BC;s Pilgrimage</i>, 
                                but which was respectably and approachably neoclassical&#x2014;regular in its versification, and grounded in the tradition of the 
                                Grand Tour to <span class="placeName-ITALY">Italy</span>. <span class="persName-SR">Rogers</span> was also, like <span class="persName-JJR">John James</span>, a successful businessman, having made his fortune in banking; 
                                and even though he lavished extravagant sums on artists, engravers, and printers in order to satisfy his perfectionism in crafting editions of his poetry, 
                                <span class="persName-SR">Rogers</span> recouped his huge investment in his illustrated edition of <i>Italy</i> and profited handsomely by its unprecedented sales. 
                                The unattractive side of <span class="persName-SR">Rogers</span> was his notoriously acid wit and his worldliness at a time when middle&#x2010;class Evangelical earnestness 
                                was turning the tide on the influence of Regency &#x201C;men of taste.&#x201D;
                  </p>
                  
                  <p>The two writers also differed profoundly in taste. <span class="persName-SR">Rogers</span> was so committed to his neoclassicism that <span class="persName-JMWT">Turner</span> 
                                reined in his apocalyptic manner typical of his other works of the period in favor of a style that would harmonize with <span class="persName-SR">Rogers</span>&#x02BC;s restraint, 
                                by using less dramatic perspective in his vignette designs for <i>Italy</i> 
                                (<a href="../bibiliography/bibliography.php#HOLCOMBCLASSICALTURNER" target="_blank" class="inactive">Holcomb, &#x201C;Neglected Classical Phase of Turner&#x02BC;s Art&#x201D;</a>). 
                                The artist whom <span class="persName-JH">Hogg</span> most admired and hoped to secure as illustrator for his grandest gift book, a deluxe edition of <i>The Queen&#x02BC;s Wake</i>, 
                                was <span class="persName-JMARTIST">John Martin</span> (<span class="date-1789">1789</span>&#x2010;<span class="date-1854">1854</span>)&#x2014;the artist who, 
                                ever far from restraining the apocalyptic, made his career by exploiting such subjects for their most histrionic theatricality and scale 
                                (All too typically for the hapless author, and despite generous subscription donations 
                                by <span class="persName-JJR">John James Ruskin</span> and others, <span class="persName-JH">Hogg</span>&#x02BC;s deluxe edition was never produced owing to disruptions in trade caused by Reform agitation in <span class="date-1832">1832</span>; 
                                see <a href="../bibiliography/bibliography.php#OHALLORANILLUSTRATIONS" target="_blank" class="inactive">O&#x02BC;Halloran, &#x201C;Illustrations to <i>The Queen&#x02BC;s Wake</i>&#x201D;</a>, c&#x2010;civ.)
                  </p>
                  
                  <p>Of the two relationships, the Ruskins&#x02BC; contact with <span class="persName-JH">Hogg</span> is the best documented 
                                (see <a href="../notes/hogg_james_note.php" target="_blank">James Hogg (ca. 1770&#x2013;1835)</a>). 
                                Their apparent preference for the <span class="persName-JH">Ettrick Shepherd</span>&#x02BC;s advice about <span class="persName-JR">Ruskin</span>&#x02BC;s future as a poet, when the youth was laboring over the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy of the &#x201C;Account&#x201D;</a>, may be no accident. 
                                Compared to <span class="persName-SR">Rogers</span>, <span class="persName-JH">Hogg</span> may have seemed hapless in his fortunes: his <span class="placeName-LONDON">London</span> venture ended in him foolishly mislaying his trust in an under&#x2010;financed publisher, 
                                who went bankrupt and squandered the opportunity to produce a collected edition worthy of the author, whereas <span class="persName-SR">Rogers</span> boasted the means to encase his writing 
                                in increasing splendor. (In <span class="date-1834">1834</span>, when <span class="persName-JJR">John James</span> wrote to <span class="persName-JH">Hogg</span> about <span class="persName-JR">John</span>&#x02BC;s &#x201C;promise of very considerable talent,&#x201D; 
                                <span class="persName-SR">Rogers</span> followed up the success of his <span class="date-1830">1830</span> <i>Italy</i> with an edition of his collected <i>Poems</i>, 
                                likewise illustrated by <span class="persName-JMWT">Turner</span>, <span class="persName-TSP">Stothard</span>, and others, which the <i>Athenaeum</i> declared to be a &#x201C;volume which, 
                                for true elegance and pictorial fancy, is unequalled, in an age remarkable for its love of splendid books&#x201D; 
                                &#x005B;<a href="../bibiliography/bibliography.php#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 274; 
                                <a href="../bibiliography/bibliography.php#PIGGOTTTURNERSVIGNETTES" target="_blank" class="inactive">Piggott, <i>Turner&#x02BC;s Vignettes</i></a>, 39&#x005D;.) 
                                Nonetheless, when writing to <span class="persName-JH">Hogg</span>, <span class="persName-JJR">John James</span> sounds pointed in addressing <span class="persName-JH">Hogg</span> as a man &#x201C;of talent and of heart,&#x201D; 
                                as compared with &#x201C;the world at large,&#x201D; which lacks &#x201C;comprehen&#x005B;sion&#x005D; . . . patience . . . feeling . . . delicacy,&#x201D; 
                                suggesting that he trusted the humble Scottish &#x201C;shepherd&#x201D; over the urbane man of taste 
                                (<a href="../bibiliography/bibliography.php#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 274).
                  </p>
                  
                  <p>Despite <span class="persName-JJR">John James</span>&#x02BC;s sentiments, both <span class="persName-SR">Rogers</span>&#x02BC;s successes and <span class="persName-JH">Hogg</span>&#x02BC;s disappointments were endemic to a period of transition between publishing in the age of 
                                <span class="persName-WS">Scott</span> and the more entrepreneurial and innovative, yet also volatile and confusing print culture 
                                in the age of <span class="persName-CJHD">Dickens</span>. In a time when critical discourse commonly bemoaned the decline of poetry in unpropitious circumstances 
                                (<a href="../bibiliography/bibliography.php#BRISTOWVICTORIANPOETINTRO" target="_blank" class="inactive">Bristow, &#x201C;Introduction,&#x201D; <i>Victorian Poet</i></a>, 4&#x2013;5), 
                                both <span class="persName-SR">Rogers</span> and <span class="persName-JH">Hogg</span> appreciated how poetry could gain new life from its relation with the visual arts&#x2014;a 
                                relation that was rooted in tradition reaching back to the humanist dialogue or <i>paragone</i> between the sister arts, 
                                yet that was also undergoing renewal and transformation by mass&#x2010;market print technologies. Both men shrewdly exploited the changes in print technology 
                                that created new venues and audiences for literature and art, such as the <a href="../notes/annuals_note.php" target="_blank">annuals and other illustrated books</a> 
                                (on <span class="persName-JH">Hogg</span>&#x02BC;s enterprising approach to these venues during an otherwise depressed period in literary publishing, see 
                                <a href="../bibiliography/bibliography.php#CURRIEINTROHOGGANNUALS" target="_blank" class="inactive">Currie, introduction to Hogg, <i>Contributions to Annuals and Gift&#x2010;Books</i></a>, xix-xxxii;  
                                and for his appreciation of the fine arts&#x2014;particularly modern painting, for its connection with literature and its accessibility to ordinary people&#x2014;see 
                                <a href="../bibiliography/bibliography.php#HUGHESHOGGARTANNUALS" target="_blank" class="inactive">Hughes, &#x201C;Hogg, Art, and the Annuals&#x201D;</a>).
                  </p>
                  
                  <p>The sole anecdote to survive concerning <span class="persName-JR">Ruskin</span>&#x02BC;s audience with <span class="persName-SR">Rogers</span> can be read in terms of commonalities shared by these mentors 
                                in the literary and artistic culture of the <span class="date-1830-1839">1830s</span>. We have no contemporary documentation of <span class="persName-JR">Ruskin</span>&#x02BC;s introduction to <span class="persName-SR">Rogers</span> under <span class="persName-TP">Pringle</span>&#x02BC;s wing, 
                                other than evidence of <span class="persName-TP">Pringle</span>&#x02BC;s regular entr&#x00E9;e to <span class="placeName-STJAMESPLACE">St. James Place</span> 
                                (see <a href="../bibiliography/bibliography.php#VIGNEPRINGLE" target="_blank" class="inactive">Vigne, <i>Thomas Pringle</i></a>, 192). 
                                <span class="persName-JR">Ruskin</span>&#x02BC;s later story about his supposed contretemps of &#x201C;congratulating . . . &#x005B;<span class="persName-SR">Rogers</span>&#x005D; with enthusiasm 
                                on the beauty of the engravings by which his poems were illustrated,&#x201D; indicating that the youth knew &#x201C;more of the vignettes 
                                &#x005B;in <i>Italy</i>&#x005D; than the verses,&#x201D; and the chiding of <span class="persName-TP">Pringle</span> &#x201C;that, in future, 
                                when . . . in the company of distinguished men, . . . &#x005B;he&#x005D; should listen more attentively to their conversation&#x201D; 
                                evokes a bygone era that valued polite conversation and dilettantish colleccting 
                                (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 34:96, 35:93). 
                                Yet, while <span class="persName-JR">Ruskin</span> fits the anecdote into what he now regarded as his fatuous beginnings as a poet, his misplaced complements to the illustrator 
                                proves him to have been a boy of his decade, who had so carefully studied the visual culture represented by <span class="persName-SR">Rogers</span>&#x02BC;s <i>Italy</i> 
                                as well as by <span class="persName-SP">Prout</span>&#x02BC;s <i>Facsimiles</i>. (While we do not know when <span class="persName-JR">Ruskin</span>&#x02BC;s introduction to <span class="persName-SR">Rogers</span> occurred, 
                                it is a reasonable guess that <span class="persName-TP">Pringle</span> secured an audience on the strength of the youth&#x02BC;s precocious achievement in the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.xml" target="_blank" class="inactive">MS IX fair copy of the &#x201C;Account&#x201D;</a>.)
                  </p>
                  
                  <p>In the mentoring of <span class="persName-pen_name-JR">J.R.</span>, perhaps the most important qualification is indicated by a word dropped in the only 
                                mention of a publication by <span class="persName-JR">John</span> to survive in the family correspondence of <span class="date-1834">1834</span>. In a verse letter to his father dated <span class="date-1834-03-10">10 March 1834</span>, 
                                <span class="persName-JR">Ruskin</span> mentions&#x2014;not <span class="persName-TP">Pringle</span> and <i>Friendship&#x02BC;s Offering</i>&#x2014;but 
                                <a href="../notes/loudon_j_c_note.php" target="_blank" class="inactive">John Claudius Loudon (1783&#x2013;1843)</a>, the editor 
                                of botanical, landscaping, and other natural history publications. A key is that <span class="persName-JR">Ruskin</span> regarded <span class="persName-JCL">Loudon</span> as a &#x201C;friend&#x201D; of the family: 
                                &#x201C;To <span class="persName-JCL">Mr. Loudon</span>, as a friend, / By way of some communication, / Some kind of little lucubration / On any sort of observation, 
                                / Among the <span class="geogName-ALPS">Alps</span>, you know, / On Micaslate, or any slates, / Granite, and gold, or toads and snakes, / I think that I shall make a show&#x201D; 
                                (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 285). 
                                <span class="persName-JR">Ruskin</span> was alluding to his geological notes&#x2014;dated <span class="date-1834-03">March 1834</span>, but published later that year&#x2014;in <span class="persName-JCL">Loudon</span>&#x02BC;s 
                                <i>Magazine of Natural History</i>, 
                                <a href="../apparatuses/enquiries_on_the_causes_of_the_colour_of_the_water_of_the_rhine_apparatus.php" target="_blank" class="inactive">"Enquiries on the Causes of the Colour of the Water of the Rhine"</a>, and 
                                <a href="../apparatuses/facts_and_considerations_on_the_strata_of_mont_blanc_apparatus.php" target="_blank" class="inactive">"Facts and Considerations on the Strata of Mont Blanc, and on Some Instances of Twisted Strata Observable in Switzerland"</a>. 
                                One is struck by the apparent anomaly that the one forthcoming publication to merit <span class="persName-JR">Ruskin</span>&#x02BC;s attention&#x2014;if he was as yet apprised of his other firsts for this year, 
                                the poetry commissions for <i>Friendship&#x02BC;s Offering</i>&#x2014;were these geological observations, and later in life <span class="persName-JR">Ruskin</span> would find it convenient 
                                to stress his keenness of factual observation at the expense of his poetic effusion. Again, however, one can perceive commonalities. 
                                <span class="persName-JCL">Loudon</span>, like <span class="persName-TP">Pringle</span> and <span class="persName-JH">Hogg</span>, was a Scot&#x2014;a member of the extended &#x201C;family&#x201D; 
                                of Scottish literati in <span class="placeName-LONDON">London</span> who surrounded the boy wonder, many of whom visited 
                                <span class="placeName-HERNE">Herne Hill</span> in the <span class="date-1830-1839">1830s</span>. <span class="persName-JCL">Loudon</span>&#x02BC;s publications also depended on the thriving visual culture 
                                of illustrated books and magazines during the period, and they were perhaps not too distant from the poetry anthologies in their communal appraoch, 
                                inviting amateurs to contribute to forums such as the 
                                <a href="../notes/magazine_of_natural_history_note.php" target="_blank" class="inactive"><i>Magazine of Natural History</i></a> 
                                and trade &#x201C;lucubrations&#x201D; that did not necessarily depend on professional status.
                  </p>
                  
                  
