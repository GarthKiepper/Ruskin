         <div id="TITLE" class="section">
            <span class="head-section">Title</span>
            
            <p>In <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a>, 
                        <span class="persName-WGC">W. G. Collingwood</span> printed the title, 
                        Account of a Tour on the Continent, without square brackets, his symbol for declaring editorial intervention. 
                        Nonetheless, the title apparently originated with him, as <span class="persName-JR">Ruskin</span> assigned no title to the 
                        sequence as a whole in any of the extant manuscripts 
                        (see <a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1: 119, 281; 
                        <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1: 119, 282). 
                        What titles <span class="persName-JR">Ruskin</span> supplied apply only to individual verse 
                        and prose sections of the work. Accordingly, in <i>ERM</i>, 
                        when the main title is fully spelled out, it is given without quotation 
                        marks, in keeping with the guidelines given in <a href="../notes/title_citation_note.php" target="_blank">The System of Title Citation for Works</a>. 
                        In this apparatus, however, for convenience in the course of discussion, a short title is used with quotation marks (i.e., the &#x201C;Account&#x201D;) 
                        in order to avoid confusion with surrounding text.
            </p>
            
            <p>In another, more minor discrepancy respecting the editorial title, both 
                        <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a> and the 
                        <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a> cite the title in their respective tables of contents as 
                        &#x201C;Account of a Tour on the Continent in <span class="date-1833">1833</span>,&#x201D; but the title pages of the work in these editions lack 
                        the extension containing the date 
                        (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1:xv, 119; 
                        <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:xix, 119; 
                        <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:xii, 340).
            </p>
            
            <p><span class="persName-WGC">Collingwood</span> also invented titles for some individual sections of verse, untitled sections that he took from 
                        <span class="persName-JR">Ruskin</span>&#x02BC;s  
                        <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft of the &#x201C;Account&#x201D;</a> in 
                        order to supplement the main copytext of poems taken from the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>. 
                        These editorial titles are as follows:
                        
               <ul>
                  
                  <li>TO COME</li>
                  
                  
               </ul>
               
            </p>
            
            <p>When two selections from the &#x201C;Account&#x201D; were published in the literary annual, 
                        <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a> 
                        (see <a href="COMPPUB">Composition and Publication</a>), 
                        the selections were subsumed together under the title, &#x201C;Fragments from a Metrical Journal&#x201D; 
                        (<a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a>, 317&#x2013;19). 
                        The responsibility for assigning this title is unknown.
            </p>
            
         </div>
         
         <div id="GENRE" class="section">
            <span class="head-section">Genre</span>
            
            <p>Composite verse and prose illustrated travelogue. As proposed in 
                        <a href="COMPPUB">Composition and Publication</a>, 
                        <span class="persName-JR">Ruskin</span> and his <span class="persName-JJR">father</span> may originally have conceived the genre 
                        of the work as a travelogue solely in verse&#x2014;a version represented by a manuscript now bound in <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a>, 
                        and designated in <i>ERM</i> as 
                        <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>. 
                        The work returned to this characterization as an all&#x2010;verse travelogue, when selections were excerpted and revised under the title, 
                        <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> 
                        (see <a href="#TITLE">Title</a>). By this time, however, the scenario of a 
                        &#x201C;Metrical Journal&#x201D; was imaginary, since <span class="persName-JR">Ruskin</span> had developed the work into a composite 
                        genre that included prose and graphic elements, as well as poetry, in its <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX version</a>.
            </p>
            
         </div>
         
         <div id="MAJORMS" class="section">
            <span class="head-section">Manuscripts</span>
            
            <p><a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a>, 
                        <a href="../apparatuses/msvii_apparatus.php" target="_blank" class="inactive">MS VII</a>, 
                        <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a>, 
                        <a href="../apparatuses/msix_apparatus.php" target="_blank" class="inactive">MS IX</a>, 
                        <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive">MS XI</a>.
            </p>
            
            <p>Facsimile and transcript by permission of Beinecke Rare Book and Manuscript Library, Yale University.</p>
            
            <p><a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a> and <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> contain draft 
                        of verse and prose sections for the &#x201C;Account,&#x201D; and <a href="../apparatuses/msix_apparatus.php" target="_blank" class="inactive">MS IX</a> contains 
                        <span class="persName-JR">Ruskin</span>&#x02BC;s fair copy of the composite genre version. 
                        <a href="../apparatuses/msvii_apparatus.php" target="_blank" class="inactive">MS VII</a> and <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive">MS XI</a> include fair copy 
                        of a few sections in hands other than <span class="persName-JR">Ruskin</span>&#x02BC;s.
            </p>
            
            <p><a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a> contains two distinct manuscripts related to the &#x201C;Account,&#x201D; 
                        designated in <i>ERM</i> as 
                        <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, and 
                        <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, respectively. 
                        When <span class="persName-WGC">W. G. Collingwood</span> was preparing 
                        <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a>, 
                        he was apparently aware of both of these manuscripts, although they had not yet been bound in <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a>. 
                        Among manuscripts currently extant, <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                        is the only known source of two poems, <a href="../showcase/passing_the_alps_poem_msia_g2.php">&#x201C;Passing the Alps&#x201D;</a> 
                        and <a href="../showcase/milan_cathedral_poem_msia_g2.php">&#x201C;Milan Cathedral&#x201D;</a>, and <span class="persName-WGC">Collingwood</span> 
                        includes both of these poems in his version of the &#x201C;Account.&#x201D; 
                        His aquaintance with <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a> is equally probable, 
                        since he draws on what he calls the &#x201C;original&#x201D; draft of the poem, &#x201C;Calais,&#x201D; 
                        selecting lines that exist only in this version (see <span class="glosses"><a href="../glosses/calais_poem_glosses#CALAISPOEMLINE2" target="_blank"" class="inactive">1</a></span>).
            </p>
            
            <p><span class="persName-WGC">W. G. Collingwood</span> is inconsistent and seemingly arbitrary in 
                        his reliance on these &#x201C;originals,&#x201D; however. 
                        When printing what he calls the &#x201C;original draft&#x201D; of the poem &#x201C;Andernacht&#x201D; and the &#x201C;first draft&#x201D; of the poem &#x201C;St. Goar,&#x201D; 
                        his choice among variants shows that he adopted copytext from 
                        <span class="persName-JR">Ruskin</span>&#x02BC;s fair copies in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a>&#x2014;and 
                        not from these poems&#x02BC; earliest extant drafts, found in 
                        <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a> 
                        (<a href="../showcase/andernacht_poem_msia.php" target="_blank" class="inactive">&#x201C;Andernacht&#x201D;</a> and 
                        <a href="../showcase/st_goar_poem_msia.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a>; 
                        see <a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> (4<sup>o</sup>, 1891)</a>, 1: 280&#x2013;83 n. 28, n. 30, and n. 32, and 
                        <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> (8<sup>o</sup>, 1891)</a>, 1: 282 n. 28, 283&#x2013;85 n. 30 and n. 32). 
                        Likewise inconsistent in his approach to <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                        he chooses arbitrarily between lines of <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a> as originally composed in the hand of <span class="persName-JJR">John James Ruskin</span> 
                        and revisions in the hand of <span class="persName-JR">John Ruskin</span>&#x2014;and at one point, he even invents 
                        a line of his own rather than accept either authority.
            </p>
            
            <p>The miscellaneous contents of <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a> were collected and bound 
                        under the auspices of the editors of the 
                        <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a>, 
                        after <span class="persName-WGC">Collingwood</span> published 
                        <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a> 
                        (see <a href="../apparatuses/msia_apparatus.php#PROVENANCE" target="_blank" class="inactive">MS IA: Provenance</a>). 
                        <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> made greater use of these earlier versions of the "Account" than did <span class="persName-WGC">Collingwood</span>. 
                        In their editing of <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a>, they 
                        refer to <span class="persName-JR">Ruskin</span>&#x02BC;s &#x201C;father&#x02BC;s copy&#x201D; 
                        of this poem, ascribing the text to <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>; 
                        and they ascribe their copytext of <a href="../showcase/st_goar_poem_msia.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a> 
                        to the manuscript <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>. 
                        This suggests, incidentally, that 
                        <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a> had been bound by 1903.
            </p>
            
         </div>
         
         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <div id="DATEOFCOMPOSITION" class="subsection">
               <span class="head-subsection">Date of Composition</span>
               
               <p>None of the manuscripts is dated by <span class="persName-JR">Ruskin</span>. Presumably, 
                            he began the work no earlier than the family&#x02BC;s departure for the <span class="geogName-CONTINENT">Continent</span> shortly after 
                            <span class="persName-JJR">John James Ruskin</span>&#x02BC;s 
                            <span class="date-1833-05-10-1833-05-31">10 May 1833</span> 
                            birthday, and it is possible that he began it no earlier than following 
                            their return home in <span class="date-1833-09-20-1833-09-30">late September 1833</span>. 
                            Neither of these possible termini a quo can be ruled out by the physical evidence of what is likely the earliest extant manuscript, 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>. 
                            This manuscript, which is in the hands of both father and son, 
                            could even have been begun during the journey itself (see <a href="COMPPUB">Composition and Publication</a>).
               </p>
               
               <p>If one argues a terminus a quo for <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                            as the return home to <span class="placeName-HERNE">Herne Hill</span>, that date would be after 
                            , which was the day that the
                            family reached <span class="placeName-BOULOGNE">Boulogne</span>,
                            according to <span class="persName-JJR">John James</span>&#x02BC;s travel
                            diary (<a href="../bibiliography/bibliography.php#RFMS33A" target="_blank" class="inactive">Diary of John James Ruskin, 1833&#x2013;46</a>, p. 74). 
                            A few more days would have been required in order to transact 
                            business and cross the <span class="geogName-ENGLISHCHANNEL">Channel</span> at 
                            <span class="placeName-CALAIS">Calais</span>.
               </p>
               
               <p>It is likely that <span class="persName-JR">Ruskin</span> carried out 
                            the bulk of the composition and fair copying (especially the composite 
                            genre version) between <span class="date-1833-10">October 1833</span> and 
                            <span class="date-1834-08">August 1834</span>, the latest date when he could have delivered his contribution, 
                            <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, along with a separate poem, 
                            <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">Saltzburg</a> (previously misdated as <span class="date-1835-12">December 1835</span>), 
                            to <a href="../notes/pringle_thomas_note.php" target="_blank">Thomas Pringle (1789&#x2013;1834)</a> the editor of 
                            <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a> 
                            for publication (see <a href="COMPPUB">Composition and Publication</a>). 
                            It is possible and even likely that, in order to devote his attention to these exciting commissions, 
                            <span class="persName-JR">Ruskin</span> ceased steady work on his 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> of the &#x201C;Account&#x201D; 
                            some months earlier than the <span class="date-1834-08">August</span> deadline&#x2014;perhaps abandoning the fair copy in 
                            approximately <span class="date-1834-05">May</span>.
               </p>
               
               <p>Strong evidence in <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> shows that 
                            <span class="persName-JR">Ruskin</span> labored on both draft and fair copy of the &#x201C;Account&#x201D; in <span class="date-1834">1834</span>, 
                            through the period when he normally prepared anniversary poetry for his father&#x02BC;s May birthday&#x2014;the poems, namely, 
                            <a href="../apparatuses/the_address_apparatus.php" target="_blank" class="inactive">&#x201C;The Address&#x201D;</a> and 
                            <a href="../apparatuses/the_vintage_apparatus.php" target="_blank" class="inactive">&#x201C;The Vintage&#x201D;</a>, along with another poem, 
                            <a href="../apparatuses/the_crystal_hunter_apparatus.php" target="_blank" class="inactive">&#x201C;The Crystal Hunter&#x201D;</a>, which he mentions in a 
                            <span class="date-1834-02-22">22 February 1834</span> letter to his father as having been &#x201C;brought to a standstill, else should be sent&#x201D; 
                            (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 280; and see 
                            <a href="../apparatuses/msviii_apparatus.php#LISTB2" target="_blank">MS VIII: Contents, <i>b</i>.2</a>). 
                            Some very tenuous evidence exists that he extended work on the project until much later into the year, past <span class="date-1834-08">September 1834</span>: 
                            in the prose section of <a href="../showcase/brussels_prose_msix" target="_blank" class="inactive">&#x201C;Brussels</a>&#x201D;, <span class="persName-JR">Ruskin</span>&#x02BC;s 
                            description of a distant view of the city resembles features in an engraved view of the subject 
                            based on a drawing by <span class="persName-JMWT">Turner</span>, an engraving that <span class="persName-JR">Ruskin</span> could not have seen prior to its publication 
                            in <span class="date-1834-09">September 1834</span> (see the gloss on <span class="persName-JR">Ruskin</span>&#x02BC;s description of the cathedral towers in 
                            <a href="../showcase/brussels_prose_part1_msviii.php" target="_blank" class="inactive">&#x201C;Part of Brussels&#x201D;</a>). 
                            However, the similarity of <span class="persName-JR">Ruskin</span>&#x02BC;s 
                            and <span class="persName-JMWT">Turner</span>&#x02BC;s prospects could be coincidental, 
                            whereas the evidence for dating the draft of the prose section of <a href="../showcase/brussels_prose_msix" target="_blank" class="inactive">&#x201C;Brussels</a>&#x201D;, 
                            which is included in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>, 
                            weighs heavily in favor of a date between <span class="date-1834-02">February</span> through <span class="date-1834-05">May 1834</span>.
               </p>
               
               <p><span class="persName-JR">Ruskin</span> probably started this <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft</a> 
                            for the &#x201C;Account&#x201D;&#x2014;draft consisting largely of poems relating to the Alpine and Italian stages of the tour, 
                            along with prose relating to the Belgian and German stages&#x2014;around the time of his <span class="date-1834-02-08">8 February 1834 birthday</span>.  
                            This important stint of draft can therefore be dated approximately between 
                            <span class="date-1834-02-08">8 February</span> and <span class="date-1834-05-10">10 May</span>. 
                            The stint terminates with draft of <span class="persName-JR">Ruskin</span>&#x02BC;s ekphrastic and topographical poem, 
                            <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a>, which was commissioned for 
                            <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a>. 
                            According to <span class="persName-WGC">W. G. Collingwood</span>, the latter poem dates from <span class="date-1835">1835</span>, 
                            so that <span class="persName-JR">Ruskin</span> must have inserted its draft later on a blank page following the <span class="date-1834">1834</span> sequence; 
                            however, in <a href="COMPPUB">Composition and Publication</a>, it is argued that the draft 
                            of <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a> 
                            falls in a logical place in <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a>, as belonging to <span class="date-1834">1834</span>. 
                            Its placement at the point where the  
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII &#x201C;Account&#x201D; draft</a> abruptly ends suggests that the commission of 
                            <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a> 
                            for <i>Friendship&#x02BC;s Offering</i> distracted 
                            <span class="persName-JR">Ruskin</span> from the &#x201C;Account,&#x201D; and he probably never returned to the project with sustained energy. 
                            No available evidence explains the timing of this commission relative to that for <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> in the same volume 
                            of <i>Friendship&#x02BC;s Offering</i>.
               </p>
               
               <p>A benchmark often connected with <span class="persName-JR">Ruskin</span>&#x02BC;s composition of the &#x201C;Account&#x201D; 
                            is his acquisition of the <span class="date-1830">1830</span> illustrated edition of the topographical poem 
                            <i>Italy</i> by <a href="../notes/samuel_rogers_note.php" target="_blank" class="inactive">Samuel Rogers (1763&#x2013;1855)</a>. 
                            According to his story in the autobiography, <i>Praeterita</i>, it was the gift of this book for <span class="persName-JR">Ruskin</span>&#x02BC;s 
                            <span class="date-1832-02-08">1832 birthday</span> along with the family&#x02BC;s acquisition by subscription in 
                            <span class="date-1833-04">April 1833</span> of <i>Facsimiles of Sketches Made in <span class="placeName-FLANDERS">Flanders</span> and <span class="placeName-GERMANY">Germany</span></i> by 
                            <a href="../notes/prout_samuel_note.php" target="_blank"><span class="persName-SP">Samuel Prout (<span class="date-1783">1783</span>&#x2013;<span class="date-1852">1852</span>)</span></a> 
                            that prompted the <a href="../notes/tour_of_1833_note.php" target="_blank">Tour of 1833</a> 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35: 79; and see 
                            <a href="#DISCUSSION">Discussion</a>). 
                            The Ruskins did bring home <span class="persName-SP">Prout</span>&#x02BC;s book in <span class="date-1833-04">April 1833</span>, but it is absurd to imagine that it 
                            sponsored <span class="persName-MR">Margaret</span>&#x02BC;s suggestion that the family &#x201C;should . . . go and see some of &#x005B;the wonderful places&#x005D; in reality,&#x201D; 
                            and that forthwith &#x201C;there were two or three weeks of entirely rapturous and amazed preparation&#x201D; for such a journey. 
                            As <span class="persName-JDHUNT">John Dixon Hunt</span> points out, a letter by <span class="persName-JR">Ruskin</span> 
                            from <span class="date-1831-03-21">21 March 1831</span> indicates that the Ruskins had been planning their 
                            &#x201C;Switzerlandish outlandish tour to <span class="placeName-ITALY">Italy</span>&#x201D; for at least two years 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35: 79; 
                            <a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 286 n. 1, 253; and 
                            <a href="../bibiliography/bibliography.php#WIDERSEA" target="_blank" class="inactive">Hunt, <i>Wider Sea</i></a>).
               </p>
               
               <p>The more elusive date is when the Ruskins acquired the <span class="date-1830">1830</span> edition of 
                            <i>Italy</i>. The question is of some interest, since it is argued in <a href="#COMPPUB">Composition and Publication</a> 
                            that <span class="persName-JR">Ruskin</span>&#x02BC;s &#x201C;Account&#x201D; underwent a development similar to that of 
                            <span class="persName-SR">Rogers</span>&#x02BC;s <i>Italy</i>&#x2014;evolving from an all&#x2010;verse to a composite&#x2010;genre work. 
                            Unfortunately, the evidence is too contradictory to be able to align the family&#x02BC;s acquisition of <span class="persName-SR">Rogers</span>&#x02BC;s 
                            <span class="date-1830">1830</span> illustrated edition with 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s reworking of the &#x201C;Account.&#x201D; In the autobiography, 
                            <span class="persName-JR">Ruskin</span> himself admits that he has 
                            &#x201C;told this story so often&#x201D; about the influence of <span class="persName-SR">Roger</span>&#x02BC;s illustrated edition 
                            and its revelation of <span class="persName-JMWT">Turner</span>&#x02BC;s art, &#x201C;that I begin to doubt its time.&#x201D; 
                            In <i>Praeterita</i>&#x02BC;s typically dramatizing fashion, <span class="persName-JR">Ruskin</span> declares that 
                            &#x201C;the main tenor of my life&#x201D; was set on <span class="date-1832-02-08">8 February 1832</span> by the birthday gift from his father&#x02BC;s business partner, 
                            <a href="../notes/telford_henry_note.php" target="_blank" class="inactive">Henry Telford (d. 1859)</a>. 
                            &#x201C;It is curiously tiresome that Mr. <span class="persName-HT">Telford</span> did not himself write my name in the book, 
                            and my father, who writes in it, &#x2018;The gift of <span class="persName-HT">Henry Telford</span>, Esq.,&#x2019;  
                            still more curiously, for him, puts no date: if it was a year later &#x005B;i.e., <span class="date-1833-02-08">8 February 1833</span>&#x005D;, no matter&#x201D; 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35: 79). 
                            In fact, the possible range is even wider. 
                            In a non&#x2010;illustrated edition, <span class="persName-SR">Rogers</span>&#x02BC;s poem had been in the <span class="persName-JR">Ruskin</span> household 
                            since <span class="date-1828">1828</span> at the latest 
                            (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 188 n. 4). 
                            This happens to be the year when <span class="persName-SR">Rogers</span> published &#x201C;Part the Second&#x201D; of the poem; 
                            and growing disgusted over the work&#x02BC;s tepid reception, he bought out remaining copies of both that volume and &#x201C;Part the First,&#x201D; and destroyed them 
                            (<a href="../bibiliography/bibliography.php#ROGERSITALY1828" target="_blank" class="inactive">Rogers, <i>Italy</i> (1828)</a>; 
                            <a href="../bibiliography/bibliography.php#ROGERSITALY1823" target="_blank" class="inactive">Rogers, <i>Italy</i> (1823)</a>; 
                            and see <a href="../notes/samuel_rogers_note.php" target="_blank" class="inactive">Samuel Rogers &#x005B;1763&#x2013;1855&#x005D;</a>). 
                            Thus, even before <span class="persName-SR">Rogers</span>&#x02BC;s poem achieved popularity in the illustrated edition, 
                            the Ruskins could count themselves among the select number of appreciative readers of the work in its original form, 
                            and it is possible that <span class="persName-JR">Ruskin</span> and <span class="persName-JJR">his father</span> 
                            projected a work on this unillustrated verse model prior to <span class="persName-JR">Ruskin</span>&#x02BC;s receiving the gift of the illustrated version.
               </p>
               
               <p>When, then, did <span class="persName-JR">Ruskin</span> first see the <span class="date-1830">1830</span> version? 
                            It is possible that the gift did spur the family&#x02BC;s travel plans, but earlier than <span class="persName-JR">Ruskin</span> remembered, 
                            since the first mention of Continental tour occurs in the letters the family letters for <span class="date-1831">1831</span>. 
                            His <span class="date-1831-02-08">1831 birthday</span> predates that first mention by only a month. 
                            An alternative, surprising possibility is that <span class="persName-JR">Ruskin</span> owned the illustrated edition only after 
                            the family had returned from the <span class="geogName-CONTINENT">Continent</span>. According to <span class="persName-JJR">John James</span>&#x02BC;s accounts, 
                            he purchased a copy of <i>Italy</i> 
                            in <span class="date-1833-10">October 1833</span>, just shortly after the <span class="date-1833-09">September</span> homecoming 
                            (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 286 n. 1). 
                            Could this explain why <span class="persName-HT">Telford</span>&#x02BC;s dedication is in <span class="persName-JJR">John James</span>&#x02BC;s hand? 
                            This <span class="date-1833-10">October 1833</span> purchase could well have been an additional copy of the <span class="date-1830">1830</span> version 
                            that was already well loved by the family, 
                            and it seems unlikely that the Ruskins would have acquired this popular book so belatedly. Nonetheless, 
                            one cannot discount the possibility that <span class="persName-SR">Rogers</span>&#x02BC;s tutelage of the Ruskins&#x02BC; 
                            viewing of the <span class="placeName-ALPS">Alps</span> and <span class="placeName-ITALY">Italy</span> was, during the tour itself, solely poetic 
                            and based on earlier versions of the poem, 
                            whereas the ekphrastic influence of <span class="persName-JMWT">Turner</span> and <span class="persName-TSP">Stothard</span> 
                            enhanced <span class="persName-JR">Ruskin</span>&#x02BC;s perception at almost precisely the time when he developed an illustrated version of his own.
               </p>
               
            </div>
            
            <div id="DATEOFPUBLICATION" class="subsection">
               <span class="head-subsection">Date of Publication</span>
               
               <p><span class="persName-WGC">W. G. Collingwood</span> attributed the first published version of the &#x201C;Account,&#x201D; 
                            <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, to <span class="date-1835">1835</span>, 
                            based on its appearance in <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a>. 
                            Forgetting that <a href="../notes/annuals_note.php" target="_blank">annuals</a> 
                            were always dated on their title pages for the year following the actual date of publication, 
                            because they were produced for gift&#x2010;giving during the holiday season of Christmas and New Year&#x02BC;s, 
                            <span class="persName-WGC">Collingwood</span> was additionally befuddled by his assumption that another poem by <span class="persName-JR">Ruskin</span> 
                            in the same volume of <i>Friendship&#x02BC;s Offering</i>, 
                            <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a>, could have dated only from the Ruskin family&#x02BC;s visit 
                            to <span class="placeName-">Salzburg</span> during their Continental 
                            <a href="../notes/tour_of_1835_note.php" target="_blank" class="inactive">Tour of 1835</a>. 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s poem was based, however, not on first&#x2010;hand experience 
                            but on an engraved vedute commissioned for the &#x201C;<span class="date-1835">1835</span>&#x201D; volume of <i>Friendship&#x02BC;s Offering</i> 
                            that was released for sale in <span class="date-1834-11">November 1834</span>. See 
                            <a href="../bibiliography/bibliography.php#WGCLIFEOFJR1893" target="_blank" class="inactive">Collingwood, <i>Life and Work of John Ruskin</i></a>, 1:59&#x2013;60; 
                            <a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> (4<sup>o</sup>, 1891)</a>, 1: 280&#x2013;83 n. 28, n. 30, and n. 32; 
                            <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> (8<sup>o</sup>, 1891)</a>, 1: 282 n. 28, 283&#x2013;85 n. 30 and n. 32; 
                            and see <a href="#PUBLICATION">Publication</a> for dates and discussion of subsequent publications of the &#x201C;Account.&#x201D;
               </p>
               
            </div>
            
