                                <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> also quoted some variant text from draft witnesses, 
                                commenting on their differences from witnesses in the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, 
                                whereas <span class="persName-WGC">Collingwood</span> restricted himself mainly to supplying the MS IX fair copies of <a href="../showcase/andernacht_poem_msix.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_msix.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a> as variants of the versions published as 
                                <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>. 
                                Even the Library Edition&#x02BC;s additional variant material from draft was often cued by <span class="persName-WGC">Collingwood</span> having first pointed out differences in his editorial notes.
                  </p>
                  
                  
