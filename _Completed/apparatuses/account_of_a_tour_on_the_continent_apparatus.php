            <div id="LINENUMBERING" class="subsection">
               <span class="head-subsection">Line Numbering in the Draft (MS IA, MS VIII)</span>
               
               <p>Additional information that possibly can be used to draw inferences about <span class="persName-JR">Ruskin</span>&#x02BC;s intended sequence 
                            of sections of the &#x201C;Account&#x201D; or his sequence of composition is the line numbering, which runs throughout verse portions of the work in 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, and the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence</a>. 
                            The numbering is listed in <a href="APPARATUSTABLE1">Table 1</a>.
               </p>
               
               <p>In the two sheets comprising <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                            the lines of the five poems are numbered continuously, 1&#x2013;173, without breaks between the separate poems. 
                            In <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                            the line numbering begins following the prose section for <a href="../showcase/calais_prose_msia_g2.php">&#x201C;Calais&#x201D;</a>, 
                            starting with the first line of <a href="../showcase/passing_the_alps_poem_msia_g2.php">&#x201C;Passing the Alps&#x201D;</a> as line no. 360, 
                            and running continuously from poem to poem, to line no. 429. Since the poems in this manuscript could never have been conceived as sequentially ordered 
                            in a topographical sense for fair copy, this instance shows that the line count was valuable to <span class="persName-JR">Ruskin</span> in some way, but not necessarily in a way that 
                            can be lined up directly against the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>.
               </p>
               
               <p>In the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence</a>, 
                            the line numbering takes up with line no. 506, applied to the mysterious poem, 
                            <a href="../showcase/oh_are_there_spirits_can_there_be_poem_msviii.php">&#x201C;Oh are there spirits, can there be&#x201D;</a>; 
                            and the numbering carries relentlessly and mechanically forward through the verse draft in 
                            <a href="../apparatuses/ms_viii_apparatus.php#CONTENTS" target="_blank" class="inactive">MS VIII: Contents, Section <i>b</i></a>. 
                            As in <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                            the line numbering in the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence</a> 
                            bears no relation to the topographical sequence of sections, since these are jumbled. 
                            Here, <span class="persName-JR">Ruskin</span> is mechanically numbering lines of poetry, for whatever use he made of the information.
               </p>
               
               <p>Thus, <span class="persName-JR">Ruskin</span>&#x02BC;s purpose in the numbering the lines of poetry is not obvious. 
                            Doubtless, he needed a count in order to estimate space needed to fair&#x2010;copy lines; however, the system would not have helped him estimate space for prose, 
                            and the presence of the numbering in <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a> 
                            suggests that he initiated the numbering scheme before he even conceived the plan for the fair copy. 
                            Whatever its function, the mechanical quality of the numbering, along with the lack of any appearance of its having been added at a later time, 
                            tends to reinforce the evidence that <span class="persName-JR">Ruskin</span> composed draft of the &#x201C;Account&#x201D; in a straightforward sequence, 
                            as suggested by the regular, uncrowded appearance of the manuscripts&#x2014;despite the helter&#x2010;skelter leaping around the map in topographical subjects.
               </p>
               
               <p>In the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence</a>, 
                            there may exist minor, localized confusions in sequence of composition, 
                            which are concealed by the apparently straightforward line numbering. For example, <span class="persName-JR">Ruskin</span> 
                            started the prose section for <span class="placeName-BRUSSELS">Brussels</span> on 62r, 
                            three&#x2010;quarters of the way down the page, and entitled it 
                            <a href="../showcase/part_of_brussels_prose1_msviii.php">&#x201C;Part of Brussels&#x201D; &#x005B;part 1&#x005D;</a>. 
                            This draft extends halfway through 62v, where it stops in mid&#x2010;sentence (&#x201C;. . . circumnavigating,&#x201D; 
                            substituted for <span class="del">traversing</span>), followed by a poem, 
                            <a href="../showcase/oh_softly_blew_the_mounting_breeze_msviii.php" target="_blank" class="inactive">&#x201C;Oh softly blew the mounting breeze&#x201D;</a> 
                            (entitled <a href="../showcase/chiavenna_poem_1891.php" target="_blank" class="inactive">&#x201C;Chiavenna&#x201D;</a> in <i>Poems</i> (<span class="date-1891">1891</span>) 
                            and in the <a href="../showcase/chiavenna_poem_le.php" target="_blank" class="inactive">Library Edition version</a>). 
                            <span class="persName-JR">Ruskin</span> numbered the poem&#x02BC;s lines 810&#x2013;22, 
                            which is continuous with a previous installment of verse (lines 778&#x2013;809), for a quite different poem, 
                            <a href="../showcase/oh_the_morn_looked_bright_over_hill_and_dale_msviii.php" target="_blank" class="inactive">&#x201C;Oh the morn looked bright over hill and dale&#x201D; &#x005B;&#x201C;The Black Forest&#x201D;&#x005D;&#x201D;</a>). 
                            The remainder of the prose passage on <span class="placeName-BRUSSELS">Brussels</span>, 
                            <a href="../showcase/part_of_brussels_prose2_msviii.php">&#x201C;Part of Brussels&#x201D; &#x005B;part 2&#x005D;</a> 
                            (taken up in mid&#x2010;line, &#x201C;perambulating . . . tread on it softly&#x201D;) begins at the top of the next recto, 63r, 
                            and it is completed on 63v.
               </p>
               
               <p>One inference is that <span class="persName-JR">Ruskin</span> wrapped the prose around the poem, 
                            already drafted in its place, on 62v. It is equally possible, however, that the sequence of composition was what it appears to be, 
                            but that the abrupt shifts between prose and poetry provide a clue about the process of fair&#x2010;copying 
                            (provided that <span class="persName-JR">Ruskin</span> had begun work in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a> by this point). 
                            In the fair copy of this prose passage for <a href="../showcase/brussels_prose_msix.php">&#x201C;Brussels&#x201D;</a>, 
                            a line of text ends with the break&#x2010;point in the draft, &#x201C;circumnavigating for&#x201D;&#x2014;with 
                            the &#x201C;for&#x201D; jammed against the justified right margin. The next line resumes with &#x201C;perambulating.&#x2014;Oh woe to the weather&#x201D; etc. 
                            One might infer, then, that <span class="persName-JR">Ruskin</span> fair&#x2010;copied 
                            <a href="../showcase/part_of_brussels_prose1_msviii.php">&#x201C;Part of Brussels&#x201D; &#x005B;part 1&#x005D;</a> in 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> as far as he had drafted the prose in MS VIII (62v); 
                            crossed out the last word in MS VIII draft on 62v (<span class="del">traversing</span>), since he could not fit it on the line in the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> 
                            (hence, the tightly fitted, almost run&#x2010;together &#x201C;circumnavigating, for&#x201D;); 
                            then stopped fair&#x2010;copying the prose in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a>, 
                            in order to draft the poem that falls next in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>, 
                            <a href="../showcase/oh_softly_blew_the_mounting_breeze_msviii.php" target="_blank" class="inactive">&#x201C;Oh softly blew the mounting breeze&#x201D;</a> 
                            (and numbered its lines accurately in sequence); and finally resumed drafting prose with 
                            <a href="../showcase/part_of_brussels_prose2_msviii.php">&#x201C;Part of Brussels&#x201D; &#x005B;part 2&#x005D;</a>, 
                            but changed his mind about the next word (making it &#x201C;perambulating,&#x201D; rather than &#x201C;traversing&#x201D;).
               </p>
               
               <p>In this case, either scenario is supported by the evidence of the line numbering and the appearance of the draft. 
                            One conclusion that might be drawn from both scenarios is that fair&#x2010;copying was as important to 
                            <span class="persName-JR">Ruskin</span> as composition. Another inference is the unsettledness of his imagination, 
                            which could danced so mercurially from prose to verse, from the memory of <span class="placeName-BRUSSELS">Brussels</span> 
                            to evoking the <span class="placeName-BLACKFOREST">Black Forest</span>.
               </p>
               
               <p>A hazard of interpreting <span class="persName-JR">Ruskin</span>&#x02BC;s line numbering in 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a> is that it can lead to ambiguity about what verse 
                            he intended for the &#x201C;Account&#x201D; project. Following the <span class="date-1833-05">May 1833</span> birthday ode for his father, 
                            <a href="../apparatuses/my_fathers_birthday_the_month_of_may_apparatus.php" target="_blank" class="inactive">&#x201C;My Father&#x02BC;s Birthday: The Month of May&#x201D;</a>, 
                            <span class="persName-JR">Ruskin</span> first applied the numbering (line no. 506) to 
                            <a href="../showcase/oh_are_there_spirits_can_there_be_poem_msviii.php">&#x201C;Oh are there spirits, can there be&#x201D;</a>, 
                            a poem that he did not fair&#x2010;copy in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a>, 
                            and that <span class="persName-WGC">Collingwood</span> as well as 
                            <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> hesitated to identify as part of the &#x201C;Account.&#x201D; 
                            <span class="persName-JR">Ruskin</span> separated the poem from the preceding birthday ode by a horizontal line&#x2014;a 
                            designation used throughout <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> to divide one work from another&#x2014;but 
                            he supplied no title for the new piece in particular or for the &#x201C;Account&#x201D; in general, 
                            which would have helpfully signaled where the draft for the topographical work begins. 
                            The sole clue that the &#x201C;Account&#x201D; draft begins with this poem is its line numbering, 
                            which is not attached to the preceding <span class="date-1831">1831</span>&#x2013;<span class="date-1833">33</span> poetry in 
                            <a href="../apparatuses/ms_viii_apparatus.php#CONTENTS" target="_blank" class="inactive">MS VIII: Contents, Section <i>a</i></a>, 
                            but that does run throughout the &#x201C;Account&#x201D; draft. In the absence of evidence to the contrary, 
                            one must assume that <span class="persName-JR">Ruskin</span> intended 
                            <a href="../showcase/oh_are_there_spirits_can_there_be_poem_msviii.php">&#x201C;Oh are there spirits, can there be&#x201D;</a> 
                            for the &#x201C;Account,&#x201D; despite the difficulty of situating the poem in the fair copy. 
                            (And one must understand the poem&#x02BC;s reference to spiritual presences, rather than to visible topography, 
                            as a significant aspect of <span class="persName-JR">Ruskin</span>&#x02BC;s thinking about his first journey abroad&#x2014;particularly 
                            the mountain scenery that may form the context of this lyric, with <a href="../showcase/source_of_the_arveron_prose_msviii.php">&#x201C;Source of the Arveron&#x201D;</a> 
                            falling next in sequence, which likewise evokes spiritual presences.
               </p>
               
               <p>This discussion is provided as a demonstration of how <span class="persName-JR">Ruskin</span>&#x02BC;s line numbering might be used as evidence in establishing the contents and development of the &#x201C;Account.&#x201D; 
                            Use of this evidence always calls for caution, since the evidence is ambiguous in itself, and <span class="persName-JR">Ruskin</span> was sometimes inconsistent in how he applied the numbering. 
                            In <a href="../apparatuses/msviii_apparatus.php#LISTB2" target="_blank">MS VIII: Contents, <i>b</i>.2</a>, 
                            he extended the numbering system from draft poetry for the &#x201C;Account&#x201D; into verse that clearly does not belong to the project, 
                            such as the <a href="../apparatuses/the_address_apparatus.php" target="_blank" class="inactive">&#x201C;The Address&#x201D;</a> and other 
                            <span class="date-1834-05">May 1834</span> birthday poems for <span class="persName-JJR">John James</span>, as well as 
                            <a href="../apparatuses/the_crystal_hunter_apparatus.php" target="_blank" class="inactive">&#x201C;The Crystal Hunter&#x201D;</a> and other poems.
               </p>
               
            </div> 
            
            
