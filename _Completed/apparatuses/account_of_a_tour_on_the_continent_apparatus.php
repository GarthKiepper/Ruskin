            <div id="DRAFT" class="subsection">
               <span class="head-subsection">Draft</span>
               
               <p>The larger hypothesis advanced here concerning the composition of the &#x201C;Account&#x201D; is that <span class="persName-JR">Ruskin</span> 
                            developed the work in stages, from a verse travelogue in the eighteenth&#x2010;century topographical manner&#x2014;as 
                            represented by <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                            a collaboration with <span class="persName-JJR">John James Ruskin</span>&#x2014;to a composite&#x2010;genre, illustrated travelogue that reflected the material culture 
                            surrounding the ekphrastic and travel&#x2010;related publishing of the <span class="date-1830-1839">1830s</span>. 
                            In so doing, <span class="persName-JR">Ruskin</span> worked toward invention of the persona, &#x201C;J.R.,&#x201D; who signed the culminating publication of the project, <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, 
                            published in the fashionable <a href="../notes/annuals_note.php" target="_blank">literary annual</a>, 
                            <i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath</i>. 
                            With the project of the &#x201C;Account&#x201D; founded in a poetic collaboration with his father, 
                            and elaborated in response to his surrounding material culture of ekphrastic and topographical publication, 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s devising of his first literary persona represents the work of both the youth himself and his editors and literary mentors. 
                            Traces of editorial intervention can be encountered only sporadically in the manuscript record, however, 
                            and evidence about <span class="persName-JR">Ruskin</span>&#x02BC;s interaction with the editor, 
                            <a href="../notes/pringle_thomas_note.php" target="_blank">Thomas Pringle (1789&#x2013;1834)</a>, is indirect.
               </p>
               
               
               <div id="DRAFTMSIAG1" class="sub-subsection">
                  <span class="head-sub-subsection">MS IA, <i>g</i>.1</span>
                  
                  <p>For a physical description of <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                                which consists of 2 sheets, see <a href="../apparatuses/msia_apparatus.php#CONTENTS" target="_blank" class="inactive">MS IA: Contents</a>, <i>g</i>. 
                                The case for this manuscript as the earliest extant surviving draft of the &#x201C;Account&#x201D; is based on the fact that its markup for revision 
                                (e.g., deletions, substitutions, transposition of lines) is incorporated into the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, 
                                and on the assumption that this group of poems about the Ruskins&#x02BC; first leg of their Continental journey, 
                                from <span class="placeName-CALAIS">Calais</span> to <span class="placeName-BRUSSELS">Brussels</span> and along the <span class="geogName-MEUSE">Meuse River</span>, 
                                formed a logical compositional starting point for the work.
                  </p>
                  
                  <p>The recto of sheet 1 begins with the poem, <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a>, 
                                entitled as such, and in the hand of <span class="persName-JJR">John James Ruskin</span>. On the verso, <span class="persName-JJR">John James</span> continued with the poem, 
                                <a href="../showcase/cassel_poem_msia_g1.php">&#x201C;Cassel&#x201D;</a> 
                                (untitled, and therefore identifiable as a new poem following 
                                <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a> only by the two poems&#x02BC; separation in the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>). 
                                At line 19 of <a href="../showcase/cassel_poem_msia_g1.php">&#x201C;Cassel&#x201D;</a>, 
                                <span class="persName-JR">John</span>&#x02BC;s hand takes over (not <span class="persName-MR">Margaret</span>&#x02BC;s, whose terminal letters are typically formed differently than what prevails here). 
                                <span class="persName-JR">Ruskin</span> carries the poem onto sheet 2 of the manuscript, filling recto and verso with the continuation of 
                                <a href="../showcase/cassel_poem_msia_g1.php">&#x201C;Cassel&#x201D;</a>, 
                                <a href="../showcase/lille_poem_msia_g1.php">&#x201C;Lille&#x201D;</a>, 
                                <a href="../showcase/brussels_poem_msia_g1.php">&#x201C;Brussels&#x201D;</a>, and 
                                <a href="../showcase/the_meuse_poem_msia_g1.php">&#x201C;The Meuse&#x201D;</a>. 
                                Substitutions of revised lines, phrases, and words appear to be solely in <span class="persName-JR">John</span>&#x02BC;s hand, 
                                even those made to <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a> 
                                originally in <span class="persName-JJR">John James</span>&#x02BC;s hand. Stronger proof that 
                                <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a> and the first 18 lines of 
                                <a href="../showcase/cassel_poem_msia_g1.php">&#x201C;Cassel&#x201D;</a> 
                                represent <span class="persName-JJR">John James</span>&#x02BC;s original composition, and not merely his copy of <span class="persName-JR">John</span>&#x02BC;s original verse&#x2014;the latter 
                                assumed to be the case by <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> 
                                (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, e.g. 2: 341 n. 2)&#x2014;requires 
                                additional research. It is at least evident that the father and son collaborated to some significant degree in the production of this material witness, 
                                and that the text is characteristic more of the Augustan verse travelogues that prevailed in <span class="persName-JJR">John James</span>&#x02BC;s youth 
                                than of the Byronic or Dickensian manner of travel narration that marked <span class="persName-JR">John</span>&#x02BC;s first decade as an author 
                                (see <a href="../bibiliography/bibliography.php#HANSONRUSKIN1830S" target="_blank" class="inactive">Hanson, &#x201C;Ruskin in the 1830s&#x201D;</a>). 
                                In effect, this sequence of five poems stands vis&#x2010;à&#x2010;vis the composite&#x2010;genre 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> 
                                similarly to how the earlier published texts of 
                                <a href="../notes/rogers_samuel_note.php" target="_blank">Samuel Rogers (1763&#x2013;1855)</a>&#x02BC;s <i>Italy</i> 
                                stand in relation to that work&#x02BC;s repackaging in <span class="date-1830">1830</span> as an illustrated travelogue.
                  </p>
                  
                  <p>One might argue that <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                                does not witness a distinct version of the &#x201C;Account&#x201D; project, one conceived as travelogue solely in verse, 
                                but rather draft that <span class="persName-JR">Ruskin</span> intended to incorporate directly into the composite&#x2010;genre 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>. 
                                That hypothesis cannot be disproven; however, evidence weighs more heavily in favor of this manuscript as representing a distinct stage of the work, 
                                in that no other draft manuscript of the &#x201C;Account&#x201D; consists solely of verse, and in that this manuscript physically suggests 
                                what was originally meant as a self&#x2010;standing fair copy, which was later subjected to revision. 
                                In arrangement of lines on the manuscript, <span class="persName-JJR">John James</span>&#x02BC;s large, eighteenth&#x2010;century hand required the full width of sheet 1, 
                                and <span class="persName-JR">John</span> sustained his father&#x02BC;s column in narrower scope and a smaller hand; however, on the verso of sheet 2, 
                                <span class="persName-JR">John</span> stacked the lines in double columns, as if imitating a printed copy. 
                                (He inserted the final poem in the sequence, <a href="../showcase/the_meuse_poem_msia_g1.php">&#x201C;The Meuse&#x201D;</a>, 
                                sideways in space remaining on the recto of sheet 2, rather than starting a third sheet, 
                                further substantiating the sense of the manuscript as a self&#x2010;contained entity.) The few substitutions&#x2014;all 
                                apparently in <span class="persName-JR">John</span>&#x02BC;s hand, but not certainly so, especially in the case of overwriting&#x2014;are largely made as strikethroughs, 
                                with insertions above or below the line, and never as in&#x2010;line revisions, 
                                suggesting that these changes formed a distinct stage of this manuscript&#x02BC;s development, following its original purpose as a fair copy. 
                                Moreover, in the margins of both sheets, <span class="persName-JR">Ruskin</span> numbered the lines of the poems sequentially and continuously (1&#x2013;173); 
                                that is, he did not begin his line numbering anew with each poem, suggesting that he regarded the poems as forming a sequence 
                  </p>
                  
               </div>
               
               
               <div id="DRAFTMSIAG2" class="sub-subsection">
                  <span class="head-sub-subsection">MS IA, <i>g</i>.2</span>
                  
                  <p>For a physical description of <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                                see <a href="../apparatuses/msia_apparatus.php#CONTENTS" target="_blank" class="inactive">MS IA: Contents</a>, <i>g</i>. 
                                This manuscript differs in size and appearance from 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, and it contains a prose section, 
                                <a href="../showcase/calais_prose_msia.php" target="_blank" class="inactive">&#x201C;Calais&#x201D;</a>. 
                                Unlike <i>g</i>.1, this manuscript was rough draft from inception, with many in&#x2010;line revisions. 
                                Its place in the evolution of the &#x201C;Account&#x201D; appears to be a transitional one between the all&#x2010;verse and the composite&#x2010;genre versions, 
                                since it contains not only both prose and verse but also a selection of sections that seems strategic, although the exact purpose of the selection is obscure. 
                                The manuscript begins with draft of the first prose section of the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> 
                                (which incorporates the changes made to the draft), but the prose is followed by drafts of poems that, 
                                in topographical terms of the actual tour, skip far ahead of the family&#x02BC;s landing at <span class="placeName-CALAIS">Calais</span>, as if presenting highlights of the tour.
                  </p>
                  
                  <p>The destinations represented by the draft poems are widely spaced but not random. 
                                The manuscript&#x02BC;s four poems form two pairs, one pair consisting of two nearly adjacent Italian destinations, 
                                <a href="../showcase/passing_the_alps_poem_msia_g2.php">&#x201C;Passing the Alps&#x201D;</a> and 
                                <a href="../showcase/milan_cathedral_poem_msia_g2.php">&#x201C;Milan Cathedral&#x201D;</a>, 
                                and the second pair consisting of two adjacent destinations along the <span class="placeName-RHINE">Rhine</span>, 
                                <a href="../showcase/andernacht_poem_msia_g2.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_msia_g2.php">&#x201C;St. Goar&#x201D;</a>. While the pairs fall in this reversed topographical order 
                                (the family actually journeyed down the <span class="geogName-RHINE">Rhine</span> to the <span class="geogName-BLACKFOREST">Black Forest</span>, and then crossed the <span class="geogName-ALPS">Alps</span> and descended to the Italian lakes and <span class="placeName-MILAN">Milan</span>), 
                                the arrangement suggests an abstract of the scope of the composite&#x2010;genre version.
                  </p>
                  
                  <p><span class="persName-JR">Ruskin</span> did not fair&#x2010;copy <a href="../showcase/passing_the_alps_poem_msia_g2.php">&#x201C;Passing the Alps&#x201D;</a> and 
                                <a href="../showcase/milan_cathedral_poem_msia_g2.php">&#x201C;Milan Cathedral&#x201D;</a> 
                                in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a>, 
                                the fair&#x2010;copy &#x201C;Account&#x201D; halting after only a few lines of the <a href="../showcase/heidelberg_prose_msix.php" target="_blank" class="inactive">prose section of &#x201C;Heidelberg&#x201D;</a>. 
                                The <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a> 
                                pair of <span class="placeName-RHINE">Rhine</span> poems do appear in the fair copy, however, the 
                                trio of poetry/prose/illustration sections on &#x201C;Andernacht,&#x201D; &#x201C;Ehrenbreitstein,&#x201D; and &#x201C;St. Goar&#x201D; forming <span class="persName-JR">Ruskin</span>&#x02BC;s final accomplishment in MS IX, 
                                before he broke off in the middle of the section on &#x201C;Heidelberg&#x201D; (although even that trio of sections in MS IX shows gaps for illustrations, 
                                the drawings either having been removed or having never been produced at all). 
                                In <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a>, the draft prose sections for 
                                <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and for 
                                <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a> fall adjacent to one another, very near the beginning of the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence for the &#x201C;Account&#x201D;</a>. 
                                Directly following the draft prose sections, moreover, there occur a series of draft poems relating to crossing the <span class="geogName-ALPS">Alps</span> (i.e., 
                                <a href="../showcase/via_mala_poem_msviii.php">&#x201C;Via Mala&#x201D;</a>, 
                                <a href="../showcase/splugen_poem_msviii.php">&#x201C;Splugen&#x201D;</a>, 
                                <a href="../showcase/the_summit_poem_msviii.php">&#x201C;The Summit&#x201D;</a>, and 
                                <a href="../showcase/the_descent_poem_msviii.php">&#x201C;The Descent&#x201D;</a> 
                                (see <a href="APPARATUSTABLE1">Table 1</a>; and 
                                <a href="../apparatuses/msviii_apparatus.php#LISTB1" target="_blank">MS VIII: Contents, section <i>b</i>, list <i>b</i>.1</a>). 
                                Thus, the sequence of draft begun in MS VIII bears a near relation to the two pairs of poems drafted in 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>.
                  </p>
                  
                  <p>What that relation was exactly, and what was the relative chronology of composition in MS IA, <i>g</i>.2, and MS VIII are problems requiring further research. 
                                It appears that <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, reflects a turning point 
                                in <span class="persName-JR">Ruskin</span>&#x02BC;s reconsideration of the genre of the &#x201C;Account,&#x201D; 
                                in which he drafted the prose to accompany the opening poem, 
                                <a href="../showcase/calais_poem_msia_g1" target="_blank" class="inactive">&#x201C;Calais&#x201D;</a> of the earlier, all&#x2010;verse version, 
                                and in which he sketched out two paired poetic representations of later 
                                cruxes in the journey, beyond the first stage of the tour in northern <span class="placeName-FRANCE">France</span> and <span class="placeName-BELGIUM">Belgium</span>, 
                                as represented in the collaborative <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a> manuscript. 
                                The sequence of draft taken up in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a> 
                                hinges on that abstract in <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                                although the chronological relation between these two draft manuscripts and the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> may have been more complex, 
                                since <span class="persName-JR">Ruskin</span> appears to have already begun the latter by the time he started the draft in 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>.
                  </p>
                  
               </div>
               
               
               <div id="DRAFTMSVIII" class="sub-subsection">
                  <span class="head-sub-subsection">MS VIII</span>
                  
                  <p><a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> is a notebook that <span class="persName-JR">Ruskin</span> 
                                had used for rough copy of poetry and since <span class="date-1831">1831</span>, and that he would continue to use for drafting imaginative work 
                                through <span class="date-1836">1836</span>, and possibly as late as <span class="date-1838">1838</span>. 
                                The <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence for the &#x201C;Account&#x201D;</a> 
                                first appears following <span class="persName-JR">Ruskin</span>&#x02BC;s draft of his 
                                <span class="date-1833-05-10">10 May 1833</span> birthday ode for <span class="persName-JJR">John James Ruskin</span>, 
                                <a href="../apparatuses/my_fathers_birthday_the_month_of_may_apparatus.php" target="_blank" class="inactive">&#x201C;My Father&#x02BC;s Birthday: The Month of May&#x201D;</a>. 
                                This sequence is logical, given that the family departed on the <a href="../notes/tour_of_1833_note.php" target="_blank">Tour of 1833</a> 
                                to the Continent following <span class="persName-JJR">John James&#x02BC;s</span> birthday; however, <span class="persName-WGC">Collingwood</span> is certainly incorrect to suggest 
                                that &#x201C;the prose and verse description of . . . &#x005B;the <span class="date-1833">1833</span> tour&#x005D; was begun&#x201D; in this manuscript notebook 
                                (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1:264; and <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 
                                1:265). His statement is inadvertently not far from the mark,
                                in that the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII sequence of &#x201C;Account&#x201D; draft</a> 
                                does represent the most significant development of the composite&#x2010;genre version to survive in draft, but <span class="persName-WGC">Collingwood</span> was not aware that 
                                this draft was preceded by earlier manuscripts&#x2014;by <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                                representing an all&#x2010;verse version of the work, and by <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                                representing a transition to the composite&#x2010;genre version. As further analysis will show, by the time <span class="persName-JR">Ruskin</span> took up 
                                <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> to continue his draft of the composite&#x2010;genre &#x201C;Account,&#x201D; 
                                he had already drafted the earlier prose sections for the &#x201C;Calais,&#x201D; &#x201C;Cassel,&#x201D; and &#x201C;Lille&#x201D; sections, and transferred the draft to 
                                his illustrated <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>.
                  </p>
                  
                  <p>Since <span class="persName-JR">Ruskin</span> did not entitle the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">&#x201C;Account&#x201D; draft in MS VIII</a> (or anywhere else), 
                                it is not clear precisely at what point the intended draft begins. Following the birthday ode, 
                                <a href="../apparatuses/my_fathers_birthday_the_month_of_may_apparatus.php" target="_blank" class="inactive">&#x201C;My Father&#x02BC;s Birthday: The Month of May&#x201D;</a>, 
                                there occurs the verse, <a href="../showcase/oh_are_there_spirits_can_there_be_poem_msviii.php">&#x201C;Oh are there spirits, can there be&#x201D;</a>, 
                                which offers no clues in its subject matter to confirm whether <span class="persName-JR">Ruskin</span> meant to connect this poem with the topographical 
                                content of the &#x201C;Account.&#x201D; With the next piece in the notebook, however&#x2014;a prose draft that <span class="persName-JR">Ruskin</span> entitled 
                                <a href="../showcase/source_of_the_arveron_prose_msviii.php">&#x201C;Source of the Arveron&#x201D;</a>&#x2014;he clearly had begun work 
                                belonging to the &#x201C;Account&#x201D; (a title that appears in <span class="persName-JR">Ruskin</span>&#x02BC;s 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii_contents.xml" target="_blank" class="inactive">List of Proposed Additional Contents for Account of a Tour on the Continent</a>, 
                                although he never fair&#x2010;copied this prose section, which would have taken its place near the end of the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>). This portion of 
                                <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> devoted to the &#x201C;Account&#x201D; then remains unusually, for this notebook, 
                                homogeneous in its dedication to a single project 
                                (see <a href="../apparatuses/ms_viii_apparatus.php#LISTB1" target="_blank" class="inactive">MS VIII: Contents, Section <i>b</i>.1</a>), 
                                only becoming mixed with other works after approximately twenty&#x2010;five items of draft for the &#x201C;Account&#x201D;&#x2014;the 
                                draft at that point intermingling with poems related to <span class="persName-JJR">John James</span>&#x02BC;s upcoming <span class="date-1834-05-10">10 May 1834</span> 
                                birthday (see <a href="../apparatuses/ms_viii_apparatus.php#LISTB2" target="_blank" class="inactive">MS VIII: Contents, Section <i>b</i>.2</a>).
                  </p>
                  
                  <p>By commencing the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">&#x201C;Account&#x201D; draft in MS VIII</a> 
                                with <a href="../showcase/source_of_the_arveron_prose_msviii.php">&#x201C;Source of the Arveron&#x201D;</a>, 
                                <span class="persName-JR">Ruskin</span> hearkens nearly to the end of the 
                                <a href="../notes/tour_of_1833" target="_blank" class="inactive">Tour of 1833</a>, when the family visited 
                                <span class="placeName-CHAMOUNI">Chamouni</span>, although shortly following this piece <span class="persName-JR">Ruskin</span> drafts the prose sections on 
                                <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a> that 
                                dovetail with the corresponding <span class="geogName-RHINE">Rhine</span> poems in <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>. 
                                The occurrence here, so far out of topographical sequence, of <span class="persName-JR">Ruskin</span>&#x02BC;s reverie piece on the &#x201C;Source of the Arveron&#x201D; can be linked to his correspondence with the writer, 
                                <a href="../notes/hogg_james_note.php" target="_blank">James Hogg (ca. 1770&#x2013;1835)</a>, which is dated <span class="date-1834-02-13">13 February 1834</span>; 
                                and following &#x201C;Source of the Arveron,&#x201D; the series of poems about crossing the <span class="geogName-ALPS">Alps</span> 
                                can be connected not only with the pair of poems on that subject in 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a> but also with his father&#x02BC;s gift 
                                on <span class="persName-JR">Ruskin</span>&#x02BC;s <span class="date-1834-02-08">8 February 1834</span> birthday of books about the <span class="geogName-ALPS">Alps</span>&#x2014;<i>Voyages dans les Alpes</i> (<span class="date-1779-1796">1779&#x2013;96</span>), by 
                                <span class="persName-HBS">Horace&#x2010;B&#x00E9;n&#x00E9;dict de Saussure</span>, and 
                                <i>Illustrations of the Passes of the Alps</i> (<span class="date-1828">1828</span>) by 
                                <span class="persName-WBI">William Brockedon</span> 
                                (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 278, 280 n. 1). 
                                This convergence of sources dates the commencement of the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII &#x201C;Account&#x201D; draft</a> around 
                                <span class="date-1834-02-13-1834-02-28">the middle to late February 1834</span>.
                  </p>
                  
                  <p>A February date for <span class="persName-JR">Ruskin</span>&#x02BC;s starting the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">&#x201C;Account&#x201D; draft in MS VIII</a> 
                                accords well with the trajectory of this writing toward a terminus ad quem among draft of poetry celebrating his father&#x02BC;s 
                                <span class="date-1834-05-10">10 May 1834</span> birthday; and this date of commencement also seems well situated to account for the progress he was likely to have made on the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>. 
                                Although the prose sections on <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a> occur near the beginning of the MS VIII draft sequence, 
                                <span class="persName-JR">Ruskin</span> cannot at that time have fair&#x2010;copied so far as the <span class="geogName-RHINE">Rhine</span> portion of the work (the &#x201C;Andernacht&#x201D; section falling 
                                approximately two&#x2010;thirds of the way into the work as fair&#x2010;copied), since he had yet to compose at least two prose accounts belonging 
                                to the Belgian portion, as well as poetry and prose belonging to the upper <span class="geogName-RHINE">Rhine</span>. 
                                In the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII &#x201C;Account&#x201D; draft</a>, 
                                the prose sections describing <span class="placeName-BRUSSELS">Brussels</span> (entitled 
                                <a href="../showcase/part_of_brussels_prose1_msviii.php">&#x201C;Part of Brussels&#x201D; &#x005B;part 1&#x005D;</a> and 
                                <a href="../showcase/part_of_brussels_prose2_msviii.php">&#x201C;Part of Brussels&#x201D; &#x005B;part 2&#x005D;</a>) and 
                                <a href="../showcase/the_meuse_prose_msviii.php">&#x201C;The Meuse&#x201D;</a> come after the prose sections on 
                                <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a>; and mixed among the poetry and prose describing 
                                Alpine and Italian destinations (never fair&#x2010;copied) that make up much of this draft, there is also writing on 
                                <a href="../showcase/aix_la_chapelle_prose_msviii.php">&#x201C;Aix la Chapelle&#x201D;</a> 
                     <a href="../showcase/cologne_prose_msviii.php">&#x201C;Cologne&#x201D;</a>. 
                                The <span class="placeName-BRUSSELS">Brussels</span> and upper <span class="geogName-RHINE">Rhine</span> prose pieces fall roughly one&#x2010;fourth to one&#x2010;third of the way into the fair copy. 
                                The logical inference is that, by <span class="date-1834-02-13-1834-02-28">the middle to late February 1834</span>, 
                                <span class="persName-JR">Ruskin</span> had transferred poetry, prose, and illustration to the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> no farther than the section on &#x201C;Brussels,&#x201D; 
                                although he may by that time have completed about one&#x2010;third of the fair copy, including the sections on &#x201C;Calais,&#x201D; &#x201C;Cassel,&#x201D; and &#x201C;Lille,&#x201D; 
                                for which the prose sections are not extant in MS VIII. This inference would also place 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, with its prose section on 
                                <a href="../showcase/calais_prose_msia.php" target="_blank" class="inactive">&#x201C;Calais&#x201D;</a>, no later than <span class="date-1834-01">January 1834</span>, if not earlier. 
                                (These conclusions assume that <span class="persName-JR">Ruskin</span> worked sequentially through the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>.)
                  </p>
                  
                  <p>The <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft sequence for the &#x201C;Account&#x201D;</a> 
