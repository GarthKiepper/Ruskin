               <div id="PUBLICATION1834" class="sub-subsection">
                  <span class="head-sub-subsection">1834</span>
                  
                  <p><a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a> 
                                contains two verse publications signed &#x201C;J.R.&#x201D; In one, <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> 
                                (pp. 317&#x2013;19), <span class="persName-JR">Ruskin</span> revised poems taken directly from the &#x201C;Account&#x201D;; in the second, <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a> 
                                (pp. 37&#x2013;38), he drew on his practice in topographical and ekphrastic verse that he gained in writing the &#x201C;Account,&#x201D; 
                                and on his observance of the materiality of illustrated travel publications of the <span class="date-1830-1839">1830s</span> that he imitated in the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>. <span class="persName-JR">Ruskin</span> composed 
                                <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a> as an ekphrastic accompaniment to an engraved vedute of the city, 
                                based on a drawing by <a href="../notes/purser_william_note.php" target="_blank">William Purser (ca. 1790&#x2013;1852)</a>, 
                                which was printed facing the first page of <span class="persName-JR">Ruskin</span>&#x02BC;s poem.
                  </p>
                  
                  <p>As an <a href="../notes/annuals_note.php" target="_blank">annual</a>, 
                                <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a> 
                                would have been published in <span class="date-1834-11">November 1834</span>, in time for the holiday season of 
                                <span class="date-1834-12-25-1835-01-01">Christmas and New Year&#x02BC;s, 1834&#x2013;35</span>. 
                                <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a> 
                                was published by <span class="orgName-SMITHELDER">Smith, Elder</span>, and in this year it was edited by 
                                <a href="../notes/pringle_thomas_note.php" target="_blank">Thomas Pringle (1789&#x2013;1834)</a>. 
                                Both the firm&#x02BC;s proprietors and the editor were Scots, like the Ruskins, and known to them personally. 
                                While some correspondence by <span class="persName-TP">Pringle</span> exists to attest to his personal acquaintance with the Ruskins 
                                (<a href="../bibiliography/bibliography.php#VIGNEPRINGLE" target="_blank" class="inactive">Vigne, <i>Thomas Pringle</i></a>, 188), 
                                no correspondence by the editor or by the Ruskin family is known to comment on <span class="persName-JR">John</span>&#x02BC;s writing for <i>Friendship&#x02BC;s Offering</i>. 
                                As remarked in <a href="#DRAFTMSVIII">Draft: MS VIII</a>, a draft of 
                                <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a> is extant; it terminates the section, 
                                <a href="../apparatuses/msviii_apparatus.php#LISTB2" target="_blank">MS VIII: Contents, <i>b</i>.2</a>, in which draft of the &#x201C;Account&#x201D; 
                                combines with draft of poetry for <span class="persName-JJR">John James</span>&#x02BC;s <span class="date-1834-03-10">10 May 1834</span> birthday. No known draft reflects the revision of poetry in the &#x201C;Account&#x201D; to construct 
                                <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, which was formed from the poems, 
                                <a href="../showcase/andernacht_poem_fo.php" target="_blank" class="inactive">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_fo.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a>. The texts were revised from their form in 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2, draft</a> 
                                (i.e., <a href="../showcase/andernacht_poem_msia_g2.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_msia_g2.php">&#x201C;St. Goar&#x201D;</a>) and 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS XI fair copy</a> 
                                (i.e., <a href="../showcase/andernacht_poem_msix.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_msix.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a>). 
                                (see <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:353&#x2013;54 n. 2).
                  </p>
                  
                  <p><span class="persName-WGC">W. G. Collingwood</span> misdated the publication of <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a> 
                                as a year later, the end of <span class="date-1835">1835</span>, rather than the end of <span class="date-1834">1834</span>, and interpreted its draft in MS VIII as placed achronologically in the notebook, 
                                showing that <span class="persName-JR">Ruskin</span> had &#x201C;abandoned&#x201D; the &#x201C;Account&#x201D; draft and taken up &#x201C;poems descriptive of new Tour&#x2014;that of <span class="date-1835">1835</span>,&#x201D; when the family first visited Salzburg. 
                                In fact, the draft is placed sequentially and chronologically, immediately following the abandoned draft of the &#x201C;Account,&#x201D; 
                                when <span class="persName-JR">Ruskin</span> took up his commission to write a poem based on an engraved image of the city, not on a visit to the actual place. 
                                A logical timeline can also be worked out for the revision of <a href="../showcase/andernacht_poem_msix.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_msix.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a> for 
                                <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>. 
                                As mentioned in <a href="#DRAFTMSVIII">Draft: MS VIII</a> and in 
                                <a href="#FAIRCOPY">Fair Copy</a>, in <span class="date-1834-03-01-1834-04-30">March or April 1834</span>, 
                                when <span class="persName-JR">Ruskin</span> was two&#x2010;thirds of his way into the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS IX fair copy</a> 
                                of the &#x201C;Account,&#x201D; he compiled the trio of sections, &#x201C;Andernacht,&#x201D; &#x201C;Ehrenbreitstein,&#x201D; and &#x201C;St. Goar.&#x201D; 
                                Since this fair copy brought together the draft of prose sections from MS VIII, 
                                <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a>, with drafts of their corresponding poems from 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, g.2</a>, 
                                <span class="persName-JR">Ruskin</span>&#x02BC;s reliance on the original versions of these poems for fair&#x2010;copying suggests that he had no notion at this time of undertaking their revision 
                                for professional publication. Since that segment of fair&#x2010;copying took place approximately in 
                                <span class="date-1834-03-01-1834-04-30">March or April 1834</span>, 
                                <span class="persName-JR">Ruskin</span> may have received <span class="persName-TP">Pringle</span>&#x02BC;s commission to revise the poems for <i>Friendship&#x02BC;s Offering</i> 
                                in April or May, and set about the work of revisions (as well as the composition of 
                                <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a>) during June or July&#x2014;a 
                                timetable that accords well with <span class="persName-TP">Pringle</span>&#x02BC;s due date, <span class="date-1834-08-01">1 August 1834</span>, 
                                for delivering the completed volume of <i>Friendship&#x02BC;s Offering</i> to the printer 
                                (see <a href="../bibiliography/bibliography.php#VIGNEPRINGLE" target="_blank" class="inactive">Vigne, <i>Thomas Pringle</i></a>, 244). 
                                (<span class="persName-TP">Pringle</span> himself died on <span class="date-1834-12-05">5 December 1834</span>; see <a href="../notes/pringle_thomas_note.php" target="_blank">Thomas Pringle (1789&#x2013;1834)</a> 
                                and <a href="../bibiliography/bibliography.php#VIGNEPRINGLE" target="_blank" class="inactive">Vigne, <i>Thomas Pringle</i></a>, 247).
                  </p>
                  
                  <p>This timetable also accords well with a <span class="date-1834-03-10">10 March 1834</span> verse epistle by <span class="persName-JR">Ruskin</span> to his father, 
                                <a href="../showcase/but_this_day_week_msxi.php" target="_blank" class="inactive">&#x201C;But this day week&#x201D;</a>, 
                                which is remarkable for <i>not</i> mentioning the <i>Friendship&#x02BC;s Offering</i> commissions. 
                                In this rhymed letter, <span class="persName-JR">Ruskin</span> drops the idea of sending some observations on natural history to the editor, 
                                <a href="../notes/loudon_j_c_note.php" target="_blank" class="inactive">John Claudius Loudon (1783&#x2013;1843)</a>, 
                                to be considered for publication in the <i>Magazine of Natural History</i>, so it seems surprising that 
                                he would have failed to add a couplet or two celebrating his submissions to another editor, <span class="persName-TP">Thomas Pringle</span> 
                                (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 285). 
                                It seems more likely that, at this time, no arrangements had as yet been made with <span class="persName-TP">Pringle</span>&#x2014;or that, 
                                if <span class="persName-JJR">John James</span> had approached the editor, <span class="persName-JR">John</span> as yet knew nothing about an impending professional debut as a poet. As recently as <span class="date-1834-01-22">22 January 1834</span>, 
                                <span class="persName-JJR">John James</span> had written to <span class="persName-JH">James Hogg</span> for advice about cultivating <span class="persName-JR">John</span>&#x02BC;s poetic talents (pointing to the &#x201C;Account&#x201D; as an example), but 
                                expressing misgivings about exposing his son to the public as a prodigy. This letter&#x2014;in which <span class="persName-JJR">John James</span> renewed contact with <span class="persName-JH">Hogg</span> abruptly, and following a long hiatus 
                                in the family&#x02BC;s slight relationship with the famed Scottish writer&#x2014;suggests that <span class="persName-JJR">John James</span> was already contemplating publication of <span class="persName-JR">John</span>&#x02BC;s poetry, 
                                but the letter also reveals the caution with which <span class="persName-JR">Ruskin</span>&#x02BC;s parents treated such a momentous possibility. 
                                Notably, the letter mentions no specific plans having been hatched with <span class="persName-TP">Pringle</span>, who was a longtime friend of <span class="persName-JH">Hogg</span>&#x02BC;s 
                                (<a href="../bibiliography/bibliography.php#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 273&#x2013;75; 
                                see also <a href="../notes/hogg_james_note.php" target="_blank">James Hogg &#x005B;1770&#x2013;1835&#x005D;</a>, and 
                                <a href="#DISCUSSION">Discussion</a>).
                  </p>
                  
                  <p><span class="persName-JH">Hogg</span> was sent a sampling of <span class="persName-JR">Ruskin</span>&#x02BC;s verse (possibly a selection from the &#x201C;Account&#x201D;), 
                                to which he responded with a critique (seconded by his children&#x02BC;s tutor) that does not survive, 
                                but that <span class="persName-JR">John</span> characterized as treating him with lenience while laying stress on the derivativeness of his verse. 
                                When <span class="persName-JR">John</span> replied gratefully to <span class="persName-JH">Hogg</span> on <span class="date-1834-02-13">13 February 1834</span>, 
                                he had received an invitation to visit the <span class="persName-JH">Ettrick Shepherd</span> on his farm in Scotland (which <span class="persName-JR">John</span> declined), 
                                but he apparently had received no direct encouragement to publish 
                                (<a href="../bibiliography/bibliography.php#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 276&#x2013;77). 
                                One can only speculate whether <span class="persName-JH">Hogg</span> had a hand subsequently in prompting his friends&#x2014;the 
                                editor, <span class="persName-TP">Thomas Pringle</span>, and the publisher, <span class="persName-AE">Alexander Elder</span>&#x2014;to consider <span class="persName-JR">Ruskin</span> as a contributor to <i>Friendship&#x02BC;s Offering</i>, 
                                a publication that had benefited by <span class="persName-JH">Hogg</span>&#x02BC;s own contributions. (The prompting may have worked in the opposite direction, with <span class="persName-TP">Pringle</span> encouraging <span class="persName-JJR">John James</span> 
                                to apply to <span class="persName-JH">Hogg</span> for advice.) One can only speculate, further, whether <span class="persName-TP">Pringle</span>&#x02BC;s introduction of the prodigy to <span class="persName-SR">Samuel Rogers</span>&#x2014;which 
                                may have occurred at this time, but concerning which no contemporary evidence is known&#x2014;had any bearing on <span class="persName-JR">Ruskin</span>&#x02BC;s public appearance as &#x201C;J.R.&#x201D; 
                                Respecting the parallel case about which we do have evidence&#x2014;<span class="persName-JR">John</span>&#x02BC;s proposal to submit a nonpoetic contribution to <span class="persName-JCL">J. C. Loudon</span>&#x02BC;s 
                                <i>Magazine of Natural History</i>&#x2014;two important facts emerge: that <span class="persName-JR">Ruskin</span> considered <span class="persName-JCL">Loudon</span>, another Scot, to be a &#x201C;friend,&#x201D; 
                                and that the boy had the confidence to propose the submission himself 
                                (<a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 285; see also 
                                <a href="../notes/rogers_samuel_note.php" target="_blank">Samuel Rogers (1763&#x2013;1855)</a>, and 
                                <a href="#DISCUSSION">Discussion</a>).
                  </p>
                  
               </div>
               
               <div id="PUBLICATION1850" class="sub-subsection">
                  <span class="head-sub-subsection">1850</span>
                  
                  <p>In <span class="date-1850">1850</span>, another verse selection from the &#x201C;Account&#x201D; was published as <a href="../showcase/ehrenbreitstein_poem_1850.php" target="_blank" class="inactive">&#x201C;Ehrenbreitstein: Fragment from a Metrical Journal (Aetat 16)&#x201D;</a> 
                                in <a href="../bibiliography/bibliography.php#POEMS1850" target="_blank" class="inactive">Ruskin, <i>Poems</i> (1850)</a> (pp. 8&#x2013;12), 
                                the privately published collection of <span class="persName-JR">Ruskin</span>&#x02BC;s poetry compiled by <span class="persName-JJR">John James Ruskin</span> and 
                                <a href="../notes/harrison_w_h_note.php" target="_blank" class="inactive">W. H. Harrison (ca. 1792&#x2013;1878)</a> (see 
                                <a href="../notes/poems_1850_note.php" target="_blank" class="inactive">Ruskin, <i>Poems</i> &#x005B;1850&#x005D;</a>). 
                                The editors&#x02BC; title consciously references the <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> 
                                published sixteen years earlier, and it is the only poem from the &#x201C;Account&#x201D; project to be included in the <span class="date-1850">1850</span> collection.
                  </p>
                  
                  <p>Perhaps the choice was an acknowledgment that, in the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS IX fair copy</a> of the &#x201C;Account,&#x201D; 
                                &#x201C;Ehrenbreitstein&#x201D; forms a thematic bridge between &#x201C;Andernacht&#x201D; and &#x201C;St. Goar,&#x201D; a connection that 
                                was broken when only the latter two poems were selected to be published as 
                                <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> 
                                (see <a href="#DISCUSSION">Discussion</a>). 
                                The poem&#x02BC;s omission from &#x201C;Fragments&#x201D; may have been memorable as a representative exercise in economy, 
                                paring down the huge &#x201C;Account&#x201D; project to its sparest unit&#x2014;a thematically contrasting pair of poems, 
                                the gothic <a href="../showcase/andernacht_poem_fo.php" target="_blank" class="inactive">&#x201C;Andernacht&#x201D;</a> and the domestic 
                                <a href="../showcase/st_goar_poem_fo.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a>&#x2014;without even a bridge poem to connect them. 
                                (<a href="../showcase/ehrenbreitstein_poem_msix.php" target="_blank" class="inactive">&#x201C;Ehrenbreitstein&#x201D;</a> takes as its topic the theme of confluence, 
                                like the meeting of the <span class="geogName-RHINE">Rhine River</span> and <span class="geogName-MOSELLE">Moselle River</span>, overlooked by the fortress of <span class="placeName-EHRENBREITSTEIN">Ehrenbreitstein</span>.) 
                                In <span class="date-1834">1834</span>, the omission from &#x201C;Fragments&#x201D; of &#x201C;Ehrenbreitstein,&#x201D; along with so much else, may have been felt particularly keenly, 
                                since the trio, &#x201C;Andernacht,&#x201D; &#x201C;Ehrenbreitstein,&#x201D; and &#x201C;St. Goar,&#x201D; were the last topographical units that <span class="persName-JR">Ruskin</span> completed&#x2014;a 
                                kind of crowning monument, like the fortress itself, although not meant as a conclusion 
                                (see <a href="../apparatuses/msviii_apparatus.php#LISTB1" target="_blank">MS VIII: Contents, section <i>b</i>, list <i>b</i>.1</a>).
                  </p>
                  
               </div>
               
               <div id="PUBLICATION1891" class="sub-subsection">
                  <span class="head-sub-subsection">1891</span>
                  
                  <p>For the reader of <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a> 
                                or of <a href="../bibiliography/bibliography.php#POEMS1850" target="_blank" class="inactive">Ruskin, <i>Poems</i> (1850)</a>, the 
                                &#x201C;Metrical Journal,&#x201D; from which <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> 
                                allegedly was extracted, was a fiction. It was also in part a falsehood, since <span class="persName-JR">Ruskin</span>&#x02BC;s travel journal had not taken a solely metrical form since the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1, version</a>. 
                                The reversion to this putative form in the published titles perhaps may reflect a prejudice on the part of <span class="persName-JJR">John James Ruskin</span>. 
                                That prejudice remained influential as late as <span class="date-1891">1891</span>, when <span class="persName-WGC">W. G. Collingwood</span> 
                                published only verse selections in the first attempt to edit the &#x201C;Account&#x201D; as a comprehensive tour poem. 
                                (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1: 119&#x2013;63, 281&#x2013;83; and 
                                <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 
                                1:119&#x2013;63, 283&#x2013;85). While he was aware of the scope of <span class="persName-JR">Ruskin</span>&#x02BC;s composite&#x2010;genre version, 
                                and aware also of <span class="persName-JR">Ruskin</span>&#x02BC;s extensive work on the project dispersed among several manuscripts 
                                (he identified MS VII, MS VIII, MS IX, and MS XI, but not MS IA &#x005B;see <a href="#MAJORMS">Manuscripts</a>&#x005D;), 
                                <span class="persName-WGC">Collingwood</span> nonetheless edited the &#x201C;Account&#x201D; as if it were strictly the &#x201C;Metrical Journal&#x201D; of the &#x201C;Fragments.&#x201D; 
                                He was limited by the parameters of his edition, which in turn obeyed the special status that the 
                                <a href="../notes/poems_1850_note.php" target="_blank" class="inactive">Ruskin, <i>Poems</i> (1850)</a> had awarded to poetry among <span class="persName-JR">Ruskin</span>&#x02BC;s juvenilia. 
                                By the <span class="date-1890-1899">1890s</span>, moreover, the privately printed and distributed <span class="date-1850">1850</span> edition of the <i>Poems</i> had taken on a talismanic aura for rare book collectors, 
                                and <span class="persName-WGC">Collingwood</span> inevitably justified the purposes of the <span class="date-1891">1891</span> edition in part by appealing to the &#x201C;no idle curiosity . . .  that prompts the admirers 
                                of Mr. <span class="persName-JR">Ruskin</span>&#x02BC;s works to collect his boyish writings and to learn the story of his youth.&#x201D; It was in &#x201C;these poems,&#x201D; it was believed, 
                                &#x201C;although he thrust them aside for prose,&#x201D; that <span class="persName-JR">Ruskin</span> &#x201C;could better put the complicated feelings, thoughts, and facts which he had to tell,&#x201D; 
                                and which therefore constituted &#x201C;the best introduction to his later and greater books&#x201D; 
                                (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1:xix, xx; and <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 
                                1:v, vi; and see <a href="../notes/collecting_of_modern_authors_note.php" target="_blank">Collecting of Modern Authors in the Late Nineteenth and Early Twentieth Centuries</a>). 
                                In particular, <span class="persName-WGC">Collingwood</span> was indebted to the collector, <a href="../notes/wise_t_j_note.php" target="_blank" class="inactive">Thomas J. Wise (1859&#x2013;1937)</a>, 
                                of whose &#x201C;full account of the published poems&#x201D; in the 
                                <a href="../notes/bibliography_of_ruskin_wise_note.php" target="_blank" class="inactive"><i>Complete Bibliography of . . . Ruskin</i> (1889&#x2013;93)</a> 
                     <span class="persName-WGC">Collingwood</span> &#x201C;gladly availed&#x201D; himself (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1:267; and <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 
                                1:268). <span class="persName-WGC">Collingwood</span> was therefore to some degree influenced by <span class="persName-TJW">Wise</span>&#x02BC;s manipulation of the bibliographical record in favor of juvenile poems 
                                that <span class="persName-TJW">Wise</span> could forge in the form of &#x201C;rare&#x201D; pamphlets (see <a href="../bibiliography/bibliography.php#WISERUSKINI" target="_blank" class="inactive">Dearden, &#x201C;Wise and Ruskin I&#x201D;</a>, 46&#x2013;47; 
                                and <a href="../notes/forgery_and_piracy_note.php" target="_blank" class="inactive">Forgery and Piracy</a>).
                  </p>
                  
                  <p>The tendency to think of the &#x201C;Account&#x201D; as a verse travelogue gained impetus from the elder <span class="persName-JJR">Ruskin</span> himself, 
                                who in <i>Praeterita</i> characterized it as &#x201C;a rhymed history of the tour&#x201D; and a &#x201C;poetical account of our tour, 
                                in imitation of <span class="persName-SR">Rogers</span>&#x02BC;s <i>Italy</i>&#x201D; (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:81). 
                                <span class="persName-WGC">Collingwood</span> quotes this characterization in his description of <a href="../apparatuses/apparatus_msix.php" target="_blank" class="inactive">MS IX</a>, 
                                thus implicitly pointing to some authority for presenting the &#x201C;Account&#x201D; as a verse travelogue. He realized, however, that in the manuscripts available to him, 
                                the &#x201C;Account&#x201D; consisted of both &#x201C;prose and verse&#x201D;; and if pressed, since he had no knowledge of the all&#x2010;verse version in 
                                <a href="../apparatuses/msia_g1_apparatus.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, he would have acknowledged that <span class="persName-JR">Ruskin</span>&#x02BC;s own later understanding 
                                of the &#x201C;Account&#x201D; was tendentious: in boyhood, <span class="persName-JR">Ruskin</span> &#x201C;had intended this volume &#x005B;<a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">the MS IX fair copy</a>&#x005D; 
                                to contain about 150 pieces of prose and poetry, and at least as many drawings! . . . I am pretty certain that . . . &#x005B;when writing <i>Praeterita</i>, 
                                <span class="persName-JR">Ruskin</span>&#x005D; was not aware of the amount of material existing in rough copies at the back of his book&#x2010;shelves,&#x201D; 
                                referring to MS VII, MS VIII, and MS XI 
                                (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1:265; and <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 
                                1:267). Whether <span class="persName-WGC">Collingwood</span> would have included prose selections from the &#x201C;Account&#x201D; in the volume of prose that he projected as a companion to the <span class="date-1891">1891</span> <i>Poems</i> is unknown.
                  </p>
                  
                  <p>What engaged <span class="persName-WGC">Collingwood</span> about the &#x201C;Account&#x201D; was less its genre than its biographical record of the family tour. 
                                He prefaces his edition of the work with a passage from <i>Praeterita</i> in which <span class="persName-JR">Ruskin</span> describes the family setting out on the 
                                <a href="../notes/tour_of_1833.php" target="_blank" class="inactive">Tour of 1833</a> 
                                (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1:119; and <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 
                                1:119; and see <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:79&#x2013;80); 
                                and he was interested in the materials &#x201C;existing in rough copies at the back of . . . &#x005B;<span class="persName-JR">Ruskin</span>&#x02BC;s&#x005D; book&#x2010;shelves,&#x201D; 
                                because the trove afforded him verse to fill out the topographical sequence to the full extent of the family&#x02BC;s tour. 
                                Where these materials lacked titles, <span class="persName-WGC">Collingwood</span> provided identifying place&#x2010;names 
                                (see <a href="#TITLE">Title</a>), sometimes on slender evidence, in order to situate poems in a topographical order. 
                                From the same motive, apparently, he reproduced only drawings that <span class="persName-JR">Ruskin</span> made during the tour itself, 
                                instead of illustrating his edition with the phenomenal <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">the MS IX fair copy</a>. 
                                <span class="persName-WGC">Collingwood</span> reproduces &#x201C;Watch&#x2010;tower at Andernach (<span class="date-1833">1833</span>),&#x201D; 
                                which <span class="persName-JR">Ruskin</span> drew in the manner of <span class="persName-SP">Prout</span>; 
                                and &#x201C;The Jungfrau from Interlachen (<span class="date-1833">1833</span>),&#x201D; in the manner of a <span class="persName-JMWT">Turner</span> vignette 
                                (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 
                                1:128 opp., and 140 opp., respectively). (Despite the appearance of &#x201C;The Jungfrau from Interlachen&#x201D; to a vignette drawn for the 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, it must have been removed prior to the editing of 
                                the Library Edition, as <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> do not record anything like it 
                                in their notes documenting placement of drawings, although at least one of these notes is muddled.)
                  </p>
                  
                  <p>For copytext, <span class="persName-WGC">Collingwood</span>&#x02BC;s first choice were texts of poems as fair&#x2010;copied by <span class="persName-JR">Ruskin</span> in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">the MS IX fair copy</a>, 
                                except for the three poems published in <span class="persName-JR">Ruskin</span>&#x02BC;s youth, in which cases he followed the revised, published texts. For &#x201C;Andernacht&#x201D; and &#x201C;St. Goar,&#x201D; 
                                in addition to the main copytexts drawn from <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, 
                                he supplied the MS IX versions in notes. Even in these texts, <span class="persName-WGC">Collingwood</span> regularized punctuation and spelling, and of course he felt free to so in publishing 
                                texts for which only rough draft was available.
                  </p>
                  
               </div>
               
