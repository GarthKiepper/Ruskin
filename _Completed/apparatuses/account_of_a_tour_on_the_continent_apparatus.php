         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <div id="EDITORIALAPPROACHES" class="subsection">
               <span class="head-subsection">Editorial Approaches to the &#x201C;Account&#x201D;</span>
               
               <p>In the broadest view, the publishing history of the &#x201C;Account&#x201D; up to <span class="date-1903">1903</span> (see <a href="#PUBLICATION"></a>) 
                            might be viewed as a gradual recovery and conjectural reconstruction of <span class="persName-JR">Ruskin</span>&#x02BC;s final intentions for the &#x201C;Account&#x201D;&#x2014;a steady editorial advance toward realizing 
                            his elaborately composite&#x2010;genre, albeit uncompleted <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair&#x2010;copy version</a>. 
                            From the publication in <span class="date-1834">1834</span> and <span class="date-1850">1850</span> by <span class="persName-JR">Ruskin</span>&#x02BC;s earliest editors, along with <span class="persName-JJR">John James Ruskin</span>&#x02BC;s oversight, of &#x201C;fragments&#x201D; from a putative &#x201C;metrical journal,&#x201D; 
                            to the reconstruction in <span class="date-1891">1891</span> by <span class="persName-WGC">W. G. Collingwood</span> of a solely metrical journal that traced the full itinerary 
                            of the <span class="date-1833">1833</span> journey, to the restoration in <span class="date-1903">1903</span> by <span class="persName-ETC">E. T. Cook</span> and <span class="persName-AW">Alexander Wedderburn</span> of prose pieces and drawings to <span class="persName-WGC">Collingwood</span>&#x02BC;s fuller metrical version&#x2014;each 
                            step in this history might be read teleologically as advancing toward realizing <span class="persName-JR">Ruskin</span>&#x02BC;s final vision for the &#x201C;Account,&#x201D; by steadily accruing more of its parts. 
                            The Library Edition version crowns this editorial trend by attempting to represent most comprehensively not only the portion of the project that <span class="persName-JR">Ruskin</span> 
                            fair&#x2010;copied and illustrated in the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX version</a> but also the continuation 
                            of the project that he planned to form a greater whole in his 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msviii_contents.php" target="_blank" class="inactive">List of Proposed Additional Contents for Account of a Tour on the Continent</a>, 
                            and that he partly drafted in MS VII, MS VIII, and MS XI.
               </p>
               
               <p>To see the editorial history as a steady advancement and simple expansion toward a single ideal text would imply, however, 
                            that each of <span class="persName-JR">Ruskin</span>&#x02BC;s editors projected the same ideal text. While all of these editors did presumably have access to sufficient evidence 
                            of <span class="persName-JR">Ruskin</span>&#x02BC;s largest ambitions for the project&#x2014;his composite&#x2010;genre version&#x2014;their respective treatments suggest conflicting views about a preferred ideal text. 
                            The question is complicated by the varying constraints of their publication venues (<i>Friendship&#x02BC;s Offering</i>, 
                            as an <a href="../notes/annuals_note.php" target="_blank">annual</a>, was meant to anthologize only brief lyric poems or tales, 
                            whereas the <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a> aimed at unprecedented completeness in collecting 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s works), but this external factor does not entirely determine the editors&#x02BC; differing ideas about how to represent the &#x201C;Account&#x201D; project 
                            most effectively or&#x2014;what was for the earlier editors inseparable from representation of the text&#x2014;their influence over <span class="persName-JR">Ruskin</span>&#x02BC;s developing idea of authorship.
               </p>
               
               <p>Initially, in terms of authorial intention and audience, and therefore of perceived ideal text, <span class="persName-JR">Ruskin</span> and his editors worked at cross purposes.
                            In the study of modern manuscripts, Donald Reiman has argued for the importance of distinguishing between a manuscript&#x02BC;s status as 
                            <i>public</i>, <i>private</i>, and <i>confidential</i>&#x2014;a distinction that depends, &#x201C;not &#x005B;on&#x005D; its degree of 
                            &#x005B;rhetorical or aesthetic&#x005D; artfulness or the level of personal intimacy between the writer and its readers but the nature and extent of the writer&#x02BC;s intended audience. 
                            A manuscript is <i>private</i> if its author intended it to be read only by one person or a specific small group of people whose identity he knew in advance; 
                            <i>confidential</i> if it was intended for a predefined but larger audience who may&#x2014;or may not&#x2014;be personally known to or interested in the author; 
                            and <i>public</i> only if it was written to be published or circulated for perusal by a widespread, unspecified audience, including such abstractions 
                            as the nation, the reading public, and posterity&#x201D; 
                            (<a href="../bibiliography/bibliography.php#REIMANMODERNMANUSCRIPTS" target="_blank" class="inactive">Reiman, <i>The Study of Modern Manuscripts</i></a>, 65). 
                            In the case of the &#x201C;Account,&#x201D; <span class="persName-JR">Ruskin</span>&#x02BC;s audience was private, in that he expected his productions to be read primarily by his family, but also confidential, 
                            in that his parents shared his work with friends whose identity he could not predict. For example, <span class="persName-JR">Ruskin</span> could not have predicted that samples of his verse 
                            composed (probably) for the &#x201C;Account&#x201D; would be read by the eminent writer, <span class="persName-JH">James Hogg</span>, but <span class="persName-JJR">John James</span> took care in his letter prefacing the sample for <span class="persName-JH">Hogg</span> 
                            to establish confidentiality: &#x201C;Touching my son, there are many to whom I would never name him or his pursuits; but to men of talent and of heart I find I can say 
                            many things that I dare not tell the world at large. It cannot comprehend; it has not patience, nor feeling, nor delicacy&#x201D; 
                            (<a href="../bibiliography/bibliography.php#GARDENMEMORIALS" target="_blank" class="inactive">Garden, ed., <i>Memorials of James Hogg</i></a>, 274). 
                            In writing the &#x201C;Account,&#x201D; <span class="persName-JR">Ruskin</span> establishes the potential confidentiality of such an audience, not by any prefatory claims on the &#x201C;heart&#x201D; of the reader, 
                            but by <i>imitating</i> public status in a document whose immediate audience is private, his family; that is, 
                            in the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair&#x2010;copy version</a>, 
                            <span class="persName-JR">Ruskin</span> imitates the materiality of illustrated travel publications of the <span class="date-1830-1839">1830s</span>, the steel&#x2010;engraved 
                            <a href="../notes/annuals_note.php" target="_blank">Annuals and Other Illustrated Books</a>, 
                            thereby rendering this version perhaps more confidential than the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1, version</a>, 
                            which was a private collaboration with his father.
               </p>
               
               <p>In editing poems from the &#x201C;Account&#x201D; as <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, 
                            <span class="persName-TP">Pringle</span> replaced <span class="persName-JR">Ruskin</span>&#x02BC;s imitation with the actual conditions and rhetoric of a public production, while creating an illusion of the original work 
                            as less confidential than private in Reiman&#x02BC;s terms. A &#x201C;journal&#x201D; belonging to &#x201C;J.R.&#x201D; seems mysteriously private 
                            in keeping with the nineteenth&#x2010;century cult of the personal, which, as Reiman remarks, led to a fashion for collecting autograph manuscripts 
                            (<a href="../bibiliography/bibliography.php#REIMANMODERNMANUSCRIPTS" target="_blank" class="inactive">Reiman, <i>The Study of Modern Manuscripts</i></a>, 18&#x2013;28). 
                            Thus the editor or editors&#x2014;the extent of <span class="persName-JJR">John James Ruskin</span>&#x02BC;s involvement in entitling or revising these poems is unknown&#x2014;widen the supposed gap between public and private. 
                            On the one hand, in <span class="date-1834">1834</span> the general readers of 
                            <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a>, 
                            might have connected the title, <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, 
                            with <span class="persName-GGB">Byron</span>&#x02BC;s <i>Childe Harold&#x02BC;s Pilgrimage</i>; however, on the other hand, the text discourages a sense of the author as an &#x201C;I,&#x201D; 
                            having eliminated the prose pieces that affect an easy, confidentially personal tone, while revising the MS IX versions of 
                            <a href="../showcase/andernacht_poem_msix.php">&#x201C;Andernacht&#x201D;</a> and 
                            <a href="../showcase/st_goar_poem_msix.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a> to achieve a sharper, more objective study in topographical contrast 
                            in their published versions. What <span class="persName-JR">Ruskin</span> had produced as a confidential, imitatively &#x201C;public&#x201D; manuscript in the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> recedes&#x2014;or seems to recede&#x2014;into the privileged knowledge of the editor.
               </p>
               
               <p>This sense of the editor as holding insider&#x02BC;s knowledge was enhanced by the publication of 
                            <a href="../showcase/ehrenbreitstein_poem_msix.php" target="_blank" class="inactive">&#x201C;Ehrenbreitstein&#x201D;</a>  
                            as <a href="../showcase/ehrenbreitstein_poem_1850.php" target="_blank" class="inactive">&#x201C;Ehrenbreitstein: Fragment from a Metrical Journal&#x201D;</a>  
                            in the privately printed and distributed <a href="../notes/poems_1850_note.php" target="_blank" class="inactive"><i>Poems</i> (1850)</a>. 
                            For many recipients of that volume, the title must have suggested merely that the editors were adding another &#x201C;fragment&#x201D; 
                            to the <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a> 
                            that had so far been made public&#x2014;if, indeed, the title meant even that much, 
                            given that the volume did not reprint the two poems that had formed the original &#x201C;Fragments,&#x201D; 
                            published sixteen years earlier. For readers to perceive this publication as more than just a compounding of fragments&#x2014;rather, 
                            as a sophisticated reinstatement of a missing middle poem that, 
                            in <span class="persName-JR">Ruskin</span>&#x02BC;s <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, 
                            had bridged the two poems, which were later separated and published as a more severely contrasting pair in 
                            <i>Friendship&#x02BC;s Offering</i>&#x2014;would 
                            have required a perceptive critical reading of the three fragments considered as a unit, 
                            a reading that would in turn have relied on a comprehensive knowledge of the textual history of the &#x201C;Account.&#x201D; 
                            Besides <span class="persName-JR">Ruskin</span> himself, perhaps only <span class="persName-JJR">John James Ruskin</span> and <span class="persName-WHH">W. H. Harrison</span> possessed that privileged and knowledgable perspective. 
                            Since this edition was printed for private circulation, the editors were in a position to share their privileged knowledge with readers. 
                            Still, for many of the original recipients of the volume (as well as for the collectors who acquired copies later), 
                            <a href="../showcase/ehrenbreitstein_poem_1850.php" target="_blank" class="inactive">&#x201C;Ehrenbreitstein: Fragment from a Metrical Journal&#x201D;</a> 
                            would not have been perceived less as opening up an ideal text of the hitherto unknown portions of a &#x201C;metrical journal,&#x201D; 
                            than as testifying to a private and privileged story about the former omission of this &#x201C;fragment.&#x201D; As <span class="persName-JJR">John James Ruskin</span> wrote to <span class="persName-WHH">Harrison</span>, 
                            clarifying his intention regarding the audience of the <span class="date-1850">1850</span> collection, &#x201C;the Book although apparently so is not intended to be anonymous 
                            in a Single Instance &#x005B;i.e., of selections included in the anthology&#x005D;. It is only <u>denied</u> the public&#x201D; 
                            (Durham, <span class="date-1850-03-24">24 March 1850</span> &#x005B;MSL 003/004/096&#x005D;, in <a href="../bibiliography/bibliography.php#MSL003-004" target="_blank" class="inactive">Letters from John James Ruskin to W. H. Harrison, 1838&#x2013;1853</a>).
               </p>
               
               <p><span class="persName-WGC">W. G. Collingwood</span> situated himself in this tradition of the privileged editor, by virtue of his decision to restrict his <span class="date-1891">1891</span> edition to poetry, 
                            reserving an edition of early prose for a separate volume (see <a href="../notes/poems_1850_note.php" target="_blank" class="inactive">Ruskin, <i>Poems</i> &#x005B;1850&#x005D;</a>). 
                            Now, the audience for the &#x201C;Account&#x201D; was again public, but inflected by the culture of rare book collecting, which created a new audience of eager and competitive consumers 
                            for rare copies of the <span class="date-1850">1850</span> <i>Poems</i> by &#x201C;J.R.&#x201C; Thus, in one sense, <span class="persName-WGC">Collingwood</span>&#x02BC;s version maintains the tradition 
                            of allowing public access to only fragments from a private horde&#x2014;so private that not even <span class="persName-JR">Ruskin</span> himself, much less the collector privileged by mere cash 
                            to own the <span class="date-1850">1850</span> <i>Poems</i>, was &#x201C;aware of the amount of material &#x005B;for the &#x201C;Account&#x201D;&#x005D; existing in rough copies at the back of . . . &#x005B;<span class="persName-JR">Ruskin</span>&#x02BC;s&#x005D; book&#x2010;shelves&#x201D; 
                            (see <a href="#PUBLICATION1891">Publication: 1891</a>). In another sense, <span class="persName-WGC">Collingwood</span> could re&#x2010;frame the &#x201C;Account&#x201D; as a public work, 
                            available to an anonymous audience, since <span class="persName-JR">Ruskin</span> had told its story in <i>Praeterita</i>. <span class="persName-WGC">Collingwood</span> signals this orientation by quoting long, contextualizing passages 
                            from the autobiography at the head of the poem and in his notes; and his his version of the text, which expands the verse selections to cover the full scope of the <span class="date-1833">1833</span> tour, 
                            serves as a poetic, documentary gloss on <span class="persName-JR">Ruskin</span>&#x02BC;s prose autobiographical narration of the master&#x02BC;s first significant experience of Continental landscape and towns. 
                            Yet, if <span class="persName-WGC">Collingwood</span> relies on the autobiography to authorize his version of a public text, 
                            his all&#x2010;verse version can claim only faulty authority relative to the manuscript &#x201C;material . . . at the back of . . . &#x005B;<span class="persName-JR">Ruskin</span>&#x02BC;s&#x005D; book&#x2010;shelves,&#x201D; 
                            for this version is not the young <span class="persName-JR">Ruskin</span>&#x02BC;s composite&#x2010;genre &#x201C;Account.&#x201D; Ironically, 
                            the manuscript that would have supplied some authorization for such a version&#x2014;the all&#x2010;verse, and perhaps more private collaboration between <span class="persName-JR">Ruskin</span> and his father, 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>&#x2014;was unknown to <span class="persName-WGC">Collingwood</span>.
               </p>
               
               <p><span class="persName-WGC">Collingwood</span>&#x02BC;s version therefore was a step toward an ideal text of <span class="persName-JR">Ruskin</span>&#x02BC;s original intentions for the &#x201C;Account&#x201D; only in a limited sense. 
                            As if jealous even of the public dimension that he afforded the work by constructing it as a tour poem illustrative of <i>Praeterita</i>, 
                            <span class="persName-WGC">Collingwood</span> substituted a new narrative in his <span class="date-1894">1894</span> biography, 
                            <a href="../bibiliography/bibliography.php#WGCLIFEOFJR1893" target="_blank" class="inactive"><i>Life and Work of John Ruskin</i></a>, 
                            suggesting that <span class="persName-JR">John</span>&#x02BC;s motives in making the work were furtive and separate from his father&#x02BC;s interests, 
                            a closely held harbinger of <span class="persName-JR">Ruskin</span>&#x02BC;s mature mission in <i>Modern Painters</i> 
                            (see <a href="../bibiliography/bibliography.php#HANSONRUSKIN1830S" target="_blank" class="inactive">Hanson, &#x201C;Ruskin in the 1830s&#x201D;</a>). 
                            This conception is almost the opposite of <span class="persName-JR">Ruskin</span>&#x02BC;s intention in his youth, which was to imitate a public work of his own time&#x2014;an 
                            imitation of <span class="persName-SR">Rogers</span>&#x02BC;s <i>Italy</i>, as acknowledged in <i>Praeterita</i>&#x2014;and which 
                            was meant to be confidentially shared with readers who qualified as &#x201C;men of talent and of heart.&#x201D; 
                            Thus, <span class="persName-WGC">Collingwood</span>&#x02BC;s version was a step toward restoration of <span class="persName-JR">Ruskin</span>&#x02BC;s original youthful project only in the sense that it provided an expanded framework, 
                            which <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> could dramatically fill out with the poems&#x02BC; missing complementary prose pieces, and with (at least) one reproduction of a hand&#x2010;illustrated page, along with footnotes describing 
                            the remaining drawings and where in the work they are placed. The Library Edition&#x02BC;s version is the first genuine attempt at representing <span class="persName-JR">Ruskin</span>&#x02BC;s work 
                            as originally conceptualized. At the same time, <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> inherited the agendas of their predecessors, and they too inevitably framed the work also 
                            as supplemental material illustrating <span class="persName-JR">Ruskin</span>&#x02BC;s autobiography.
               </p>
               
            </div>
            
            
