            <div id="SOURCES" class="subsection">
               <span class="head-subsection">Sources and Influences</span>
               
               <p>The most familiar description of the literary and artistic context surrounding both the actual journey of <span class="date-1833">1833</span> and 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s composition of the &#x201C;Account&#x201D; is his story in the autobiography, <span class="title-book-JRPRAETERITA"><i>Praeterita</i></span>, 
                            in which he declares that &#x201C;the main tenor of my life&#x201D; was established by the gift of the <span class="date-1830">1830</span> 
                            illustrated edition of the topographical poem <span class="title-poem-ROGERSITALY"><i>Italy</i></span> by 
                            <a href="../notes/samuel_rogers_note.php" target="_blank" class="inactive">Samuel Rogers (1763&#x2013;1855)</a>, 
                            with engraved vignettes after drawings by <a href="../notes/turner_j_m_w_note.php" target="_blank" class="inactive">J. M. W. Turner (1775&#x2013;1851)</a>, 
                            <a href="../notes/stothard_thomas_note.php" target="_blank">Thomas Stothard (1755&#x2013;1834)</a>, 
                            <a href="../notes/prout_samuel_note.php" target="_blank">Samuel Prout (1783&#x2013;1852)</a>, and others 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 39:79; and see 
                            <a href="../bibiliography/bibliography.php#ROGERSITALY1830" target="_blank" class="inactive">Rogers, <i>Italy</i> &#x005B;1830&#x005D;</a>).
               </p>
               
               <p>A second influence, according to <span class="title-book-JRPRAETERITA"><i>Praeterita</i></span>, was the family&#x02BC;s acquisition in <span class="date-1833-04">April 1833</span> 
                            of <a href="../bibiliography/bibliography.php#PROUTSKETCHES" target="_blank" class="inactive"><span class="title-book-PROUTSKETCHES"><i>Facsimiles of Sketches Made in Flanders and Germany</i></span></a> 
                            by <a href="../notes/prout_samuel_note.php" target="_blank">Samuel Prout (1783&#x2013;1852)</a>: 
                            &#x201C;I well remember going with my father into the shop where subscribers entered their names, 
                            and being referred to the specimen print, the turreted window over <span class="placeName-MOSELLE">Moselle</span>, 
                            at <span class="placeName-COBLENTZ">Coblentz</span>. We got the book home to <span class="placeName-HERNE">Herne Hill</span> before the time
                            of our usual annual tour&#x201D; 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 39:79; and see 
                            <a href="../bibiliography/bibliography#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 286 n. 1). 
                            This work, which was indeed published that spring with <span class="persName-JJR">John James Ruskin</span> listed among the printed list of subscribers, 
                            is credited by <span class="persName-JR">Ruskin</span>, doubtless apocryphally, with having prompted the family&#x02BC;s tour spontaneously, 
                            whereas the journey must in fact have long been in planning: 
                            &#x201C;as my mother watched my father&#x02BC;s pleasure and mine in looking at the wonderful places, she said, why should we not go and see some of them in reality? 
                            My father hesitated a little, then with glittering eyes said&#x2014;why not? And there were two or three weeks of entirely rapturous and amazed preparation&#x201D; 
                            (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 39:79). It was likely the case, however, 
                            that <span class="persName-SP">Prout</span> helped to orient the family to the north of Europe, as <span class="persName-SR">Rogers</span> oriented them to the southern region of the Continent 
                            (see ).
               </p>
               
               <p>While these two publications of course did not hold exclusive sway over either the Ruskins&#x02BC; tour itinerary or <span class="persName-JR">Ruskin</span>&#x02BC;s embodiment of their experiences in the <span class="title-JRACCOUNTTOUR">&#x201C;Account&#x201D;</span>, 
                            <span class="persName-SR">Rogers</span>&#x02BC;s and <span class="persName-SP">Prout</span>&#x02BC;s books did play an important and representative role in <span class="persName-JR">Ruskin</span>&#x02BC;s conceptualization of his family&#x02BC;s northern and southern destinations, 
                            and in the interplay of tourism and the materiality of travel writing and publication in the <span class="date-1830-1839">1830s</span>. 
                            <span class="persName-SP">Prout</span>&#x02BC;s <span class="title-book-PROUTSKETCHES"><i>Facsimiles</i></span> provided a pictorial tour through 
                            <span class="placeName-BELGIUM">Belgium</span> and <span class="placeName-GERMANY">Germany</span>, 
                            whereas <span class="persName-SP">Rogers</span>&#x02BC;s <span class="title-poem-ROGERSITALY"><i>Italy</i></span> led the reader on the more traditional Grand Tour of the South. 
                            The books contrasted also in their material representations. The <i>Facsimiles</i> consists of fifty lithographic prints 
                            of picturesque medieval and Renaissance architectural sites in northern <span class="geogName-EUROPE">Europe</span>, and it contains no letterpress&#x2014;only 
                            the place names lettered directly onto the lithographic stone, as part of the image, to identify the locales depicted. 
                            The lithographs, then a new medium in <span class="placeName-BRITAIN">Britain</span>, would have struck the Ruskins with their dramatically large size 
                            (22 by 15 inches) and watercolor&#x2010;like texture, as compared with the exquisite preciousness of the finely etched, steel&#x2010;engraved vignettes 
                            (2&#x2013;3 inches by 3&#x2013;4 inches) that illustrate <span class="persName-SR">Rogers</span>&#x02BC;s <span class="date-1830">1830</span>
                  <span class="title-poem-ROGERSITALY"><i>Italy</i></span>. (A more typical size for the engraved area of steel&#x2010;engraved book illustration was 7 by 5 inches 
                            &#x005B;<a href="../bibiliography/bibliography.php#HUNNISETTSTEELENGRAVED" target="_blank" class="inactive">Hunnisett, <i>Steel&#x2010;Engraved Book Illustration in England</i></a>, 35&#x005D;.) 
                            <span class="persName-SR">Rogers</span>&#x02BC;s <span class="title-poem-ROGERSITALY"><i>Italy</i></span> was also a more traditional work, despite its up&#x2010;to&#x2010;date reliance on the 
                            new technologies of steel engraving and mass production that sponsored the boom in <a href="../notes/annuals_note.php" target="_blank">Annuals and Other Illustrated Books</a>. 
                            <span class="persName-SR">Rogers</span>&#x02BC;s verse and taste were late&#x2010;eighteenth&#x2010;century in manner, and his <span class="title-poem-ROGERSITALY"><i>Italy</i></span> could not compete in popularity with 
                            <span class="persName-GGB">Lord Byron</span>&#x02BC;s fourth part of <span class="title-poem-BYRONPILGRIMAGE"><i>Childe Harold&#x02BC;s Pilgrimage</i></span>, which is set in <span class="placeName-VENICE">Venice</span>. 
                            <span class="persName-SR">Rogers</span>&#x02BC;s poem gained popular acclaim solely by means of the <span class="date-1830">1830</span> edition&#x02BC;s graphic enhancements 
                            (see <a href="../notes/samuel_rogers_note.php" target="_blank" class="inactive">Samuel Rogers (1763&#x2013;1855)</a>).
               </p>
               
               <p><span class="persName-JR">Ruskin</span>&#x02BC;s <span class="title-JRACCOUNTTOUR">&#x201C;Account&#x201D;</span> underwent a development parallel to that of <span class="persName-SR">Rogers</span>&#x02BC;s work, from a travelogue written solely in verse and in an eighteenth&#x2010;century manner 
                            of topographical poetry, to an illustrated composite&#x2010;genre travel narrative typical of the steel&#x2010;engraved landscape <a href="../notes/annuals_note.php" target="_blank">annuals</a> 
                            of the <span class="date-1830-1839">1830s</span> (see <a href="#COMPPUB">Composition and Publication</a>). As remarked above, there is some ambiguity concerning when the Ruskin family 
                            acquired a copy of the <span class="date-1830">1830</span> illustrated edition of <span class="title-poem-ROGERSITALY"><i>Italy</i></span>, which they already owned in an earlier, non&#x2010;illustrated version. 
                            Regardless of when the book entered the household, however, its influence is palpable from the start of <span class="persName-JR">Ruskin</span>&#x02BC;s 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>. Critics have focused too exclusively on <span class="persName-SR">Rogers</span>&#x02BC;s <span class="title-poem-ROGERSITALY"><i>Italy</i></span> 
                            as <span class="persName-JR">Ruskin</span>&#x02BC;s model, at the cost tracing his imitation of <span class="persName-SP">Prout</span>&#x02BC;s <span class="title-book-PROUTSKETCHES"><i>Facsimiles</i></span> 
                            (see, e.g., <a href="../bibiliography/bibliography.php#SPEARRUSKINSITALY" target="_blank" class="inactive">Spear, &#x201C;Ruskin&#x02BC;s Italy&#x201D;</a>), 
                            but it is true that <span class="persName-SR">Rogers</span>&#x02BC;s book frames the foundational layout and conception of the composite&#x2010;genre <span class="title-JRACCOUNTTOUR">&#x201C;Account&#x201D;</span>. 
                            <span class="persName-JR">Ruskin</span> modeled his section divisions on <span class="persName-SR">Rogers</span>&#x02BC;s layout, observing how text and illustration form a coherent unit. 
                            Like <span class="persName-SR">Rogers</span>, he entitles his topographical sections so as to identify a landmark destination&#x2014;&#x201C;Calais,&#x201D; &#x201C;Cassel,&#x201D; &#x201C;Lille,&#x201D; &#x201C;Brussels,&#x201D; 
                            &#x201C;The Meuse,&#x201D; &#x201C;Aix la Chapelle,&#x201D; &#x201C;Cologne,&#x201D; &#x201C;Andernacht,&#x201D; &#x201C;Ehrenbreitstein,&#x201D; &#x201C;St. Goar&#x201D; and 
                            &#x201C;Heidelberg&#x201D;&#x2014;and for these section titles, he uses a lettering that imitates <span class="persName-SR">Rogers</span>&#x02BC;s restrained display type. 
                            (&#x201C;St. Goar&#x201D; and &#x201C;Heidelberg&#x201D; are missing their headings, owing to the unfinished state of the fair copy.) 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s placement of illustration vis&#x2010;à&#x2010;vis text likewise imitates <span class="persName-SR">Rogers</span>&#x02BC;s layout, 
                            by heading the first page of each section with a landscape vignette, above the title, 
                            in order to situate the reader in a distinct place, and by closing each section with a figure vignette, as a tailpiece in order to caption some portion of the narrative.
               </p>
               
               <p>In this arrangement of text and image, <span class="persName-JR">Ruskin</span> captured a notable innovation of the <span class="date-1830">1830</span> illustrated edition of <span class="title-poem-ROGERSITALY"><i>Italy</i></span>. 
                            <span class="persName-SR">Rogers</span> probably based his layout on the admired designs by 
                            <span class="persName-WBPUBLISHER">William Bulmer</span> (<span class="date-1757-1830">1757&#x2013;1830</span>) for his <span class="orgName-">Shakspeare Press</span>, 
                            particularly <span class="persName-WBPUBLISHER">Bulmer</span>&#x02BC;s editions in the <span class="date-1790-1799">1790s</span> of English poets, with wood&#x2010;engraved vignettes by <span class="persName-">Thomas Bewick</span> (<span class="date-1753-1828">1753&#x2013;1828</span>) 
                            set at the heads of title pages of the poems 
                            (<a href="../bibiliography/bibliography.php#PIGGOTTTURNERSVIGNETTES" target="_blank" class="inactive">Piggott, <i>Turner&#x02BC;s Vignettes</i></a>, 20). 
                            This elegant page layout was not possible in the mass&#x2010;produced 
                            <a href="../notes/annuals_note.php" target="_blank">Annuals and Other Illustrated Books</a>, which first arose in the <span class="date-1820-1829">1820s</span>. 
                            In order to achieve wide distribution, these popular books relied on the durability of steel engraving, 
                            an intaglio process that clashes with relief type. In the eighteenth&#x2010;century illustrated book, <span class="persName-">Bewick</span>&#x02BC;s fine wood engravings could be set in the same frame as the type, 
                            allowing text and image to be printed together on the same page. To solve this problem, and achieve the elegant effects of <span class="persName-WBPUBLISHER">Bulmer</span>&#x02BC;s designs, 
                            and yet print the books on a massive scale, <span class="persName-SR">Rogers</span>&#x02BC;s printers innovated a technique, 
                            which was widely remarked in reviews of the <span class="date-1830">1830</span> <span class="title-poem-ROGERSITALY"><i>Italy</i></span>, 
                            whereby precise registration allowed the sheets to be run through the press twice, once for the engraving, and once for the type 
                            (<a href="../bibiliography/bibliography.php#POWELLTURNERSVIGNETTES" target="_blank" class="inactive">Powell, &#x201C;Turner&#x02BC;s Vignettes and the Making of Rogers&#x02BC; &#x2018;Italy&#x2019;&#x201D;</a>, 3). 
                            <span class="persName-JR">Ruskin</span> was attentive to this up&#x2010;to&#x2010;date innovation.
               </p>
               
               <p>But <span class="persName-JR">Ruskin</span>&#x02BC;s fair&#x2010;copy version is not just an illustrated travel poem, but a composite&#x2010;genre travelogue, combining prose with verse and illustration. 
                            Although <span class="persName-SR">Rogers</span>&#x02BC;s <i>Italy</i> is subtitled <i>A Poem</i>, the author 
                            did include some prose&#x2014;brief essays and a tale&#x2014;in &#x201C;Part the Second&#x201D; (<span class="date-1828">1828</span>) of the work 
                            (&#x201C;National Prejudices,&#x201D; &#x201C;Caius Cestius,&#x201D; &#x201C;Foreign Travel,&#x201D; and &#x201C;The Bag of Gold,&#x201D; in 
                            <a href="../bibiliography/bibliography.php#ROGERSITALY1828" target="_blank" class="inactive">Rogers, <i>Italy</i> (1828)</a>, 
                            35&#x2013;40, 51&#x2013;52, 62&#x2013;71, 107&#x2013;19); and he reprinted these prose pieces and added two more tales to the <span class="date-1830">1830</span> edition 
                            (&#x201C;Marcolini&#x201D; and &#x201C;Marco Griffoni,&#x201D; in 
                            <a href="../bibiliography/bibliography.php#ROGERSITALY1830" target="_blank" class="inactive">Rogers, <i>Italy</i> (1830)</a>, 
                            85&#x2013;87, 230&#x2013;32). But <span class="persName-SR">Rogers</span> disperses his few prose pieces irregularly, with at most a pattern of loose association 
                            with their immediately surrounding poems, whereas <span class="persName-JR">Ruskin</span> constructs each of his topographical sections (with one exception), using both verse and prose. 
                            Unlike <span class="persName-SR">Rogers</span>, <span class="persName-JR">Ruskin</span> does not independently title his prose pieces, but subordinates them to the topographical unit, 
                            separating prose from poem with a drawing, which serves as a hinge between the writing and binds the parts into an associative, but still coherent unit. 
                            While complementing the poems, <span class="persName-JR">Ruskin</span>&#x02BC;s prose pieces also contrast with his rather generalized, picturesque poetic views, by bringing them down to earth 
                            with first&#x2010;person, anecdotal and comedic commentary about specific locale.
               </p>
               
               <p>By this arrangement, <span class="persName-JR">Ruskin</span> amplifies the pattern of text and illustration in the <span class="date-1830">1830</span> <i>Italy</i>, which typically consists of a landscape vignette 
                            after a drawing by <span class="persName-JMWT">Turner</span> as a header, above a poem&#x02BC;s title, and a figure vignette after a drawing by <span class="persName-TSP">Stothard</span> as a tailpiece. While in its present condition, 
                            the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> is missing many drawings 
                            (see <a href="#FAIRCOPY">Fair Copy</a>), sufficient examples remain in place to confirm <span class="persName-JR">Ruskin</span>&#x02BC;s intentions. 
                            Compared to <i>Italy</i>, the &#x201C;Account&#x201D; not only complements each poem with a prose piece, 
                            but also supplies each poem and prose piece with its own headpiece and tailpiece&#x2014;four vignettes in total 
                            for each topographical unit, the prose piece&#x02BC;s vignette serving as the hinge. <span class="persName-JR">Ruskin</span> honors <span class="persName-SR">Rogers</span>&#x02BC;s pattern of using a landscape or cityscape as a headpiece, 
                            and a figure drawing as tailpiece, but he varies the pattern somewhat (as does <span class="persName-SR">Rogers</span>) by occasionally heading a section or a prose piece 
                            with a more anecdotal, narrative vignette.
               </p>
               
               <p>The most striking variation is the section on &#x201C;Aix la Chapelle,&#x201D; which begins with an imposing drawing 
                            in <span class="persName-SP">Prout</span>&#x02BC;s manner of the city&#x02BC;s cathedral, followed by the section heading, but which proceeds with a prose piece only, and no poem. 
                            At the end of the section, the manuscript shows a large gap, which may have been meant to be filled by a tailpiece, but which was more likely reserved 
                            for the headpiece of the following section, &#x201C;Cologne&#x201D; (for which the heading is lettered at the bottom of the page). 
                            The condensation of this section&#x02BC;s parts into only a drawing and a prose piece contrasts with the expansion and elaboration that prevails in other sections. 
                            <span class="persName-JR">Ruskin</span> carries the condensation into the prose commentary, which he writes in both the comic and the picturesque modes (the latter typically represented by verse 
                            in the &#x201C;Account&#x201D;), with the speaker first riffing on the <span class="date-1748-04-24">Peace of Aix la Chapelle</span> to mock the somnolent &#x201C;peace treaties&#x201D; governing the local postillions, 
                            but then rhapsodically describing with the sublimity of the cathedral&#x02BC;s interior in moonlight. Finally, the speaker parodies a guidebook&#x02BC;s art and artifact tour 
                            of the cathedral. Perhaps <span class="persName-JR">Ruskin</span> chose &#x201C;Aix la Chapelle&#x201D; as an exercise in condensation in order to signify the city&#x02BC;s pivotal geographical position 
                            at the juncture of <span class="placeName-BELGIUM">Belgium</span>, <span class="placeName-NETHERLANDS">Netherlands</span>, and <span class="placeName-GERMANY">Germany</span>, and the its distinctive, imperial status.
               </p>
               
               <p>At the opposite end of the spectrum, <span class="persName-JR">Ruskin</span> carries out the principle of elaborating <span class="persName-SR">Rogers</span>&#x02BC;s pattern to its fullest in the sections on &#x201C;Andernacht,&#x201D; 
                            &#x201C;Ehrenbreitstein,&#x201D; and &#x201C;St. Goar.&#x201D; The outer sections of the trio work as contrasts&#x2014;the first, gothic in mode; and the third, domestic. Between them, 
                            &#x201C;Ehrenbreitstein&#x201D; plays with the topic of confluence, as the meeting point of the darkly gothic and of sunny domesticity, symbolized by the meeting point 
                            of the <span class="geogName-RHINE">Rhine River</span> and the <span class="geogName-MOSELLE">Moselle River</span>. The fortress of <span class="placeName-EHRENBREITSTEIN">Ehrenbreitstein</span> looms over the rivers&#x02BC; juncture, depicted by <span class="persName-JR">Ruskin</span> in a vignette copied after <span class="persName-JMWT">Turner</span>. 
                            Each of the sections is planned for the fullest complement of poetry, prose, and drawings; together, the three sections traverse an elaborate yet balanced 
                            range of topographical modes.
               </p>
               
               <p>In his drawings imitating engravings, <span class="persName-JR">Ruskin</span> strives to harmonize his illustrations with text in the manner of <span class="persName-SR">Rogers</span>&#x02BC;s landscape illustrator, 
                            <a href="../notes/turner_j_m_w_note.php" target="_blank" class="inactive">J. M. W. Turner (1775&#x2013;1851)</a>, 
                            who exploited the potential of fine&#x2010;lined but durable steel engraving to allow indefinite borders of an engraving to melt into the white space of the paper, 
                            creating a world within the page 
                            (<a href="../bibiliography/bibliography.php#PIGGOTTTURNERSVIGNETTES" target="_blank" class="inactive">Piggott, <i>Turner&#x02BC;s Vignettes</i></a>, 14&#x2013;16). 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s drawings emphasize how such frameless, soft&#x2010;edged rectangular, square, or oval designs harmonize 
                            with his left&#x2010; and right&#x2010;justified blocks of text to make a unity of effect. Less noticed is <span class="persName-JR">Ruskin</span>&#x02BC;s attention to the contrasting textures of <span class="persName-SP">Prout</span>&#x02BC;s lithographs, 
                            such as his copy of <span class="persName-SP">Prout</span>&#x02BC;s &#x201C;Liege,&#x201D; which concludes the section on &#x201C;The Meuse.&#x201D; Exacting as it is, the copy is not just mechanical, but an exploration of 
                            the anecdotal, touristic world of the traveler&#x02BC;s picturesque opened up by the fluid and watercolor&#x2010;like texture of a lithograph, 
                            as compared with the fairy world of light and exotic landscape revealed by the exquisite intaglio of <span class="persName-JMWT">Turner</span>&#x02BC;s vignettes. As the largest of <span class="persName-JR">Ruskin</span>&#x02BC;s drawings 
                            for the &#x201C;Account,&#x201D; set broadside and filling an entire page of the MS IX notebook, the &#x201C;Liege&#x201D; copy comments also on the scale 
                            of <span class="persName-SP">Prout</span>&#x02BC;s folio&#x2010;sized <i>Facsimiles</i> as a contrast with the miniature vignette illustrations for <span class="persName-SR">Rogers</span>&#x02BC;s <i>Italy</i>.
               </p>
               
               <p>In <span class="persName-JR">Ruskin</span>&#x02BC;s liveliness of response to the material world of art production and reproduction that guided early Victorian picturesque travel, his achievement 
                            in the &#x201C;Account&#x201D; poses possibilities that are neglected by interpretations that insist on the narrow constraints and neuroticism of <span class="persName-JR">Ruskin</span>&#x02BC;s early self&#x2010;instruction 
                            in drawing. For example, Paul H. Walton  sees <span class="persName-JR">Ruskin</span>&#x02BC;s adoption of <span class="persName-SP">Prout</span>&#x02BC;s &#x201C;outline style&#x201D; of depicting old architecture with &#x201C;broken lines and dots&#x201D; 
                            as one example of the youth&#x02BC;s tendency &#x201C;to revert to pen and ink in more factual studies of picturesque buildings and views,&#x201D; 
                            owing to a misguided direction of &#x201C;his earliest art activity . . . along lines strictly defined by adult standards and ideas of what 
                            was educationally desirable, so that he was not encouraged, nor did he have the time, to draw in a playful way the kinds of figure subjects in which a child 
                            imaginatively expresses and defines a conception of himself within a framework of human relationships.&#x201D; As a contrast with this &#x201C;crippl&#x005B;ing&#x005D;&#x201D; of his 
                            imagination, which &#x201C;had much to do with the tragedy of his personal life,&#x201D; Walton points to &#x201C;the happy, normal family life of a boy&#x201D; like <span class="persName-JR">Ruskin</span>&#x02BC;s 
                            later sexual nemesis, John Everett Millais, whose youthful drawings included the kind of &#x201C;subjects usually preferred by boy artists: 
                            soldiers, animals, machines, caricatures of family and friends.&#x201D; As a consequence, even when a prospect of freedom opened up in <span class="persName-JR">Ruskin</span>&#x02BC;s youthful art teaching, 
                            such as when his first drawing master, <a href="../notes/runciman_charles_note.php" target="_blank" class="inactive">Charles Runciman (1798&#x2013;1864)</a> challenged the boy &#x201C;to imitate the free, 
                            controlled sweep of the pencil in . . . &#x005B;the teacher&#x02BC;s&#x005D; models,&#x201D; <span class="persName-JR">Ruskin</span> retreated to his more formal depictions of buildings in pen and ink 
                            (<a href="../bibiliography/bibliography.php#WALTONDRAWINGS" target="_blank" class="inactive">Walton, <i>Drawings of John Ruskin</i></a>, 13, 7, 8, 7, 6). 
                            It is not true, however, that <span class="persName-JR">Ruskin</span>&#x02BC;s writing and drawings neglect subjects appropriate to &#x201C;boy artists&#x201D;; 
                            more importantly, Walton&#x02BC;s division of the normative from the creative, laden with convention, omits how spontaneity and invention can be prompted 
                            in surprising ways, and one of these is <span class="persName-JR">Ruskin</span>&#x02BC;s playfulness in manipulating the material culture surrounding a youth of the <span class="date-1830-1839">1830s</span>. Just as <span class="persName-JR">Ruskin</span> not only amuses himself 
                            with shrinking the dimensions of the &#x201C;Aix la Chapelle&#x201D; section, but also insightfully considers how he can work within these self&#x2010;imposed contraints 
                            to make his prose for this section compensate for the lack of a poem by multiplying its registers of effect, 
                            so he both playfully and meaningfully shrinks the scale of his Proutesque drawing 
                            for the &#x201C;Lille&#x201D; section, with its topic of recession and expansion through motion&#x2014;the travelers receding &#x201C;Farther and farther . . . / From <span class="placeName-CASSEL">Cassel</span>s insulated crest&#x201D; to where &#x201C;Lille upon us sudden 
                            broke&#x201D; in its &#x201C;rich irregularity&#x201D; of &#x201C;form and figure fair, / . . . moving, breathing, living there&#x201D; (<a href="../showcase/lille_poem_msix.php">"Lille"</a>, 
                            lines 11&#x2013;12, 32, 38&#x2013;40). In a contrasting strategy, he swells his Proutesque drawing of the facade of Aachen Cathedral for &#x201C;Aix la Chapelle&#x201D; to almost 
                            a full page, in keeping with the prose description of a monumental cathedral as &#x201C;a noble, a beautiful, a sublime thing&#x201D; 
                            (<a href="../showcase/aix_la_chapelle_prose_msix.php">&#x201C;Aix la Chapelle&#x201D;</a>).
               </p>
               
               <p>Viewed in this way, the &#x201C;Account&#x201D; seems less the &#x201C;unfinished folly&#x201D; that <span class="persName-JR">Ruskin</span> 
                            condemned in <i>Praeterita</i> (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:81), 
                            than a project that had yielded as much creative potential as <span class="persName-JR">Ruskin</span> could teach himself, 
                            despite his compulsively listing future additions to the work on the back flyleaves of <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a>. 
                            The project propelled him forward into active participation as the author, &#x201C;J.R.,&#x201D; in the material culture of ekphrastic and topographical 
                            illustrated publication.
               </p>
               
               
               <div id="PROUT" class="sub-subsection">
                  <span class="head-sub-subsection">Views of Northern Europe in <span class="persName-SP">Prout</span> and Other Literary and Artistic Sources</span>
                  
                  <p>For British travelers in the <span class="date-1820-1839">1820s&#x2013;30s</span>, emergent developments in touristic experience were perhaps most vivid in new and less familiar routes 
                            opening up in northern Europe alongside the well&#x2010;worn path of the Grand Tour to <span class="placeName-ITALY">Italy</span>. 
                            Apart from the attraction of <span class="placeName-WATERLOO">Waterloo</span> Field, travel in the Pas&#x2010;de&#x2010;Calais and <span class="placeName-FLANDERS">Flanders</span> remained 
                            comparatively unfamiliar except as a route to be got over as quickly as possible on the way to <span class="placeName-FRANCE">France</span> and <span class="placeName-GERMANY">Germany</span>. 
                            While in <i>Praeterita</i> <span class="persName-JR">Ruskin</span> exaggerates the solitary significance of 
                            <span class="persName-SP">Prout</span>&#x02BC;s <i>Facsimiles</i> (<span class="date-1833">1833</span>) in prompting the family&#x02BC;s 
                            journey, the artist&#x02BC;s early importance to British perceptions of northern Europe is corroborated by the novelty of his representations of towns 
                            in northern <span class="placeName-FRANCE">France</span>, <span class="placeName-BELGIUM">Belgium</span>, and <span class="placeName-GERMANY">Germany</span>. 
                            If <span class="persName-GGB">Byron</span> had rushed readers down the <span class="geogName-RHINE">Rhine</span> in canto 3 (<span class="date-1816">1816</span>) of <i>Childe Harold&#x02BC;s Pilgrimage</i> (stanzas 46&#x2013;61, in 
                            <a href="../bibiliography/bibliography.php#BYRONWORKS" target="_blank" class="inactive">Byron, <i>Complete Poetical Works</i>, ed. McGann</a>, 2:93&#x2013;99), 
                            <span class="persName-SP">Prout</span> enabled viewers to dwell on <i>Illustrations of the Rhine</i>, published <span class="date-1822">1822</span>&#x2013;<span class="date-1826">26</span>, 
                            based on an <span class="date-1821">1821</span> tour (see <a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <i>Samuel Prout</i></a>, 50&#x2013;53). 
                            (<span class="persName-SP">Prout</span>&#x02BC;s volume documenting tours of southern <span class="geogName-EUROPE">Europe</span>, 
                            <i>Sketches Made in France, Switzerland, and Italy</i>, was not published until <span class="date-1839">1839</span>.) 
                            While such scenic representations grew more familiar in the <span class="date-1836-1839">later 1830s</span> and <span class="date-1840-1849">1840s</span> 
                            from similar projects by <span class="persName-TMR">T. M. Richardson</span> (<span class="date-1784">1784</span>&#x2013;<span class="date-1848">1848</span>), 
                            <span class="persName-JDH">J. D. Harding</span> (<span class="date-1797">1797</span>&#x2013;<span class="date-1863">1863</span>), 
                            <span class="persName-TSB">T. S. Boys</span> (<span class="date-1803">1803</span>&#x2013;<span class="date-1874">74</span>), 
                            <span class="persName-CFS">Clarkson Stanfield</span> (<span class="date-1793">1793</span>&#x2013;), 
                            and <span class="persName-LH">Louis Haghe</span> (<span class="date-1806">1806</span>&#x2013;<span class="date-1885">85</span>), 
                            <span class="persName-SP">Prout</span> was a foundational influence on artists seeking architectural picturesque subjects in these regions
                            (<a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <i>Samuel Prout</i></a>, 76).
                  </p>
                  
                  <p>Since <span class="persName-SP">Prout</span> had established his lead in depicting the market towns of the Low Countries and <span class="placeName-GERMANY">Germany</span> 
                            since the <span class="date-1820-1829">1820s</span>, his <i>Facsimiles</i> of <span class="date-1833">1833</span> 
                            may well have served as a kind of guidebook for the Ruskins (although of course they could not take a folio volume with them). <span class="persName-SP">Prout</span>&#x02BC;s views would have 
                            persuaded them to adopt a more leisurely and attentive pace through these towns, on the first leg of their Continental tour. 
                            In fact, the route that the Ruskins traced through northern <span class="placeName-FRANCE">France</span> and along the <span class="placeName-RHINE">Rhine</span> 
                            in <span class="placeName-GERMANY">Germany</span> is strikingly similar to the actual path of <span class="persName-SP">Prout</span>&#x02BC;s sketching tour 
                            of <span class="date-1821">1821</span>, although the sequence of the published lithographs in <i>Facsimiles</i> departs somewhat from the actual route, 
                            opening with a market scene occupying the square in front of the Hotel de Ville in <span class="placeName-BRUSSELS">Brussels</span> 
                            (see <a href="../bibiliography/bibliography.php#LOCKETTPROUT" target="_blank" class="inactive">Lockett, <i>Samuel Prout</i></a>, 50, 59). 
                            For <span class="persName-SP">Prout</span>&#x02BC;s itinerary and his place among other travel publications relating to northern <span class="geogName-EUROPE">Europe</span> during this period, see 
                            <a href="../notes/tour_of_1833.php#NORTHERNEUROPE" target="_blank" class="inactive">Tour of 1833: Northern France, Belgium, and Germany</a>.
                  </p>
                  
