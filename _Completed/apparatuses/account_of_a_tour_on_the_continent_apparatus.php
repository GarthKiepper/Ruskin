         <div id="COMPPUB" class="section">
            <span class="head-section">Composition and Publication</span>
            
            <p>To reconstruct the composition and explain the publication history of the 
                        &#x201C;Account,&#x201D; one must cope with complex materials distributed 
                        throughout several distinct manuscripts. 
                        <span class="persName-JR">Ruskin</span> never completed the work, and we cannot know to what 
                        extent the surviving manuscripts represent the totality of versions that he 
                        conceived at any given stage of composition. The extant manuscripts do, however, 
                        appear representative of his evolving intentions. The compositional history is presented here, first, 
                        in the form of a summary <a href="#OVERVIEW">Overview</a>; 
                        and then the history is elaborated in separate accounts of the <a href="#DRAFT">Draft</a>,  
                        <a href="#FAIRCOPY">Fair Copy</a>, 
                        <a href="#PUBLICATION">Publication</a> histories, 
                        and the issue of <a href="#LINENUMBERING">Line Numbering</a>. The analysis is supported by two tables. 
                        <a href="APPARATUSTABLE1">Table 1</a> lists the sequential contents of each of the manuscripts and of the published versions of the work. 
                        <a href="APPARATUSTABLE2">Table 2</a> lists the sequential contents and planned extension of the final manuscript version.
            </p>
            
            <div id="OVERVIEW" class="subsection">
               <span class="head-subsection">Overview</span>
               
               <p>The evolution of the &#x201C;Account,&#x201D; culminated in edited 
                            publications of <span class="date-1834-11">November 1834</span>, <span class="date-1850">1850</span>, 
                            <span class="date-1891">1891</span>, and <span class="date-1902">1903</span>. 
                            In none of these did the editors make explicit the authority for their texts. In general, 
                            however, the successive published versions may be said steadily to approach closer to realizing 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s intentions for the latest of his manuscript 
                            versions&#x2014;the composite&#x2010;genre <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, along with 
                            his plan, never realized, for extending and completing the work. Although earlier versions became incorporated into the 
                            <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX version</a>, that final, composite&#x2010;genre form was not necessarily implied 
                            in earlier versions; and those earlier versions arguably show <span class="persName-JR">Ruskin</span> 
                            pursuing a distinct form of travelogue from what the work became.
               </p>
               
               <p>Here is a summary of the stages of composition and publication, as represented by surviving manuscripts.</p>
               
               <ul>
                  
                  <li>In the first stage of composition, as represented by two sheets later bound in 
                                <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a>, and designated in <i>ERM</i> as 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                                <span class="persName-JR">John</span> and <span class="persName-JJR">John James Ruskin</span> 
                                collaborated on an all&#x2010;verse version of the travelogue, relating the family&#x02BC;s initial stage of their journey, 
                                which took them through northern <span class="placeName-FRANCE">France</span> and <span class="placeName-BELGIUM">Belgium</span>. 
                                The manuscript contains poems entitled <a href="../showcase/calais_poem_msia_g1.php">&#x201C;Calais&#x201D;</a>, 
                                <a href="../showcase/cassel_poem_msia_g1.php">&#x201C;Cassel&#x201D;</a>, <a href="../showcase/lille_poem_msia_g1.php">&#x201C;Lille&#x201D;</a>, 
                                <a href="../showcase/brussels_poem_msia_g1.php">&#x201C;Brussels&#x201D;</a>, and <a href="../showcase/the_meuse_poem_msia_g1.php">&#x201C;The Meuse&#x201D;</a>. 
                                The father and son composed this version either during the <span class="date-1833-05-1833-09">May&#x2010;September 1833</span> journey itself, 
                                or following the family&#x02BC;s return home around <span class="date-1833-09-20">20 September</span> (see <a href="#DATE">Date</a>).
                  </li>
                  
                  <li>In a second stage of composition, <span class="persName-JR">Ruskin</span> reconceptualized and elaborated the work as a composite&#x2010;genre 
                                rather than as a solely verse travelogue. This stage is epitomized in brief by a folded sheet, 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>, 
                                which like <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a> was later bound in 
                                <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a>. Serving perhaps as a sample or an abstract of the fully developed composite&#x2010;genre work 
                                to come, <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a> contains a draft of the prose section, 
                                <a href="../showcase/calais_prose_msia.php" target="_blank" class="inactive">&#x201C;Calais&#x201D;</a>, along with draft of the poems,
                                <a href="../showcase/passing_the_alps_msia_g2.php" target="_blank" class="inactive">&#x201C;Passing the Alps&#x201D;</a>, 
                                <a href="../showcase/milan_cathedral_msia_g2.php" target="_blank" class="inactive">&#x201C;Milan Cathedral,&#x201D;</a>, 
                                <a href="../showcase/andernacht_poem_msia.php" target="_blank" class="inactive">&#x201C;Andernacht&#x201D;</a>, and 
                                <a href="../showcase/st_goar_poem_msia.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a>. 
                                <span class="persName-JR">Ruskin</span> continued to develop the composite&#x2010;genre version in draft that occupies a significant portion of 
                                <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> 
                                (see <a href="../apparatuses/msviii_apparatus.php#SECTIONB" target="_blank">MS VIII: Contents, section <i>b</i></a>).
                  </li>
                  
                  <li>The <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII draft</a> includes verse, prose, and some rough drawings, 
                                supplying text for the fair copy of the composite&#x2010;genre version that <span class="persName-JR">Ruskin</span> must by this time have had underway in 
                                <a href="../apparatuses/msix_apparatus.php" target="_blank" class="inactive">MS IX</a>. This draft relates mainly to the <span class="geogName-RHINE">Rhine</span> journey, 
                                the sighting and crossing of the <span class="geogName-ALPS">Alps</span>, the tour in northern <span class="placeName-ITALY">Italy</span>, 
                                and the tour of <span class="placeName-SWITZERLAND">Switzerland</span> 
                                (see <a href="APPARATUSTABLE1">Table 1</a> for complete contents). Near the beginning of this draft appear the prose sections for 
                                <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a>, suggestively dovetailing with the corresponding poems about those places drafted in 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msia_g2.php" target="_blank" class="inactive">MS IA, <i>g</i>.2</a>. 
                                Thus, when <span class="persName-JR">Ruskin</span> took up <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a> 
                                to continue his draft (probably around the start of <span class="date-1834-02">February 1834</span>), 
                                he was catching up with composition of prose sections to match with poetry about places in <span class="placeName-BELGIUM">Belgium</span> 
                                and <span class="geogName-GERMANY">Germany</span>, which he had already composed in the <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a> 
                                &#x201C;Account&#x201D; manuscripts. 
                                The next prose sections to appear in the <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII sequence of drafts</a>, 
                                following the prose for <a href="../showcase/andernacht_prose_msviii.php">&#x201C;Andernacht&#x201D;</a> and for <a href="../showcase/st_goar_prose_msviii.php">&#x201C;St. Goar&#x201D;</a>, 
                                are those for <a href="../showcase/the_meuse_prose_msviii.php">&#x201C;The Meuse&#x201D;</a>, 
                                <a href="../showcase/cologne_prose_msviii.php">&#x201C;Cologne&#x201D;</a>, and 
                                <a href="../showcase/brussels_prose_msviii.php" target="_blank" class="inactive">&#x201C;Brussels&#x201D;</a>, 
                                suggesting that by this point <span class="persName-JR">Ruskin</span> had fair&#x2010;copied the full composite sections of verse, prose, and drawing for 
                                &#x201C;Calais,&#x201D; &#x201C;Cassel,&#x201D; and &#x201C;Lille&#x201D; in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a>. 
                                Meanwhile, mixed in with this prose draft relating to northern <span class="placeName-EUROPE">Europe</span> in 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>, <span class="persName-JR">Ruskin</span> carried forward with poetry about the <span class="geogName-ALPS">Alps</span>
                                (see <a href="../apparatuses/msviii_apparatus.php#LISTB1" target="_blank">MS VIII: Contents, section <i>b</i>, list <i>b</i>.1</a>).
                  </li>
                  
                  <li>In <a href="../apparatuses/msix_apparatus.php" target="_blank" class="inactive">MS IX</a>, containing the elegant and elaborate 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">fair copy</a> of the composite&#x2010;genre version of the &#x201C;Account,&#x201D; 
                                <span class="persName-JR">Ruskin</span> mimicked the verse travelogues and prose landscape 
                                <a href="../notes/annuals_note.php" target="_blank">annuals</a> of the <span class="date-1830-1839">1830s</span>, 
                                which featured steel engravings after drawings by major landscape artists, and letterpress in verse or prose by writers 
                                who specialized in on&#x2010;demand travel writing, regardless of whether these writers had personally visited the places they described 
                                (see <a href="#DISCUSSION">Discussion</a>). 
                                <span class="persName-JR">Ruskin</span> organized the fair copy as composite groups, 
                                each typically consisting of a topographical poem about the place, a prose account of travel adventures, and related drawings, 
                                and each entitled according to major destinations along the course of the Ruskins&#x02BC; journey. 
                                In the drawings, <span class="persName-JR">Ruskin</span> either exactingly copied or imitated and adapted the manner of work by topographical and figure illustrators, 
                                which he knew from publications of the <span class="date-1830-1839">1830s</span>. His models included, most prominently, landscape vignettes by 
                                <a href="../notes/turner_j_m_w_note.php" target="_blank" class="inactive">J. M. W. Turner (1775&#x2013;1851)</a> and figure vigettes by 
                                <a href="../notes/stothard_thomas_note.php" target="_blank">Thomas Stothard (1755&#x2013;1834)</a> for 
                                the <span class="date-1830">1830</span> illustrated edition of <i>Italy</i> by 
                                <a href="../notes/samuel_rogers_note.php" target="_blank" class="inactive">Samuel Rogers (1763&#x2013;1855)</a>; 
                                and the lithographs of architectural scenes 
                                by <a href="../notes/prout_samuel_note.php" target="_blank">Samuel Prout (1783&#x2013;1852)</a> 
                                for the <span class="date-1833">1833</span> <i>Facsimiles of Sketches Made in Flanders and Germany</i>. 
                                But <span class="persName-JR">Ruskin</span> drew on other graphic sources, as well, or invented his own scenes.
                  </li>
                  
                  <li><span class="persName-JR">Ruskin</span> failed to complete the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>, 
                                but he outlined its planned extension in a 
                                <a href="../showcase/account_of_a_tour_on_the_continent_msviii_contents.php" target="_blank" class="inactive">List of Proposed Additional Contents for Account of a Tour on the Continent</a>, 
                                which he outlined on the back endpapers of <a href="../apparatuses/msviii_apparatus.php" target="_blank">MS VIII</a>. 
                                Several of these proposed additions are represented by poetry or prose 
                                sections that he had already drafted in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>, 
                                and some illustrations proposed in the list may correspond to drawings that form a brief gallery of Alpine scenes 
                                pasted onto otherwise blank pages at the end of the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a>. 
                                Draft writing and drawings for this proposed extension of the fair copy may have been lost, but much of the ambitious plan probably was never executed. 
                                A few other fugitive pieces of writing, probably intended for the &#x201C;Account&#x201D; and fair&#x2010;copied in hands other than <span class="persName-JR">Ruskin</span>&#x02BC;s, 
                                can be found in <a href="../apparatuses/msvii_apparatus.php" target="_blank" class="inactive">MS VII</a> and 
                                <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive">MS XI</a>.
                  </li>
                  
                  <li><span class="persName-JR">Ruskin</span> commenced his <a href="../showcase/account_of_a_tour_on_the_continent_msviii_contents.php" target="_blank" class="inactive">List of Proposed Additional Contents for Account of a Tour on the Continent</a> 
                                with entries for <span class="placeName-ANDERNACHT">Andernacht</span>, <span class="placeName-EHRENBREITSTEIN">Ehrenbreitstein</span>, <span class="placeName-STGOAR">St. Goar</span>, 
                                <span class="placeName-HEIDELBERG">Heidelberg</span>, and <span class="placeName-SCHAFFHAUSEN">Schaffhausen</span>&#x2014;sections 
                                for which he had drafted&#x2014;or was in process of drafting&#x2014;poems and prose in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>. 
                                Since this draft occurs shortly prior to and overlaps with draft of poetry for his <span class="persName-JJR">father</span>&#x02BC;s <span class="date-1834-05-10">10 May 1834</span> birthday, 
                                <span class="persName-JR">Ruskin</span> perhaps began compiling his list of proposed contents in <span class="date-1834-03-01-1834-04-30">March or April 1834</span>. 
                                The fair copy in <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a> breaks off in the middle of the section on 
                                <span class="placeName-HEIDELBERG">Heidelberg</span>, suggesting that <span class="persName-JR">Ruskin</span> ceased fair&#x2010;copying 
                                at about the same time that he stopped drafting in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a> and developed the projected list of contents. 
                                However, as indicated by a list for projected development of the project, 
                                <span class="persName-JR">Ruskin</span> did not so much abandon the &#x201C;Account&#x201D; 
                                as put the work on hold&#x2014;and the reason was not the distraction of a family tour following his 
                                <span class="persName-JJR">father</span>&#x02BC;s birthday, but a new major project. <span class="persName-JR">Ruskin</span> segued his achievement 
                                in the &#x201C;Account&#x201D; into an exciting commmission for the engraved gift annual, 
                                <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a>. 
                                Drawing directly on the text of the &#x201C;Account,&#x201D; he revised the poems 
                                <a href="../showcase/andernacht_poem_fo.php" target="_blank" class="inactive">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_fo.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a>, which were 
                                published under the collected title, <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>. 
                                Even more auspiciously, for the same volume of <i>Friendship&#x02BC;s Offering</i>, 
                                he drew on his self&#x2010;instruction in the materiality of illustrated travel publications of the 
                                <span class="date-1830-1839">1830s</span> to compose an ekphrastic, topographical poem, 
                                <a href="../apparatuses/saltzburg_apparatus.php" target="_blank">&#x201C;Saltzburg&#x201D;</a>, 
                                based on an engraving of that title. Both the picture by the artist, 
                                <a href="../notes/purser_william_note.php" target="_blank">William Purser (ca. 1790&#x2013;1852)</a>, and the poem presumably were commissioned by 
                                the editor, <a href="../notes/pringle_thomas_note.php" target="_blank">Thomas Pringle (1789&#x2013;1834)</a>, 
                                and were published in <a href="../bibiliography/bibliography.php#FRIENDSHIPSOFFERING1835" target="_blank" class="inactive"><i>Friendship&#x02BC;s Offering; and Winter&#x02BC;s Wreath . . . for MDCCCXXXV</i></a>. 
                                <span class="persName-JR">Ruskin</span>&#x02BC;s new poem, as well as his revised poems for the <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, 
                                must have been completed no later than <span class="date-1834-08">August 1834</span>, 
                                when <span class="persName-TP">Pringle</span>&#x02BC;s compilation was due to the printer in time for publication in <span class="date-1834-11">November 1834</span>.
                  </li>
                  
                  <li>In <span class="date-1850">1850</span>, <span class="persName-JJR">John James Ruskin</span> and <span class="persName-JR">Ruskin</span>&#x02BC;s editor, 
                                <a href="../notes/harrison_w_h_note.php" target="_blank" class="inactive">W. H. Harrison</a>, published 
                                <a href="../showcase/ehrenbreitstein_poem_1850.php" target="_blank" class="inactive">Ehrenbreitstein</a> in 
                                <a href="../notes/poems_1850_note.php" target="_blank" class="inactive">Ruskin, <i>Poems</i> (1850)</a>. 
                                As one reason for selecting this poem from among the unpublished verse for &#x201C;Account,&#x201D; 
                                the editors may have meant to repair a rift caused by the omission of this poem from <a href="../showcase/account_of_a_tour_on_the_continent_fo.php" target="_blank" class="inactive">&#x201C;Fragments from a Metrical Journal&#x201D;</a>, 
                                since, in the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX fair copy</a> of the &#x201C;Account,&#x201D; 
                                the poems &#x201C;Andernacht,&#x201D; &#x201C;Ehrenbreitstein,&#x201D; and &#x201C;St. Goar&#x201D; form a connected trio.
                  </li>
                  
                  <li>Almost six decades after <span class="persName-JR">Ruskin</span> started the &#x201C;Account,&#x201D; 
                                two versions of the work were edited for publication. One version, 
                                prepared by <span class="persName-WGC">W. G. Collingwood</span> 
                                for <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a>, 
                                consisted solely of verse extracts, in keeping with the parameters of <span class="persName-WGC">Collingwood</span>&#x02BC;s anthology. 
                                A second version, edited by <span class="persName-ETC">E. T. Cook</span> and 
                                <span class="persName-AW">Alexander Wedderburn</span> for the second volume (<span class="date-1903">1903</span>) of the 
                                <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a>, 
                                used <span class="persName-WGC">Collingwood</span>&#x02BC;s version as a base text, 
                                but folded in <span class="persName-JR">Ruskin</span>&#x02BC;s complementary prose sections 
                                from <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a> along with some additional verse 
                                that <span class="persName-WGC">Collingwood</span> had neglected in <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a>.
                  </li>
                  
                  <li>Both of these edited versions used the <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a> fair copy as main copytext, 
                                supplemented by draft from <a href="../showcase/account_of_a_tour_on_the_continent_msviii.php" target="_blank" class="inactive">MS VIII</a> in order to represent the full scope of the 
                                <span class="date-1833">1833</span> tour. Both versions also preferred <span class="persName-JR">Ruskin</span>&#x02BC;s revised texts of 
                                <a href="../showcase/andernacht_poem_fo.php" target="_blank" class="inactive">&#x201C;Andernacht&#x201D;</a> and 
                                <a href="../showcase/st_goar_poem_fo.php" target="_blank" class="inactive">&#x201C;St. Goar&#x201D;</a> 
                                as published in <a href="../notes/friendships_offering_note.php" target="_blank"><i>Friendship&#x02BC;s Offering</i></a>, 
                                although printing those poems&#x02BC; earlier versions in the notes. 
                                <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span>&#x02BC;s version was the first to reproduce a page 
                                from <a href="../showcase/account_of_a_tour_on_the_continent_msix.php" target="_blank" class="inactive">MS IX</a> exemplifying <span class="persName-JR">Ruskin</span>&#x02BC;s integration of text and picture. 
                                <span class="persName-WGC">Collingwood</span>&#x02BC;s edition reproduced sketches that <span class="persName-JR">Ruskin</span> made during the actual tour (these also reprinted in the Library Edition version). 
                                Thus, the Library Edition edition version comes closest to realizing <span class="persName-JR">Ruskin</span>&#x02BC;s intentions 
                                for the composite&#x2010;genre version of the &#x201C;Account,&#x201D; considered as a reflection of illustrated travelogues of the <span class="date-1830-1839">1830s</span>, 
                                whereas <span class="persName-WGC">Collingwood</span>&#x02BC;s version seems constructed as a biographical souvenir of the <span class="date-1833">1833</span> tour. 
                                <span class="persName-WGC">Collingwood</span> did not justify his editorial procedures for the &#x201C;Account,&#x201D; and he is unlikely to have derived textual authority 
                                for an all&#x2010;verse version from <a href="../showcase/account_of_a_tour_on_the_continent_msia_g1.php" target="_blank" class="inactive">MS IA, <i>g</i>.1</a>, 
                                which he appears not to have known (see gloss, line 2, <a href="../showcase/calais_poem_1891.php" target="_blank" class="inactive">&#x201C;Calais&#x201D;</a>). 
                                In light of <span class="persName-JR">Ruskin</span>&#x02BC;s dismissive remarks about the &#x201C;Account&#x201D; in chapter 4 (<span class="date-1885">1885</span>) of the autobiography, <i>Praeterita</i>, 
                                it is unlikely that the now elderly <span class="persName-JR">Ruskin</span> contributed to <span class="persName-WGC">Collingwood</span>&#x02BC;s editing, 
                                except by indirectly encouraging the biographical approach that underpins <span class="persName-WGC">Collingwood</span>&#x02BC;s procedure&#x2014;and 
                                even in this respect, the editor elaborated his own story about the Ruskins&#x02BC; <span class="date-1833">1833</span> Continental tour, 
                                as presented in his <span class="date-1894">1894</span> biography, <a href="../bibiliography/bibliography.php#WGCLIFEOFJR1893" target="_blank" class="inactive"><i>Life and Work of John Ruskin</i></a> 
                                (see <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35: 81; and see <a href="#DISCUSSION">Discussion</a>).
                  </li>
                  
               </ul>
               
