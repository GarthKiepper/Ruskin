         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <div id="" class="subsection">
               <span class="head-subsection">Occasion</span>
               
               <p>The poem reflects a visit to <span class="placeName-RAGLAN">Raglan Castle</span> near <span class="placeName-MONMOUTH">Monmouth</span>, 
                        constructed largely in the <span class="date-1400-1499">fifteenth century</span> and significantly damaged during the <span class="date-1642-1651">Civil War</span>. 
                        The poem, along with references
                        in <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span></a> 
                        to <span class="placeName-MONMOUTH">Monmouth</span> and <span class="placeName-TINTERN">Tintern Abbey</span>,
                        confirms that the Ruskins visited the <span class="geogName-WYE">Wye Valley</span> in <span class="date-1827">1827</span>. 
                        The family could have departed no earlier than when <span class="persName-JJR">John James Ruskin</span> completed his business travel, 
                        a trip prolonged beyond his <span class="date-1827-05-10">10 May birthday</span> through at least the week of <span class="date-1827-05-14-1827-05-20">14&#x2013;20 May 1827</span> 
                        (see <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 157 n. 1; 
                        see also <a href="../apparatuses/wales_apparatus.php" target="_blank"><span class="title-poem-WALES">&#x201C;Wales&#x201D;</span></a> and 
                        <a href="../apparatuses/spring_blank_verse_apparatus.php" target="_blank"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>).
               </p>
               
               <p>By <span class="date-1827">1827</span>, the <span class="geogName-WYE">Wye</span> tour had sponsored a tourism industry of nearly three&#x2010;quarters of a century&#x02BC;s standing, 
                            and in the <span class="date-1820-1829">1820s</span> the comforts and efficiency of the tour were augmented 
                        by steam transport on the river and by improved roads on land 
                        (see <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>).
                        For the excursion from <span class="placeName-MONMOUTH">Monmouth</span> to <span class="placeName-RAGLAN">Raglan</span> in particular, 
                            the local guidebook author and printer, <span class="persName-CHMONMOUTH">Charles Heath</span>, highlighted the commodiousness of the 
                            &#x201C;new road from <span class="placeName-MONMOUTH">Monmouth</span> to <span class="placeName-RAGLAN">Ragland</span>, a pleasant drive of eight miles,&#x201D; 
                            in his eleventh (<span class="date-1829">1829</span>) edition of 
                        <a href="../bibiliography/bibliography.php#HEATHRAGLANDCASTLE" target="_blank" class="inactive"><i>Historical and Descriptive Accounts of the Ancient and Present State of Ragland Castle</i></a> (n.p.), 
                            although further research is required to determine in what year <span class="persName-CHMONMOUTH">Heath</span> first printed this notice.
               </p>
               
               <p>As indicated by other poems in the anthology,
                        <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>, 
                        the Ruskins probably traveled on from southern <span class="placeName-WALES">Wales</span> to visit
                        family in <a href="../notes/perth_note.php" target="_blank" class="inactive"><span class="placeName-PERTH">Perth</span></a>, <span class="placeName-SCOTLAND">Scotland</span>
                        (see <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>).
               </p>
               
            </div>
            
            <div id="" class="subsection">
               <span class="head-subsection">Sources</span>
               
               <p>With its trope of a mouse&#x02BC;s house reflecting in miniature a human scale of awareness of and provision for danger, 
                            the poem is related to <span class="persName-JR">Ruskin</span>&#x02BC;s adaptation 
                            of poems, tales, and lessons by <span class="persName-WCP">William Cowper</span>, <span class="persName-ALB">Anna Letitia Barbauld</span>, and 
                            <span class="persName-JAIKIN">John Aikin</span> in <a href="../apparatuses/the_needless_alarm_apparatus.php" target="_blank"><span class="title-poem-THENEEDLESSALARM">&#x201C;The Needless Alarm&#x201D;</span></a> 
                            (see especially, for that poem, <a href="../apparatuses/the_needless_alarm_apparatus.php#SOURCES" target="_blank">Discussion: Sources</a>). 
                            Also, contextualizing both the poem and the anthology that the poem heads, <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>, 
                            is the tradition of picturesque travel, writing, and art. Comically, however, <span class="persName-JR">Ruskin</span>&#x02BC;s narrator reduces the elements of the 
                            picturesque to a mere catalogue of the castle&#x02BC;s architectural features, while the most picturesque effect seems achieved by the mice, 
                            which patch their &#x201C;falling dwellings&#x201D; with vegetation contrasting pleasingly with the stone of the &#x201C;spiral towers.&#x201D;
               </p>
               
               <p> contains a drawing, 
                            <a href="../drawings/ragland_castle_drawing_msiii.jpg" target="_blank" class="inactive">&#x201C;Ragland Castle When Newly Built&#x201D;</a>, which was probably suggested by 
                            <span class="persName-CHMONMOUTH">Heath</span>&#x02BC;s guidebook, 
                            <a href="../bibiliography/bibliography.php#HEATHRAGLANDCASTLE" target="_blank" class="inactive"><i>Historical and Descriptive Accounts of the Ancient and Present State of Ragland Castle</i></a>. 
                            See <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>.
               </p>
               
            </div>
            
