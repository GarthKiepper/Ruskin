         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p><span class="title-poem-THEHILLOFKINNOUL">&#x201C;The Hill of Kinnoul&#x201D;</span> shares with other poems, such as 
                        <a href="../apparatuses/look_at_that_ship_apparatus.php" target="_blank"><span class="title-poem-THESHIP">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at that Ship&#x201D; &#x005B;1827&#x005D;</span></a>, 
                        an interest in multiple perspectives of viewing. In this case, the multiple perspectives were encouraged by guidebook advice for viewing this
                        prominent landscape feature in <span class="placeName-PERTH">Perth</span>. See the <a href="../notes/hill_of_kinnoull_place_note.php" target="_blank">Hill of Kinnoull (place)</a>
                        for <span class="date-1800-01-01-1899-12-31">nineteenth&#x2010;century</span> guidebook advice for views, taken of the <span class="geogName-KINNOULLHL">hill</span> from below and of the
                        prospects from above, just as <span class="persName-JR">Ruskin</span>&#x02BC;s poem is interested in &#x201C;above below&#x201D; (l. 5).
            </p>
            
