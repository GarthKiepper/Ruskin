      <p>What is it that makes the very heart leap within you at the sight of a 
                    hill&#x02BC;s blue outline; that so ætherializes the soul and ennobles the spirit; 
                    that so raises you from the earth and from aught of the earth? Is it 
                    their apparent proximity to the blue heaven&#x02BC;s inaccessibleness? is it the 
                    humbling sense of your own littleness or the immoveable, unchangeable 
                    magnificence of that which has seen the beginning of the world and will see<div id="" class="pb"></div>
                    its end, or is it that the thoughts range insensibly from the things created 
                    to Him who created them? I know not. How it thrilled through me, 
                    when first, far away, across the lake&#x2010;like swell of the deep waters of that 
                    wondrous river, rose the cloudy outline of the blue mountains. Long time 
                    hath past over me since I saw the swell of a blue hill. I have longed for 
                    them&#x2014;I have yearned for them as an exile yearns for his native land, and 
                    I am with them.</p>
      
