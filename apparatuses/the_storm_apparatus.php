         <div id="COMPPUB" class="section">
            <span class="head-section">Composition and Publication</span>
            
            <p>Previously unpublished.</p>
            
            <p>Two strophes on the subject of a storm appear in succession, separated by a 
                        horizontal line. Such lines separate the stanzas also in 
                        <a href="../apparatuses/glen_of_glenfarg_apparatus.php" target="_blank"><span class="title-poem-GLENOFGLENFARG">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</span></a>, 
                        another poem in <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>, 
                        but in that case the stanzas clearly flow continuously from one to the next. In <span class="title-poem-THESTORM">&#x201C;The storm&#x201D;</span>, 
                        strophe (<i>b</i>) appears to start the poem anew and revise (<i>a</i>); and as an indication that <span class="persName-JR">Ruskin</span> 
                        meant strophe (<i>b</i>) to replace (<i>a</i>), he wrote the word 
                        &#x201C;bad&#x201D; on the same line as the runover of the final line of (<i>a</i>), as if declaring that strophe rejected. Little
                        time appears to have elapsed between this decision and the composition of (<i>b</i>), the
                        hand remaining consistent; and the immediacy of this revision perhaps explains why <span class="persName-JR">Ruskin</span> did not repeat the title, 
                        as if considering the original title to remain in play.
            </p>
            
            <p>While the meaning of <i>bad</i> may be open to interpretation, I have marked it as a metamark&#x2014;not a part of the poem 
                        itself, but as disapproving comment on strophe (<i>a</i>&#x005D;),
                        prompting the composition of (<i>b</i>) as a replacement. The most obvious evidence
                        for this interpretation is that the runover line becomes jibberish (&#x201C;light bad nings&#x201D;) if one treats <i>bad</i> as part of the line itself. 
                        Other evidence for treating the word as a separable metamark is the
                        physical alignment of the runovers of the final two lines, which, if  is omitted, appears in keeping with that of other poems
                        in the anthology.
            </p>
            
         </div>
         
         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>If <i>bad</i> is read as a judgment on strophe (<i>a</i>),
                        one quality that may explain the rejection is the violence of the woman&#x2019;s
                        death in that version of the poem. In other very early work, there is evidence of <span class="persName-JR">Ruskin</span>&#x02BC;s avoiding expression of 
                        violence. For example, among <a href="../notes/boyhood_books_note.php" target="_blank" class="inactive">Ruskin&#x02BC;s boyhood books</a> is his copy of 
                        <a href="../bibiliography/bibliography.php#DAYHISORY" target="_blank" class="inactive"><span class="title-novel-DAYHISTORY-italic">The History of Little Jack</span></a> 
                        (<span class="date-1820">1820</span>) by <a href="../notes/day_thomas_note.php" target="_blank" class="inactive">Thomas Day (1748&#x2013;89)</a>
                        (<a href="../bibiliography/bibliography.php#DEARDENRUSKINLIBRARY" target="_blank" class="inactive">Dearden, <i>Library of John Ruskin</i></a>, no. 690), 
                        in which <span class="persName-JR">Ruskin</span> traced outlines of the scenes depicted in the plates, using the plates&#x02BC; blank versos&#x2014;all of the plates, 
                        that is, except one that depicts two boys fighting. Another early example of apprehension of conflict is the poem, 
                        <a href="../apparatuses/the_defiance_of_war_msi_apparatus.php" target="_blank" class="inactive"><span class="title-poem-THEDEFIANCEOFWAR">&#x201C;The Defiance of War&#x201D;&#x003E;</span></a>.
            </p>
            
