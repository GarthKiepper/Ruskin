            <span class="head-section">Title</span>
            
            <p>&#x201C;The Storm&#x201D;</p>
            
            <p><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-THESTORM">&#x201C;The storm&#x201D;</span>. In manuscript, 
                        the title serves for what read as two distinct versions of this poem, rather than as two strophes of a single poem. 
                        The two parts do not appear to form a single work in the usual sense (see
                        <a href="#COMPPUB">Composition and Publication</a>, although <span class="persName-JR">Ruskin</span> did not repeat the title, 
                        but only drew a line between the strophes. Rather, the second part or strophe seems to start the poem anew, as if as a second version; the two parts 
                        are designated editorially as (<i>a</i>) and (<i>b</i>).
            </p>
            
            <p>See also <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
