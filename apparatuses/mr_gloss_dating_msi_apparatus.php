            <span class="head-section">Manuscripts</span>
            
            <p><a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a> (p. 103), a <a href="../notes/red_book_note.php" target="_blank">Red Book</a> devoted
                    primarily to <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>. The gloss is inserted in the 
                    <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a> between the fourth poem in the anthology, 
                    <a href="../apparatuses/the_needless_alarm_apparatus.php" target="_blank">&#x201C;The Needless Alarm,&#x201D;</a> and the fifth poem,
                    <a href="../apparatuses/on_papas_leaving_home_apparatus.php" target="_blank">&#x201C;On Papa&#x02BC;s Leaving Home.&#x201D;</a></p>
            
            <p>Facsimile and transcript by permission of Beinecke Rare Book and Manuscript Library, Yale University.</p>
            
