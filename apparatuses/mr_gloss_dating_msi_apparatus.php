         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1826-01-1827-01">January 1826&#x2013;January 1827</span>.
            </p>
            
            <p>At the end of &#x201C;The Needless Alarm,&#x201D; <a href="../notes/ruskin_margaret_note.php" target="_blank" class="inactive"><span class="persName-MR">Margaret Ruskin</span></a>
                    wrote &#x201C;<span class="date-1826-01">Jan<sup>y</sup> 1826</span>.&#x201D; Immediately below this, she drew a horizontal
                    rule, followed by &#x201C;this book begun about <span class="date-1826-09-1826-10">Sep<sup>t</sup>
                    or Oct 1826</span> / finished about <span class="date-1827-01">Jan<sup>y</sup> 1827</span>.&#x201D;
            </p>
            
            <p>Clearly, &#x201C;this book&#x201D; refers to the whole of <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a>; and, therefore, 
                    <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> took the preceding date,
                    &#x201C;<span class="date-1826-01">Jan<sup>y</sup> 1826</span>,&#x201D; as applying to &#x201C;The Needless Alarm&#x201D; in
                    particular and thus as identifying <span class="persName-JR">Ruskin</span>&#x02BC;s earliest dated verse known to
                    that editor (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>,
                    1:xxii; <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:viii).
            </p>
            
            <p>In the <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a>, 
                    <a href="../notes/cook_e_t_note.php" target="_blank" class="inactive"><span class="persName-ETC">E. T. Cook</span></a> and 
                    <a href="../notes/wedderburn_note.php" target="_blank" class="inactive"><span class="persName-AW">Alexander Wedderburn</span></a>
                    disagreed with <span class="persName-WGC">Collingwood</span>&#x02BC;s interpretation, ascribing the status of the earliest dated verse possibly to 
                    <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a>, 
                    which is &#x201C;poem I&#x201D; in the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a>:
                    &#x201C;there is nothing to show,&#x201D; they wrote, &#x201C;that &#x005B;<span class="persName-MR">Margaret</span>&#x02BC;s date&#x005D; does not apply equally to all four pieces, composed
                    presumably in the order in which they are placed in the book.&#x201D; (They also pointed to another possible contendor,
                    besides <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a>, 
                    for the distinction of earliest poem&#x2014;<a href="../apparatuses/ragland_castle_apparatus.php" target="_blank">&#x201C;Ragland Castle,&#x201D;</a> which is the first poem in another
                    anthology, <a href="../notes/poetry_discriptive_apparatus.php" target="_blank">Poetry Descriptive</a>, in <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a>, 
                    and which they date &#x201C;as early as these&#x201D; poems in <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a> 
                    &#x005B;<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:255 n. 1; 
                    see <a href="../apparatuses/wales_apparatus.php#DATE" target="_blank">&#x201C;Wales&#x201D;: Date</a>&#x005D;.)
            </p>
            
            <p>Putting aside <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span>&#x02BC;s faulty assumptions about the composition of the poems in the anthology
                    (i.e., that <span class="persName-MR">Margaret</span>&#x02BC;s note necessarily refers to all
                    four poems preceding the note, not just to &#x201C;The Needless Alarm&#x201D;; and that the first poem of this group
                    to be fair&#x2010;copied, <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a> 
                    or &#x201C;poem I,&#x201D; must necessarily have been the first composed), their interpretation of what <span class="persName-MR">Margaret</span>&#x02BC;s date,
                    &#x201C;<span class="date-1826-01">Jan<sup>y</sup> 1826</span>,&#x201D; does signify seems hardly credible: the date, according to the editors, &#x201C;seems to be the date of the note, and not of any one of the verses in
                    particular.&#x201D; That is, in their view, &#x201C;<span class="date-1826-01">Jan 1826</span>&#x201D; applies to
                    the note that follows, &#x201C;this book begun about <span class="date-1826-09-1826-10">Sep<sup>t</sup> or Oct 1826</span> / finished about <span class="date-1827-01">Jan<sup>y</sup> 1827</span>.&#x201D;
                    The obvious discrepancy between the alleged date <i>of</i> the note and the date <i>in</i> the note is explained, further, 
                    as the writer&#x02BC;s mistake: &#x201C;perhaps, writing at the beginning of a new year, <span class="persName-MR">Mrs. Ruskin</span> made the common error of not altering the
                    old year&#x02BC;s date&#x201D;&#x2014;writing, that is, &#x201C;<span class="date-1826-01">Jan<sup>y</sup> 1826</span>,&#x201D; but intending
                    &#x201C;<span class="date-1827-01">Jan<sup>y</sup> 1827</span>&#x201D; (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:255 n. 1).
            </p>
            
            <p>Since Cook and Wedderburn&#x02BC;s reasoning depends on overruling what <span class="persName-MR">Margaret</span> actually wrote, <span class="persName-WGC">Collingwood</span>&#x02BC;s interpretation seems certainly more
                    straightforward and surely at least as convincing&#x2014;namely, that her first date applies to &#x201C;The Needless Alarm,&#x201D; but not necessarily to all four
                    of the poems preceding the note. While &#x201C;The Needless Alarm&#x201D; must have been fair&#x2010;copied &#x201C;about <span class="date-1827-01">Jan<sup>y</sup>
                        1827</span>&#x201D; along with the other verse at the end of <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a>, <span class="persName-MR">Margaret</span> would have known if <span class="persName-JR">Ruskin</span> had composed
                    the poem a year earlier than the others. There is, moreover, another instance of <span class="persName-MR">Margaret</span>&#x02BC;s glossing a poem with a date earlier than surrounding 
                    poems&#x2014;<a href="../apparatuses/2.php#DATE" target="_blank" class="inactive">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</a>, 
                    in <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a>&#x2014;and, in that case, <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> do accept <span class="persName-MR">Margaret</span>&#x02BC;s authority at face value. Finally, it seems
                    implausible that <span class="persName-MR">Margaret</span> would have written <span class="date-1826">1826</span> for <span class="date-1827">1827</span> without noticing the error, since her note about the date of
                    the whole of <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a> (which is unquestioned) is placed immediately below.
            </p>
            
            <p>Without following <span class="persName-ETC">Cook</span> and <span class="persName-AW">Wedderburn</span> in their argument for dating &#x201C;The Needless Alarm&#x201D; some time after 
                    <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a>, 
                    one might, however, accept their suggestion that <span class="persName-MR">Margaret</span> intends &#x201C;<span class="date-1826-01">Jan<sup>y</sup> 1826</span>&#x201D; to refer to all four poems
                    preceding her note. This possibility is entertained in dating these poems.
            </p>
            
