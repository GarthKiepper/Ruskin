         <div id="MAJORMS" class="section">
            <span class="head-section">Manuscripts</span>
            
            <p><a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> (pp. 31&#x2013;32), 
                    a <a href="../notes/red_book_note.php" target="_blank">Red Book</a> 
                    devoted initially to <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span></a>. 
                    <span class="title-poem-JRTHESHIP1827">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at That Ship&#x201D; &#x005B;1827&#x005D;</span> 
                    is the fifth poem in the <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive"><span class="title-anthology-">MS III Second Poetry Anthology</span></a>. 
                    Another copy&#x2014;a 20 by 13 cm single sheet, pencil manuscript&#x2014;is bound in <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSXI">MS XI</span></a>.
            </p>
            
            <p>Facsimiles and transcripts by permission of Beinecke Rare Book and Manuscript Library, Yale University.</p>
            
            <p>The inclusion of the latter manuscript in <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSXI">MS XI</span></a> 
                    suggests that someone interpreted the sheet as originally having been enclosed in a letter or as having
                    formed a kind of epistolary communication in itself&#x2014;such as the <a href="../notes/new_years_poems_note.php" target="_blank" class="inactive">New Year&#x02BC;s Poem</a> presentation copies, 
                    which are bound in <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSXI">MS XI</span></a>. 
                    The only evidence qualifying this manuscript as a communication of this kind are two horizontal folds of the sheet; nothing on the
                    reverse, in <span class="persName-JR">Ruskin</span>&#x02BC;s <i>own</i> hand, indicates such an intention.
            </p>
            
         </div>
         
         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1827-02-1827-03">February or March 1827</span>. <span class="persName-JR">Ruskin</span> dated the <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSXI">MS XI</span></a> 
                    witness as &#x201C;<span class="date-1827-02-1827-03"><span class="sic">febuary &#x005B;sic&#x005D;</span> or march 1827</span>.&#x201D; On the reverse, another
                    hand wrote &#x201C;<span class="date-1827-02-16">16 Feb<sup>y</sup> 1827</span>.&#x201D; Adjoining the latter date is written an obscure notation, 
                    &#x201C;No &#x005B;<i>or</i> Mo&#x005D; 6817.&#x201D;
            </p>
            
            <p><span class="persName-JR">Ruskin</span>&#x02BC;s <span class="date-1827-02-1827-03">two&#x2010;month scope</span> for dating his own manuscript 
                    suggests uncertainty, whether about a date of composition prior to this fair copy or about the 
                    date of the fair copy itself. The date does not appear to be in a hand different from that responsible for the manuscript as a whole, 
                    as if <span class="persName-JR">Ruskin</span> returned later to add the date to the manuscript; and consistently with the text, he wrote the date in pencil, 
                    rather than with pen and ink, which he had learned to use by the time he fair&#x2010;copied the poem in the 
                    <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive"><span class="title-anthology-">MS III Second Poetry Anthology</span></a> (see 
                    <a href="../notes/hand_note.php" target="_blank"><span class="persName-JR">Ruskin</span>&#x02BC;s Handwriting</a>). 
                    (He did, however, possibly <i>erase</i> material in the 
                    <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSXI">MS XI</span></a> version when making the later fair copy; see 
                    ). It
                    seems likelier&#x2014;particularly if the date on the reverse is correct&#x2014;that he was merely imitating <span class="persName-MR">his mother</span>&#x02BC;s tentative manner
                    of dating in her notation of <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>, 
                    &#x201C;this book begun about <span class="date-1826-09-1826-10">Sep<sup>t</sup> or Oct 1826</span> / finished about <span class="date-1827-01">Jan<sup>y</sup> 1827</span>,&#x201D; 
                    a notation that she had written in that manuscript probably only a few weeks earlier.
            </p>
            
            <p>If this hypothesis is correct, and <span class="persName-JR">Ruskin</span> 
                    was imitating <span class="persName-MR">his mother</span>&#x02BC;s dating of <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>, 
                    then the <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSXI">MS XI</span></a> witness of 
                    <span class="title-poem-JRTHESHIP1827">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at That Ship&#x201D; &#x005B;1827&#x005D;</span> 
                    may be contemporaneous with the final items that <span class="persName-JR">Ruskin</span> inserted in <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>&#x2014;that is, 
                    those items that may have chronologically followed <span class="persName-MR">Margaret</span>&#x02BC;s inscription. This convergence is especially notable respecting the drawing on the inside of the back endboard of 
                    <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>, 
                    <a href="../drawings/heights_of_wisdom_drawing_msi.jpg" target="_blank" class="inactive">&#x201C;Heights of Wisdom, Depth of Fools&#x201D;</a>, which <span class="persName-JR">Ruskin</span> 
                    dated <span class="date-1827-03-21">21 March</span> (provided that date refers to <span class="date-1827">1827</span>, and not <span class="date-1826">1826</span>). 
                    The convergence may also apply to the poems that are physically situated following Margaret's dating notation amid the items making up 
                    <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>&#x2014;namely, 
                    <a href="../apparatuses/on_papas_leaving_home_apparatus.php" target="_blank"><span class="title-poem-ONPAPASLEAVINGHOME">&#x201C;On Papa&#x02BC;s Leaving Home&#x201D;</span></a> and 
                    <a href="../apparatuses/on_the_rainbow_apparatus.php" target="_blank"><span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span></a>.
            </p>
            
            <p>See <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
