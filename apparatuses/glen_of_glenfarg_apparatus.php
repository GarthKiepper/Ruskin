            <p>In <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a>, <span class="title-poem-GLENOFGLENFARG">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;</span>) 
                        is the seventh poem in <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>.
                        Since this anthology was compiled in <span class="date-1827">1827</span>, the <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> witness
                        may represent a revision or copy of a witness, now lost, that was composed in or around <span class="date-1826-09">September 1826</span>
                        (<a href="#DATE">see Date</a>). The existence of a prior witness may be indicated by <span class="persName-JR">Ruskin</span>&#x02BC;s having dropped a
                        rhyme word in the <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> witness (see Textual Note).
            </p>
            
         </div>
         
         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1826-09">September 1826</span> (or possibly sometime between about <span class="date-1827-07-01-1827-12-31">July and December 1827</span>).
            </p>
            
            <p>At the bottom of the first page in <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> containing this poem (it continues onto the
                        next page), <a href="../notes/ruskin_margaret_note.php" target="_blank" class="inactive"><span class="persName-MR">Margaret Ruskin</span></a> wrote
                        &#x201C;<span class="date-1826-09">Sep<sup>t</sup> 1826</span>&#x201D; (in the <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a>,
                        the hand is incorrectly attributed to <a href="../notes/ruskin_john_james_note.php" target="_blank" class="inactive"><span class="persName-JJR">John James Ruskin</span></a>
                        &#x005B;<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:257n&#x005D;). The date
                        appears to refer to this poem in particular, rather than to the entire group of poems to which it belongs,
                        <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>, a group that dates from a year
                        later, <span class="date-1827-07-01-1827-12-31">the second half of 1827</span> (see <a href="../apparatuses/wales_apparatus.php#DATE" target="_blank"><span class="title-poem-WALES">&#x201C;Wales&#x201D;</span>: Date</a>; and 
                        <a href="../notes/tours_of_1826-27_note#WALESANDSCOTLAND.php" target="_blank" class="inactive">Tours of 1826&#x2013;27: Wales and Scotland, 1827</a>; for
                        another case in which <span class="persName-JR">Ruskin</span>&#x02BC;s parents probably dated a poem separately from the group to which it belongs, see
                        <a href="../apparatuses/the_needless_alarm_apparatus.php#DATE" target="_blank"><span class="title-poem-THENEEDLESSALARM">&#x201C;The Needless Alarm&#x201D;</span>: Date</a>).
            </p>
            
            <p><a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a>
                        believed that <span class="title-poem-GLENOFGLENFARG">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</span> was inspired by the
                        Ruskins&#x02BC; passing through <a href="../notes/glenfarg_place_note.php" target="_blank"><span class="geogName-GLENFARG">Glenfarg</span></a>,
                        in the course of a visit to the family of <span class="persName-JJR">John James</span>&#x02BC;s sister, <a href="../notes/richardson_jessie_note.php" target="_blank" class="inactive"><span class="persName-JRP">Jessie Richardson</span></a>, in
                        <a href="../notes/perth_note.php" target="_blank" class="inactive"><span class="placeName-PERTH">Perth</span></a>,
                        <span class="placeName-SCOTLAND">Scotland</span>. <span class="persName-WGC">Collingwood</span> assumed they would have started in <span class="date-1826-05-10-1826-05-20">mid-May</span>, as became usual in later years,
                        and he reasoned, therefore, that <span class="persName-JR">Ruskin</span> must have composed a poem dated <span class="date-1826-09">September 1826</span> &#x201C;during the journey home&#x201D;
                        from this visit (although <span class="persName-WGC">Collingwood</span> left open the possibility that the family also made an <span class="date-1826-09-22-1826-12-21">autumn</span> visit, separate from and
                        in addition to the <span class="date-1826-03-20-1826-06-21">spring</span> journey &#x005B;<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> (4<sup>o</sup>, 1891)</a>, 1:xxiii;
                        <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> (8<sup>o</sup>, 1891)</a>, 1:x&#x005D;).
                        We now know that, in <span class="date-1826">1826</span>, owing to a &#x201C;barren year&#x201D; in business, <span class="persName-JJR">John James</span> was kept away from
                        <a href="../notes/herne_hill_note.php" target="_blank" class="inactive"><span class="placeName-HERNE">Herne Hill</span></a> by unusually lengthy business
                        travel until well after his <span class="date-1826-05-10">May birthday</span>, and the family holiday was consequently delayed
                        (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 1:137, 149, 150 n. 5). Also,
                        any such family journey would have been delayed, and even prevented, owing to the illness and <span class="date-1827-01-01-1827-03-20">death</span> by consumption of
                        <span class="persName-JRP">Ruskin&#x02BC;s cousin</span>, <a href="../notes/richardson_james_note.php" target="_blank" class="inactive"><span class="persName-JRP">James Richardson</span></a>, which did cause <span class="persName-JR">Ruskin</span>&#x02BC;s parents to visit <span class="placeName-SCOTLAND">Scotland</span> in <span class="date-1826-03">April of that year</span>, but it
                        is unclear whether <span class="persName-JR">Ruskin</span> accompanied them (<a href="#DISCUSSION">see Discussion</a>; and
                        <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>). Of course, even
                        if <span class="persName-JR">Ruskin</span> himself did not travel to <span class="placeName-SCOTLAND">Scotland</span> in <span class="date-1826">1826</span>, nothing prevents his having written in <span class="date-1826-09">September</span> about
                        <a href="../notes/glenfarg_place_note.php" target="_blank"><span class="geogName-GLENFARG">Glenfarg</span></a>.
            </p>
            
            <p>There is always the possibility that <span class="persName-MR">Margaret</span> was simply mistaken in dating the poem <span class="date-1826-09">September 1826</span>, and that it
                        belongs to <span class="date-1827-09-22-1827-12-31">fall/winter 1827</span>&#x2014;along with the other poem in <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> about
                        <a href="../notes/glenfarg_place_note.php" target="_blank"><span class="geogName-GLENFARG">Glenfarg</span></a>,
                        <a href="../apparatuses/glenfarg_apparatus.php" target="_blank" class="inactive"><span class="title-poem-GLENFARG">&#x201C;Glenfarg&#x201D; <i>and</i> &#x201C;Glen of Glenfarg&#x201D; (&#x201C;Papa how pretty those icicles are&#x201D;)</span></a>, which exists in a firmly dated <span class="date-1828-01-01">New Year&#x02BC;s 1828</span>
                        witness. But no evidence shows that <span class="persName-MR">Margaret</span> intended other than what she wrote.
            </p>
            
            <p>See <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
            </p>
            
