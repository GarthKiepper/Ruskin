         <div id="TITLE" class="section">
            <span class="head-section">Title</span>
            
            <p><span class="title-poem-GLENOFGLENFARG">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</span></p>
            
            <p><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-GLENOFGLENFARG">&#x201C;glen of glenfarg&#x201D;</span>. He gave the same title to a poem of <span class="date-1828">1828</span>:
                        <a href="../apparatuses/glenfarg_apparatus.php" target="_blank" class="inactive"><span class="title-poem-GLENFARG">&#x201C;Glenfarg&#x201D; <i>and</i> &#x201C;Glen of Glenfarg&#x201D; (&#x201C;Papa how pretty those icicles are&#x201D;)</span></a>.
            </p>
            
            <p>See <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
         </div>
         
         <div id="GENRE" class="section">
            <span class="head-section">Genre</span>
            
            <p>Poem; <a href="../notes/topographical_poetry_note.php" target="_blank" class="inactive">topographical poem</a>. 
                        Four&#x2010;line stanza, tetrameter, rhyme abab.
            </p>
            
            <p>See <a href="#DISCUSSION">Discussion</a> for possible connection with travel.
            </p>
            
         </div>
         
