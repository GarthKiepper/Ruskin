         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>One cannot assume that <span class="persName-JR">Ruskin</span> based the poem on eyewitness experience of travel to <span class="placeName-SCOTLAND">Scotland</span>, 
                        as the poem itself forms one of the very few scraps of evidence that he took such a journey to <span class="placeName-NBRITAIN">North Britain</span> with his parents prior to <span class="date-1827">1827</span>; see
                        <a href="../notes/tours_of_1822-24_note.php" target="_blank">Tours of 1822&#x2013;24</a>, and
                        <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>. Tending to support such an assumption, however, 
                        is the comparative obscurity of the valley of <a href="../notes/glenfarg_place_note.php" target="_blank"><span class="geogName-GLENFARG">Glenfarg</span></a> 
                        as a topic. Personal experience seems more likely to have sponsored the theme than cultural or historical interest.
            </p>
            
            <p>For the characterization of the <span class="geogName-GLENFARG">valley</span> in the travel literature of the period, see <a href="../notes/glenfarg_place_note.php" target="_blank">Glenfarg (Place)</a>.
                        In keeping with the georgic character of <span class="persName-JR">Ruskin</span>&#x02BC;s poem, the <span class="geogName-GLENFARG">valley</span> was associated with gentle and domestic hill scenery, in contrast with sublime experience of <span class="geogName-SCOTTISHHIGHLANDS">Highland Scotland</span>. See
                        also <a href="../apparatuses/poetry_discriptive_apparatus.php#DISCUSSION" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span>: Discussion</a></p>
            
