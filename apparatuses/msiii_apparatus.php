         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <div id="HAND" class="subsection">
               <span class="head-subsection">Hand</span>
               
               <p>MS III exhibits <span class="persName-JR">Ruskin</span>&#x02BC;s fledgling use of pen and ink, first taken
                            up in <span class="date-1827-04">April 1827</span>, as compared with the earlier <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a> and 
                            <a href="../apparatuses/msiva_apparatus.php" target="_blank" class="inactive">MS IVA</a>, which he wrote entirely in pencil. Perhaps, it was probably to practice
                            printing with pen and ink that he covered the inside back endboard of MS III with random words and letters.
               </p>
               
               <p>To print <a href="../apparatuses/harry_and_lucy_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2</a> and 
                            <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;poetry discriptive&#x201D;</span></a>, 
                            <span class="persName-JR">Ruskin</span> copied his letters in ink over top of pencil, which remains visible beneath. He
                            continued to rely on pencil for the drawings in MS III, but not for their legends, which he printed in ink.
               </p>
               
            </div>
            
            <div id="COLLATION2" class="subsection">
               <span class="head-subsection">Collation</span>
               
               <p>As <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> deduced,
                        <span class="persName-JR">Ruskin</span> worked in differing parts of MS III at widely differing times. A
                        rough chronology of his use of this <a href="../notes/red_book_note.php" target="_blank">Red Book</a> can
                        be sorted out as follows, incorporating but revising and elaborating on reconstructions by <span class="persName-WGC">W. G. Collingwood</span> 
                        (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>,1:262; 
                        <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:263; 
                        and <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:530) and by 
                        <span class="persName-HGV">Helen Gill Viljoen</span> 
                        (&#x201C;Dating MSS. of Boyhood,&#x201D; <a href="../bibiliography/bibliography.php#VILJOENPAPERS" target="_blank" class="inactive">Viljoen Papers</a>, box F).
               </p>
               
               <p>First, as indicated by the travel itinerary in <a href="../apparatuses/harry_and_lucy_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2</a>,
                        <span class="persName-JR">Ruskin</span> may have begun this &#x201C;Harry and Lucy&#x201D; narrative during an <span class="date-1827-09-22-1827-12-21">autumn 1827</span>
                        visit to <span class="placeName-WALES">Wales</span> and <span class="placeName-SCOTLAND">Scotland</span>. He appears originally
                        to have reserved three&#x2010;quarters of the notebook for &#x201C;Harry and Lucy,&#x201D; since later pages (pp. 36, 42) retain the
                        penciled indications, &#x201C;chap 6&#x201D; and &#x201C;chap 7.&#x201D; The narrative never extended that far, instead ending on p. 21
                        with the unfulfilled promise, &#x201C;But I will put them &#x005B;Harry and Lucy&#x005D; on to scarthing moor in another chapter.&#x201D; He may also
                        have intended the drawings on pp. , 34, 37, and 43 to accompany the projected narrative (see
                        <a href="../notes/msiii_drawings_note.php" target="_blank" class="inactive">MS III Drawings</a>).
               </p>
               
               <p>At about the same time that he was composing <a href="../apparatuses/harry_and_lucy_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2</a>,
                        <span class="persName-JR">Ruskin</span> entered&#x2014;farther in the book, following the space left
                        blank for expansion of &#x201C;Harry and Lucy&#x201D;&#x2014;the anthology <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;poetry discriptive&#x201D;</span></a>. As
                        <span class="persName-HGV">Viljoen</span> comments, the ink printing used for these poems resembles that
