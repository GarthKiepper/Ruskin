         <div id="TITLE" class="section">
            <span class="head-section">Title </span>
            
            <p>MS III</p>
            
            <p><a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> assigned the roman
                    numeral III to this <a href="../notes/red_book_note.php" target="_blank">Red Book</a> in his &#x201C;Preliminary Note on
                    the Original MSS. of the Poems&#x201D; (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> (1891) &#x005B;4<sup>o</sup>, 1891&#x005D;</a>,
                    1:262; <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>,
                    1:263).
            </p>
            
            <p><span class="date-1870">Two decades earlier</span>, <span class="persName-JR">Ruskin</span> himself had set the precedent for numbering the
                    <a href="../notes/red_book_note.php" target="_blank">Red Books</a>, including this one: on the title page for 
                    <a href="../apparatuses/harry_and_lucy_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2</a>, he wrote, &#x201C;Red book No. 2. <span class="date-1870-09-08">Sept 8<sup>th</sup> 1870</span>&#x201D;
                    (see <a href="../apparatuses/harry_and_lucy_apparatus.php#TITLE.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2: Title</a>). On the same day, he docketed 
                    <a href="../apparatuses/msiv_apparatus.php" target="_blank" class="inactive">MS IV</a> as &#x201C;Red book No. 1,&#x201D; and <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a> as &#x201C;Red
                    book No. 3.&#x201D; For the meaning of this numbering, see <a href="../notes/red_book_note.php" target="_blank">Red Books</a>.
            </p>
            
            <p>Apart from these numerical designations, <span class="persName-JR">Ruskin</span>&#x02BC;s
                    title&#x2014;intended originally, it appears, for MS III in its entirety as well as for its main work&#x2014;is
                    &#x201C;<span class="caps">Harry and Lucy / Concluded / Being / the Last Part / of / Early Lessons / in / Four Vols / Vol 2 / with Copper Plates / Hernhill</span>&#x201D;
                    (see <a href="../apparatuses/harry_and_lucy_apparatus.php#TITLE" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 2: Title</a>). This title page, like that for 
                    <a href="../apparatuses/msi_apparatus.php" target="_blank">MS I</a> and its main work, <a href="../apparatuses/harry_and_lucy_apparatus.php" target="_blank" class="inactive">&#x201C;Harry and Lucy,&#x201D; Vol. 1</a>,
                    fails to take account of a poetry anthology, <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;poetry discriptive&#x201D;</span></a>, situated
                    toward the end of the manuscript. Despite the omission, <span class="persName-JR">Ruskin</span> probably compiled the anthology at about the same time he was
                    inscribing the manuscript&#x02BC;s main work, &#x201C;Harry and Lucy,&#x201D; Vol. 2. Presumably, he wanted the manuscript&#x02BC;s contents to complement those
                    of MS I. Despite this original intention, the contents of MS III over time grew much more miscellaneous than the title page indicated, or that had been
                    the case in MS I.
            </p>
            
            <p>See also <a href="../notes/title_citation_for_major_mss.note.php" target="_blank" class="inactive">System of Title Citation for Major Manuscripts</a>.
            </p>
            
