         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p>Probably <span class="date-1827-05-1829-03">Fall 1827 (not earlier than May)&#x2013;March 1829</span>.
            </p>
            
            <p><a href="#DISCUSSION">See Discussion</a>: <span class="persName-JR">Ruskin</span>
                    started composition in the <a href="../notes/red_book_note.php" target="_blank">Red Book</a> during or shortly after a family
                    journey, probably <span class="date-1827-05-1827-12-21">in autumn 1827 but not earlier than May</span> (see <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>). 
                    This period of composition continued through <span class="date-1828-01-1828-04">early 1828</span>, probably for <span class="date-1828-01-01">New Year</span>&#x02BC;s presentation.
                    He then returned to use of the Red Book about <span class="date-1829-03">March 1829</span>.
            </p>
            
            <p>Some poems predate their witnesses in MS III&#x2014;notably <a href="../apparatuses/glen_of_glenfarg_apparatus.php" target="_blank">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</a>, 
                    dated <span class="date-1826-09">September 1826</span> in <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;poetry discriptive&#x201D;</span></a>; and 
                    <a href="../apparatuses/time_blank_verse_apparatus.php" target="_blank">&#x201C;Time: Blank Verse&#x201D;</a>, dated <span class="date-1827-01-01">1 January 1827</span> in 
                    <a href="../apparatuses/msxi_apparatus.php" target="_blank" class="inactive">MS XI</a>, and included here in the <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive">MS III Second Poetry Anthology</a>
                    &#x2014;but MS III itself was not in use at that time.
            </p>
            
            <p>See <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
            </p>
            
