         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>Like <a href="../apparatuses/glen_of_glenfarg_apparatus.php" target="_blank"><span class="title-poem-GLENOFGLENFARG">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</span></a>), 
                        <span class="title-poem-ONSCOTLAND">&#x201C;On Scotland&#x201D;</span> may have beend directly related to a Ruskin family journey to 
                        <span class="placeName-SCOTLAND">Scotland</span> in <span class="date-1826-09">September 1826</span>. As discussed in 
                        <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>, however, 
                        it is unclear whether evidence indicates two separate northern journeys, one of <span class="date-1826">1826</span> and one of <span class="date-1827">1827</span>, 
                        or only a single tour of <span class="date-1827">1827</span>. It is certainly the case that 
                        <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a> 
                        was incorrect to assume that <span class="title-poem-ONSCOTLAND">&#x201C;On Scotland&#x201D;</span> contains &#x201C;a reminiscence of the <span class="date-1826-05">May</span> 
                        sunshine in which &#x005B;the Ruskins&#x005D; went northwards&#x201D; contrasting &#x201C;with the <span class="date-1826-09-22-1826-12-21">autumnal</span> gloom of the departure&#x201D; 
                        from <a href="../notes/perth_note.php" target="_blank" class="inactive"><span class="placeName-PERTH">Perth</span></a> 
                        (<a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1:xxiii,
                        <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:ix). 
                        We now know that, in <span class="date-1826">1826</span>, the family could have journeyed north only later in the year, not in <span class="date-1826-05">May</span>.
            </p>
            
            <p>The poem&#x02BC;s trope of &#x201C;changes&#x201D; can refer to a topographical experience without that experience being literally seasonal, 
                        as <span class="persName-WGC">Collingwood</span> assumed; the phrase &#x201C;as on sprightly May&#x201D; functions as a simile. 
                        The strength of this poem&#x02BC;s topographical observation of contrast between &#x201C;pretty <span class="placeName-PERTH">Perth</span>&#x201D; 
                        and the dreariness of the moor, and of the character of the <span class="geogName-EARN">Earn River</span>, 
                        is the strongest argument for the poem&#x02BC;s grounding in a journey north that would have to have taken place in <span class="date-1826">1826</span>, 
                        not <span class="date-1827">1827</span>, in order for <span class="persName-JR">Ruskin</span> to have entered the poem 
                        in <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>&#x2014;if 
                        the poem refers to actual and recent experience at all.
            </p>
            
            <p>The &#x201C;changes&#x201D; can also be emotional. See again <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a> 
                        for how <span class="date-1826">1826</span> was additionally burdened by the <span class="date-1826-05-08">death</span> of <span class="persName-JR">Ruskin</span>&#x02BC;s cousin
                        <a href="../notes/richardson_james_note.php" target="_blank" class="inactive"><span class="persName-JRPSON">James Richardson</span></a>, who had been living at
                        <a href="../notes/herne_hill_note.php" target="_blank" class="inactive"><span class="placeName-HERNE">Herne Hill</span></a> and working for
                        <a href="../notes/ruskin_telford_note.php" target="_blank" class="inactive"><span class="orgName-RUSKINETAL">Ruskin, Telford, and Domecq</span></a>, 
                        but by <span class="date-1826-04">April 1826</span> had grown so ill from tuberculosis that he had to return to <span class="placeName-SCOTLAND">Scotland</span>. 
                        If <span class="persName-JJR">John James</span> and <span class="persName-MR">Margaret</span> accompanied <span class="persName-JRPSON">James</span> to 
                        <a href="../notes/perth_note.php" target="_blank" class="inactive"><span class="placeName-PERTH">Perth</span></a>, where he died on <span class="date-1826-05-08">8 May</span>&#x2014;and 
                        <span class="persName-MR">Margaret</span> later recalled to <span class="persName-JJR">John James</span> the &#x201C;journey up,&#x201D; when &#x201C;both you &#x0026; I had repeatedly asked &#x005B;<span class="persName-JRPSON">James</span>&#x005D;
                        if he was quite sure he was happy&#x201D;&#x2014;they presumably would not have exposed <span class="persName-JR">John</span> to infection by bringing him along on the journey 
                        (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive"><i>Ruskin Family Letters</i></a>, 142).
            </p>
            
            <p><span class="persName-JR">Ruskin</span> grew despondent over <span class="persName-JRPSON">James</span>&#x02BC;s departure, his depression deepened by the protracted absence also of <span class="persName-JJR">his father</span>: 
                        &#x201C;No papa no James,&#x201D; he complained to his mother 
                        (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 141). 
                        For other writing possibly connected with <span class="date-1826-05-08"><span class="persName-JJR">James</span>&#x02BC;s death</span>, 
                        see <span class="title-poem-THEDEFIANCEOFWAR">&#x201C;The Defiance of War&#x201D;</span>; 
                        <span class="title-poem-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span>; and a canceled line in 
                        <span class="title-poem-THESHIP182829">&#x201C;The Ship&#x201D; &#x005B;1828&#x2013;29&#x005D;</span> (&#x201C;by which the Scottish <span class="persName-JRPSON">James</span> did meet his <span class="date-1826-05-08">death</span>&#x201D;).
            </p>
            
