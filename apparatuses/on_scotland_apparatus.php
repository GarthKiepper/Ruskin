            <span class="head-section">Title</span>
            
            <p><span class="title-poem-ONSCOTLAND">&#x201C;On Scotland&#x201D; &#x005B;&#x201C;Farewell to Scotland&#x201D;&#x005D;</span></p>
            
            <p><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-ONSCOTLAND">&#x201C;on scotland&#x201D;</span>. He also
                        designated the poem as &#x201C;poem II&#x201D; in <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>. 
                        The title &#x201C;Farewell to Scotland&#x201D; was invented by <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span></a>
                        for <a href="../notes/poems_1891_note.php" target="_blank" class="inactive"><i>Poems</i> (1891)</a>.
            </p>
            
            <p>See also <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
         </div>
         
