         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>For the <span class="date-1827">1827</span> family tour to <span class="placeName-WALES">Wales</span> and <span class="placeName-SCOTLAND">Scotland</span>, 
                        on which <span class="persName-JR">Ruskin</span> was pining to set forth in <span class="date-1827-05">May</span>, 
                        but which was delayed by his <span class="persName-JJR">father</span>&#x02BC;s protracted business travel, 
                        see <a href="../apparatuses/wales_apparatus.php#DISCUSSION" target="_blank">&#x201C;Wales&#x201D;: Discussion</a> and 
                        <a href="../notes/tours_of_1826-27_note.php" target="_blank">Tours of 1826&#x2013;27</a>.
            </p>
            
            <p>With its arcane naming of <i>nectarine</i> as <i>nectron</i>, and its attention to exotic species such as the <i>Kerria japonica</i> from 
                        <span class="placeName-CHINA">China</span> (see glosses to the poem), <span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span> 
                        reflects the romance that empire imparted to British botany of the long nineteenth century. This exoticism is reflected also in the contemporaneous travel narrative in 
                        <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span></a>, 
                        in which the travelers visit a friend who shows off &#x201C;the mimosa the papyrus and the coffee tree&#x201D;&#x2014;a visit to a hothouse of tropical plants, whether true or imagined.
            </p>
            
