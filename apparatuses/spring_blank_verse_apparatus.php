         <div id="TITLE" class="section">
            <span class="head-section">Title</span>
            
            <p><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span>.
            </p>
            
            <p>Entitled <span class="title-poem-SPRINGBLANKVERSE">&#x201C;SPRING&#x201D;</span> in the version included in a <span class="date-1827-05">May 1827</span> 
                        letter to his <span class="persName-JJR">father</span> (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 158); 
                        and as &#x201C;spring<span class="space" style="padding-left:2.4em">Â </span> blank verse&#x201D;
                        in <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a>. See <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
         </div>
         
         <div id="GENRE" class="section">
            <span class="head-section">Genre</span>
            
            <p>Poem. <span class="persName-JR">Ruskin</span> applied the term <i>blank verse</i> also to <a href="../apparatuses/time_blank_verse_apparatus.php" target="_blank"><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span></a> and to
                        <a href="../apparatuses/on_the_rainbow_apparatus.php" target="_blank"><span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span></a>. The verse is indeed unrhymed, 
                        although not consistently pentameter.
            </p>
            
         </div>
         
         <div id="MAJORMS" class="section">
            <span class="head-section">Manuscripts</span>
            
            <p>Contained along with <a href="../apparatuses/wales_apparatus.php" target="_blank"><span class="title-poem-WALES">&#x201C;Wales&#x201D;</span></a>in a <span class="date-1827-05">May 1827</span> 
                        letter to his <span class="persName-JJR">father</span> (<a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 158); 
                        also contained in <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> (pp.
                        63&#x2013;65&#x005B;<i>a</i>&#x005D;), a <a href="../notes/red_book_note.php" target="_blank">Red Book</a> devoted
                        primarily to <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span></a>. 
                        <span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span> is the fourth poem in <span class="title-manuscript-MSIII">MS III</span>&#x02BC;s
                        <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>.
            </p>
            
            <p>On <span class="date-1827-04-28">28 April 1827</span>, <span class="persName-MR">Margaret</span> announced to <span class="persName-JJR">John James</span> 
                        that &#x201C;<span class="persName-JR">John</span> has sent you his first written letter.&#x201D; By <i>written</i>, she may have meant his first letter in his own hand 
                        as opposed to her transcription (an example of the latter being a letter dated <span class="date-1823-03-15">15 March 1823</span> from both <span class="persName-JR">John</span> 
                        and <span class="persName-MR">Margaret</span> to <span class="persName-JJR">John James</span>, which is written entirely in <span class="persName-MR">Margaret</span>&#x02BC;s hand, 
                        and which is the only extant epistle by <span class="persName-JR">Ruskin</span> 
                        prior to <span class="date-1827-05">May 1827</span>, putting aside his presentation copies of poems). <span class="persName-MR">Margaret</span> may also&#x2014;or instead&#x2014;have 
                        meant by the term <i>written</i> to refer to <span class="persName-JR">Ruskin</span>&#x02BC;s first letter in cursive hand, as opposed to printing. 
                        As she goes on to remark, &#x201C;I believe the showing you his writing occupied his thoughts fully more than how he expressed his feelings so you must excuse that.&#x201D; 
                        <span class="persName-VAB">Van Akin Burd</span> notates this &#x201C;written&#x201D; letter as &#x201C;unidentified,&#x201D; 
                        but it seems probable that <span class="persName-MR">Margaret</span> was referring to <span class="persName-JR">Ruskin</span>&#x02BC;s letter of <span class="date-1827-05">May 1827</span> 
                        (containing <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span> and <span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring&#x201D;</span>), 
                        which <span class="persName-VAB">Burd</span> prints following <span class="persName-MR">Margaret</span>&#x02BC;s of <span class="date-1827-04-28">28 April 1827</span>. 
                        <span class="persName-JR">Ruskin</span> wrote this letter in an awkward cursive script&#x2014;mostly in pencil, 
                        except for a postscript in ink. In this letter, he was also using pen and ink for the first time, which <span class="persName-MR">Margaret</span> says that he was 
                        &#x201C;much delighted at being able to use.&#x201D; He copied the two poems, 
                        unlike the text of the letter, entirely in ink, although in a print, not cursive hand 
                        (see <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 156, 127&#x2013;28, 157 n. 4).  
                    
            </p>
            
            <p>Facsimile and transcript by permission of Beinecke Rare Book and Manuscript Library, Yale University.</p>
            
            
         </div>
         
         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1827-05">May 1827</span> or possibly somewhat earlier. Evidence is contradictory in determining which is the earlier of the two pairs of 
                        <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span> and <span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring&#x201D;</span>&#x2014;the 
                        <span class="date-1827-05">May 1827</span> version or the <span class="title-manuscript-MSIII">MS III</span> version. 
                        The greater assurance with which <span class="persName-JR">Ruskin</span> appears to handle a pen and ink, along with the suggestiveness of some variants, 
                        points to <span class="title-manuscript-MSIII">MS III</span> as the later version: e.g., in line 8 of <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span>, 
                        a possibly overwritten alteration in punctuation in the <span class="date-1827-05">May 1827</span> version 
                        is confirmed in <span class="title-manuscript-MSIII">MS III</span>, indicating the latter as the secondary version. Similarly, in <span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring&#x201D;</span>, 
                        an addition above line 1 in <span class="date-1827-05">May 1827</span> is duly incorporated into <span class="title-manuscript-MSIII">MS III</span>, 
                        and the that version contains a new phrase at line 14, which is not present in <span class="date-1827-05">May 1827</span>. 
                        At the same time, the <span class="title-manuscript-MSIII">MS III</span> version is uncharacteristically marred by grammatical and spelling errors that do not appear 
                        in the <span class="date-1827-05">May 1827</span> version. It is unlike <span class="persName-JR">Ruskin</span> to introduce errors in a subsequent copy. 
                        (See the glosses attached to the two transcriptions.)
            </p>
            
            <p>Oddest of all is <span class="persName-JR">Ruskin</span>&#x02BC;s employment of the term <i>nectron</i> in <span class="title-manuscript-MSIII">MS III</span>, 
                        an unusual form as compared with the more familiar 
                        <i>nectarine</i> used in the <span class="date-1827-05">May 1827</span> version. Complicating this word choice, in <span class="date-1827-05">May 1827</span> 
                        also contains one occurrence of <i>nectron</i>, which <span class="persName-JR">Ruskin</span> scores through and replaces with <i>nectarine</i>. 
                        It seems equally plausible, therefore, to treat <span class="date-1827-05">May 1827</span> as a later version that 
                        corrects word choice and grammatical, punctuation, and spelling errors found in <span class="title-manuscript-MSIII">MS III</span>. Or perhaps the two versions 
                        are independent copies deriving from a third, prior version, now lost.
            </p>
            
            <p>See also <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
            </p>
            
            
