         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>In this brief poem, it is possible that <span class="persName-JR">Ruskin</span> is exploring how he can use the line&#x2014;both in itself and in its arrangement on the physical page&#x2014;to highlight
                        poetic effects, such as repetition, consonance, and assonance. For example, on the physical page, by breaking the first line with a runover (printed in smaller
                        lettering), he draws attention to the symmetry of the repeated <i>thy</i> in the first part of the line. Similiarly, the bold enjambment in line 3 emphasizes the
                        consonance of <i>twittering</i> and <i>tempests</i>. Assonance is prominent throughout.
            </p>
            
