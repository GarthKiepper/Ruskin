         <div id="MAJORMS" class="section">
            <span class="head-section">Manuscripts</span>
            
            <p><a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSIA">MS IA</span></a>; 
                        <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> (pp. 25&#x2013;27).
            </p>
            
            <p>Facsimile and transcript by permission of Beinecke Rare Book and Manuscript Library, Yale University.</p>
            
            <p>In <span class="title-manuscript-MSIA">MS IA</span>, which is a compilation, the manuscript per se of the poem 
                        (see <a href="../apparatuses/msia_apparatus.php#CONTENTS" target="_blank" class="inactive">MS IA: Contents</a>), 
                        is a one&#x2010;sheet, 12.5 by 20 cm holograph, presumably meant as a 
                        <a href="../notes/new_years_poems_note.php" target="_blank" class="inactive">New Year&#x02BC;s Poem</a> presentation copy.
            </p>
            
            <p>In <span class="title-manuscript-MSIII">MS III</span>, a <a href="../notes/red_book_note.php" target="_blank">Red Book</a> devoted primarily to
                        <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span></a>, 
                        <span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span> is the second poem in the 
                        <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive"><span class="title-anthology-MSIIIPOETRYANTHOLOGY2">MS III Second Poetry Anthology</span></a>.
            </p>
            
         </div>
         
         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1827-01-01">1 January 1827</span>.
            </p>
            
            <p>Dated by <span class="persName-JR">Ruskin</span> in the <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSIA">MS IA</span></a> manuscript, 
                        an annotation that identifies the work as the first definitely known 
                        instance of a <a href="../notes/new_years_poems_note.php" target="_blank" class="inactive">New Year&#x02BC;s Poem</a> (see also 
                        <a href="../apparatuses/the_needless_alarm_apparatus.php#DISCUSSION" target="_blank"><span class="title-poem-THENEEDLESSALARM">&#x201C;The Needless Alarm&#x201D;</span>: Discussion</a>). 
                        At the end of the <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> witness, 
                        a hand other than <span class="persName-JR">Ruskin</span>&#x02BC;s dated the poem as 
                        &#x201C;<span class="date-1827-01-01">Jan 1.1827</span>.&#x201D; The hand appears to be <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive">W. G. Collingwood</a>&#x02BC;s.
            </p>
            
            <p>In <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a>, given the poem&#x02BC;s position amid writing from <span class="date-1828-01-1828-04">early 1828</span> (i.e.,
                        positioned between <a href="../apparatuses/the_constellations_apparatus.php" target="_blank" class="inactive"><span class="title-poem-THECONSTELLATIONS">&#x201C;The Constellations: Northern, Some of the Zodiac, and Some of the Southern</span>&#x201D;</a> 
                        and <a href="../apparatuses/the_sun_apparatus.php" target="_blank" class="inactive"><span class="title-poem-THESUN">&#x201C;The Sun&#x201D;</span></a>), this witness must date from <span class="date-1828-01-1828-04">early 1828</span>.
            </p>
            
            <p>See <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
            </p>
            
