            <span class="head-section">Title</span>
            
            <p><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span></p>
            
            <p>The poem is untitled in <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSIA">MS IA</span></a>; 
                        entitled by <span class="persName-JR">Ruskin</span> as <span class="caps"><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time / Blank Verse&#x201D;</span></span> 
                        in his later witness, <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a>. Specifying <i>blank
                        verse</i> in the title is in keeping with another poem probably composed within a few months (or even weeks or days) of
                        <span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span>&#x2014;<a href="../apparatuses/on_the_rainbow_apparatus.php" target="_blank"><span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse</span>&#x201D;</a>.
            </p>
            
            <p>See <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
         </div>
         
