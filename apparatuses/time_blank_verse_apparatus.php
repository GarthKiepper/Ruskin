         <div id="COMPPUB" class="section">
            <span class="head-section">Composition and Publication</span>
            
            <p><a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSIA">MS IA</span></a> witness, 
                        <a href="../bibiliography/bibliography.php#RFL" target="_blank" class="inactive">Burd, ed., <i>Ruskin Family Letters</i></a>, 150&#x2013;51.
            </p>
            
            <p><a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> witness, 
                        <a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> (4<sup>o</sup>, 1891)</a>,
                        1:xxvi&#x2013;xxvii; <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> (8<sup>o</sup>, 1891)</a>,
                        1:xiii&#x2013;xiv; and <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:258&#x2013;59.
            </p>
            
            <p>As the second poem of the <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive"><span class="title-anthology-MSIIIPOETRYANTHOLOGY2">MS III Second Poetry Anthology</span></a>, 
                        <span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span> in this version cannot derive from the 
                        <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive"><span class="title-manuscript-MSIA">MS IA</span></a> version, as claimed in the 
                        <a href="../notes/library_edition_note.php" target="_blank" class="inactive">Library Edition</a> 
                        (<a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:258 n. 1), since, in 
                        the anthology, it precedes <a href="../apparatuses/the_sun_apparatus.php" target="_blank" class="inactive"><span class="title-poem-THESUN">&#x201C;The Sun&#x201D;</span></a>, 
                        which belongs to <span class="date-1828">1828</span>.
                        Rather, the <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a> version is a copy, with variants, 
                        of the earlier presentation copy for <span class="date-1827-01-01">New Year&#x02BC;s Day, 1827</span>.
            </p>
            
         </div>
         
         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p>Since <a href="../apparatuses/the_sun_apparatus.php" target="_blank" class="inactive"><span class="title-poem-THESUN">&#x201C;The Sun&#x201D;</span></a>, 
                        like <span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span>, is a 
                        <a href="../notes/new_years_poems_note.php" target="_blank" class="inactive">New Year&#x02BC;s Poem</a>, <span class="persName-JR">Ruskin</span> may have 
                        celebrated the <span class="date-1828-01-01">New Year&#x02BC;s Day for 1828</span> by enhancing 
                        <a href="../apparatuses/msiii_apparatus.php" target="_blank"><span class="title-manuscript-MSIII">MS III</span></a>, inserting after 
                        <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy&#x201D; Vol. 2</span></a>, the 
                        <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive"><span class="title-anthology-MSIIIPOETRYANTHOLOGY2">MS III Second Poetry Anthology</span></a>, which 
                        includes these poems. It is possible that this observance repeated a similar pattern in <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>, 
                        to which he had added <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a> 
                        following <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy,&#x201D; Vol. 1</span></a>, 
                        in <span class="date-1827-01-1827-03">early 1827</span>. (In <span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span>, 
                        he included <a href="../apparatuses/the_needless_alarm_apparatus.php" target="_blank"><span class="title-poem-THENEEDLESSALARM">&#x201C;The Needless Alarm&#x201D;</span></a>, 
                        which may have been composed a year earlier, in <span class="date-1826-01">January 1826</span>, and which may therefore have
                        likewise served as a New Year&#x02BC;s Poem; see <a href="../apparatuses/the_needless_alarm_apparatus.php#DISCUSSION" target="_blank">&#x201C;The Needless Alarm&#x201D;: Discussion</a>.)
            </p>
            
            <p>In <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a>, the poem wraps around a drawing, <a href="../drawings/ship_and_house_drawing_msiii.jpg" target="_blank" class="inactive">Ship and House</a>,
                        which bears no evident relation to the poem. <span class="persName-JR">Ruskin</span> probably intended the drawing for a continuation of 
                        <a href="../apparatuses/harry_and_lucy_vol2_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy,&#x201D; Vol. 2</span></a>,
                        which he ended prior to reaching this page, using the remaining space for the <span class="title-anthology-MSIIIPOETRYANTHOLOGY2">MS III Second Poetry Anthology</span>.
            </p>
            
