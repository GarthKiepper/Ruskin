      <span id="DOTHFALLGLOSS">&#x201C;Which . . . doth fall&#x201D; (MS II)&#x2014;As originally conceived, the line probably read: &#x201C;Which from thy highest mountain falls&#x201D;, 
                    which was octosyllabic, but provided an imperfect rhyme of &#x201C;falls&#x201D; for &#x201C;call&#x201D; in the following line. Ruskin therefore revised the line 
                    to the version found in the fair copy, &#x201C;Which from thy mountain head doth fall&#x201D;, which is also correctly octosyllabic, but the line appears confused in the draft. 
                    Ruskin deleted &#x201C;highest&#x201D; and substituted &#x201C;head&#x201D; above, appearing to create the nonsense phrase, &#x201C;head mountain&#x201D;, 
                    but in fact intending &#x201C;head&#x201D; as a kind of runover, forming &#x201C;mountain head&#x201D;. Finally, at the end of the line, he deleted the <i>s</i> on &#x201C;falls&#x201D; 
