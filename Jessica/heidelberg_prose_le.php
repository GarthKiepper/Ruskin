      <p>The <span class="placeName-building-HEIDELBERG">castle of Heidelberg</span> is exceeding desolate. Armies have razed 
                    its foundations, the thunder hath riven its towers, and there is no sound 
                    in its courts, and the wind is still in the open galleries.<span class="glosses"><a href="../glosses/heidelberg_glosses_contextual.php#DESOLATE" target="_blank"" class="inactive">4</a></span> The grass is very 
