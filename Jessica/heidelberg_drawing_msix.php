      Pen and ink, approx. ? &#x00D7; ? cm (image only). The editors of the <a href="../notes/library_edition_note.html" target="_blank" class="inactive">Library Edition</a> 
                        describe the image as a &#x201C;sketch of a town in a large plain, with distant mountains,&#x201D; 
                        although they misplaced its position in the manuscript&#x2014;at least, as presently found&#x2014;locating it between the poem and prose section of &#x201C;St. Goar,&#x201D; 
                        which would be on the preceding verso (43v), rather than its current placement (44v) 
                        (<a href="../bibiliography/bibliography.html#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:360). 
                        While the vignette is placed at the head of the poem about <span class="placeName-HEIDELBERG">Heidelberg</span>, 
                        the depiction of the distant settlement bears none of that town&#x02BC;s landmarks&#x2014;no castle nestled against the mountainside, 
                        no arched bridge across the river, no gothic cathedral tower (rather, a round dome is prominent). Considering the attention that 
                        <span class="persName-JR">Ruskin</span> gives to the castle ruins in his poem and prose description, it seems strange that he would omit that feature 
                        in a drawing meant to represent a view of the town. The single appropriate feature is the broad plain: 
                        as the <span class="geogName-NECKAR">Neckar</span> emerges from the <span class="placeName-ODENWALD">Odenwald</span> mountain range 
                        at <span class="placeName-HEIDELBERG">Heidelberg</span>, the river does flow into a level plain toward <span class="placeName-MANNHEIM">Mannheim</span>, 
                        where it empties into the <span class="geogName-RHINE">Rhine</span>. Otherwise, the scene might just as well depict <span class="placeName-STGOAR">St. Goar</span> 
                        as <span class="placeName-HEIDELBERG">Heidelberg</span>&#x2014;except, in that case, the broad plain would be wrong, 
                        and no <span class="geogName-LORELEI">Lorelei</span> is shown looming around a bend in the river&#x2014;and if the scene was meant to show how &#x201C;towards the western day, / 
                        Manheims towers softened lay,&#x201D; as the opening of <span class="persName-JR">Ruskin</span>&#x02BC;s poem suggests, 
                        then the mountains behind distant <span class="placeName-MANNHEIM">Mannheim</span> are wrong. The picture may be as nonspecific as it appears, 
                        and its placement just as random, since the fair copy of the &#x201C;Account&#x201D; ends abruptly soon afterward, in the middle of a sentence, with only eight lines of the prose section 
                        of &#x201C;Heidelberg&#x201D; carried over from draft.
                    
      
      
