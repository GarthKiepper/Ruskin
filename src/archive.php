        <span id="APPARATUSES" class="head-section">Apparatuses</span>
        <hr/>
        <p>For the unitiatied and initiated alike, Apparatuses function as entry points into the early work of John Ruskin&#x2014;providing context for this work by title, genre, witness, date of composition/publication, and more. Manuscript Apparatuses focus upon the letters, notebooks, and books in which Ruskin's early work is found. Anthology Appartuses generally focus upon subsets of these&#x2014;such as poetry anthologies&#x2014;which may themselves contain several works. And Work Apparatuses focus upon the individual works themselves, which may include prose, poems, and fragments in various stages of the writing processes&#x2014;from draft to fair copy to print. Clicking/tapping on one of the links below will open it in a new browser window/tab so that this page can be referred back to as needed.</p>
