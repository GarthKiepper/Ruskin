         <span class="head-section">Anthologies</span>
         
         <div id="ANTHOLOGIES1826-27" class="subsection">
            <span class="head-subsection">Anthologies, 1826&#x2013;27</span>
            
            <p>The earliest extant anthologies are the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a> 
                                (1826&#x2013;27); <a href="../apparatuses/poetry_discriptive_apparatus.php" target="_blank">Poetry Descriptive</a> (1827) in 
                                <a href="../apparatuses/msiii_apparatus" target="_blank" class="inactive">MS III</a>; and, also in the latter <a href="../notes/red_book_note.php" target="_blank">Red Book</a>,
                                the <a href="../apparatuses/msiii_poetry_anthology_2_apparatus.php" target="_blank" class="inactive">MS III Second Poetry Anthology</a>, and the 
                                <a href="../apparatuses/msiii_poetry_anthology_3_apparatus.php" target="_blank" class="inactive">MS III Third Poetry Anthology</a>.
            </p>
            
            <p>What was <span class="persName-JR">Ruskin</span>&#x02BC;s model for his first anthologies? In <a href="../notes/lesson_note.php" target="_blank" class="inactive">lesson</a>
                                dialogues by <a href="../notes/edgeworth_maria_note.php" target="_blank" class="inactive"><span class="persName-ME">Maria Edgeworth</span></a> and 
                                <a href="../notes/joyce_jeremiah_note.php" target="_blank" class="inactive"><span class="persName-JJ">Jeremiah Joyce</span></a>, he would have found poetry liberally quoted,
                                especially poems by associates of the <a href="../notes/lunar_society_of_birmingham_note.php" target="_blank" class="inactive"><span class="orgName-LUNARSOCIETY">Lunar Society</span></a> such as 
                                <a href="../notes/darwin_erasmus_note.php" target="_blank"><span class="persName-ED">Erasmus Darwin</span></a>. He may also have seen &#x201C;miscellanies&#x201D;
                                and &#x201C;beauties,&#x201D; kinds of anthologies popular throughout the <span class="date-1701-1800">eighteenth century</span> and into the <span class="date-1820-1829">1820s</span>.
            </p> 
            
            <p>Such anthologies, according to <span class="persName-BMB">Barbara M. Benedict</span>, encouraged their middle&#x2010;class audiences to construct literature 
                                &#x201C;by responsive reading rather than by authorial control,&#x201D; the author having &#x201C;virtually disappeared from the presentation of his texts; 
                                the reader now &#x2018;takes&#x2019; the text as he wills where once he took it as the author&#x02BC;s lesson&#x201D; 
                                (<a href="../bibiliography/bibliography.php#MISCELLANIES" target="_blank" class="inactive">Benedict, &#x201C;Literary Miscellanies,&#x201D;</a> p. 409; see also 
                                <a href="../bibiliography/bibliography.php#BEAUTIES" target="_blank" class="inactive">Benedict, &#x201C;&#x2018;Beauties&#x2019; of Literature,&#x201D;</a> p. 323).
                                <span class="persName-JR">Ruskin</span> does appropriate a poem by <span class="persName-ED">Erasmus Darwin</span> and transform it as his own in 
                                <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a>. 
                                This evidence of reader&#x2010;centeredness is offset, however, by <span class="persName-ME">Edgeworth</span>&#x02BC;s training of her young readers in what one would now call <i>close reading</i>. 
                                In part 2 of <i>Frank</i> (1802), the boy, who is about <span class="persName-JR">Ruskin</span>&#x02BC;s age, is not told who authored lines he has heard recited by his father 
                                (the reader learns from a footnote that the author is <span class="persName-ED">Darwin</span>), and Frank is cautioned by his mother that he is too young to &#x201C;&#x2018;understand the sense of them yet&#x2019;&#x201D;; nonetheless, 
                                his mother leads Frank through a close analysis of the lines&#x02BC; metaphor and diction that can scarcely be considered unstudious of authorized meaning 
                                (<a href="../bibiliography/bibliography.php#EDGEWORTHWORKS" target="_blank" class="inactive">Edgeworth, <i>Works,</i></a> 12: 195, and see pp. 195&#x2013;99).
            </p>
            
            <p>Another function dramatically evident in the <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank">MS I Poetry Anthology</a> is the &#x201C;forging&#x201D; of identity that 
                                <span class="persName-LC">Linda Colley</span> characterizes as the making of Britons in the <span class="date-1701-1834">eighteenth and early nineteenth centuries</span>. 
                                The spring of that formation, fear of the Other (Francophobia) invading the homeland, is the most persuasive explanation 
                                for R<span class="persName-JR">Ruskin</span>&#x02BC;s motives in his (possibly overdetermined) poem, <a href="../apparatuses/the_defiance_of_war_apparatus.php" target="_blank">&#x201C;The Defiance of War&#x201D;</a>. 
                                The identity thus imaginatively preserved in this poem is, in the anthology, welded both to <span class="placeName-NBRITAIN">North Briton</span> and to <span class="placeName-SBRITAIN">South</span>,
                                with the poems <a href="../apparatuses/on_scotland_apparatus.php" target="_blank">&#x201C;On Scotland&#x201D;</a> and <a href="../apparatuses/the_steam_engine_apparatus.php" target="_blank">&#x201C;When furious up from mines the water pours&#x201D; &#x005B;&#x201C;The Steam Engine&#x201D;&#x005D;</a>
                                &#x2014;the latter an imitation of a poem by the <span class="geogName-ENGLISHMIDLANDS">English Midlands</span> poet, <span class="persName-ED">Erasmus Darwin</span>, celebrating in turn the invention by the illustrious Scot, <span class="persName-JW">James Watt</span>. 
                                The final poem of the anthology, <a href="../apparatuses/on_the_rainbow_apparatus.php" target="_blank">&#x201C;On the Rainbow: In Blank Verse&#x201D;</a>, seems to seal the collection 
                                with the &#x201C;apocalyptic interpretation of history&#x201D; that, as <span class="persName-LC">Colley</span> remarks, represented <span class="placeName-BRITAIN">Britain</span> as <span class="placeName-ISRAEL">Israel</span> &#x201C;and its opponents as Satan&#x02BC;s accomplices,&#x201D; 
                                a conviction that remained engraved in British minds, unfaded by rationalism, and that characterized the thinking of &#x201C;many devout Protestants,&#x201D; Dissenter and Anglican alike 
                                (<a href="../bibiliography/bibliography.php#BRITONS" target="_blank" class="inactive">Colley, Britons</a>, pp. 30&#x2013;31).
            </p>
            
