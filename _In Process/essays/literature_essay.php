         <p>From 1826 to  <span class="persName-JR">Ruskin</span>&#x02BC;s poetry appears in four forms:
                        
            <ul>
               
               <li>as anthologies fair&#x2010;copied in the <a href="../notes/red_book_note.php" target="_blank">Red Books</a> and other bound manuscripts; as single fair&#x2010;copy
                                presentations of occasional poems, such as New Year&#x02BC;s Poems;
               </li>
               
               <li>as inclusions within letters, whether as part of the text or as a separate enclosure; and, in a form of presentation that evidently was never fully realized in this earliest period
                                of writing;</li> 
               
               <li>as a volume&#x2010;length poem&#x2014;<a href="../apparatuses/eudosia_apparatus.php" target="_blank" class="inactive">&#x201C;Eudosia,&#x201D;</a> for example, apparently being intended;
               </li>
               
               <li>as a &#x201C;poem on the universe,&#x201D; to fill <a href="../apparatuses/msiv_apparatus.php" target="_blank" class="inactive">MS IV</a>.
               </li>
               
            </ul>
            
         </p>
         
         <p>In , <span class="persName-JR">Ruskin</span> realized the ambition of a volume&#x2010;length poem with <a href="../apparatuses/iteriad_apparatus" target="_blank" class="inactive">&#x201C;Iteriad&#x201D;</a> 
                        in <a href="../apparatuses/msvii_apparatus.php" target="_blank" class="inactive">MS VII</a> (albeit only briefly, as he follows &#x201C;Iteriad&#x201D; in that manuscript with an incomplete fair copy of the incompletely drafted 
                        Byronic epic, <a href="../apparatuses/athens_apparatus.php" target="_blank" class="inactive">&#x201C;Athens&#x201D;</a>). From , he compiles a volume&#x2010;length anthology, &#x201C;Miscellaneous Poetry&#x201D; 
                        in <a href="../apparatuses/msv_apparatus.php" target="_blank" class="inactive">MS V.</a></p>
         
