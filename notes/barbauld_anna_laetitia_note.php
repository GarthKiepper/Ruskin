      <div class="title"><span class="persName-ALB">Anna Letitia Barbauld</span> (1743&#x2013;1825)
      </div>
      
      <p>Educationist, poet, and writer for children. With her brother, the physician and writer, <span class="persName-JAIKIN">John Aikin</span> (1747&#x2013;1822), <span class="persName-ALB">Barbauld</span> was a member of an intellectual Dissenter circle 
                    including the Edgeworths, <span class="persName-ED">Erasmus Darwin</span>, and others, whose influence as educationists remained widespread and reflected in <span class="persName-JR">Ruskin</span>&#x2019;s early reading and writing, 
                    although their politics of many in this circle belonged to a bygone revolutionary fervor. <span class="persName-JAIKIN">Aikin</span> and <span class="persName-ALB">Barbauld</span>&#x2019;s 
                    <span class="title-AIKINBARBAULDEVENINGS">Evenings at Home</span> (<span class="date-1792-1796">1792&#x2013;96</span>) is likely 
                    to have formed part of the Ruskin family library, <a href="../notes/sharp_f_j_note.php" target="_blank" class="inactive"><span class="persName-FJS">F. J. Sharp</span> (1880&#x2013;1957)</a> 
                    acquiring from <span class="placeName-BRANTWOOD">Brantwood</span> what he believed to be <span class="persName-JR">Ruskin</span>&#x02BC;s boyhood copy of that book 
                    (<a href="../bibiliography/bibliography.php#VILJOENRUSKINSBACKGROUNDS" target="_blank" class="inactive">Viljoen, <i>Sharp Collection</i></a>, 8), 
                    as well as a copy of <span class="persName-ALB">Barbauld</span>&#x2019;s <span class="title-BARBAULDHYMNSINPROSE">Hymns in Prose</span> (<span class="date-1781">1781</span>). 
                    The influence of <span class="title-AIKINBARBAULDEVENINGS">Evenings at Home</span> is discernible in what is probably <span class="persName-JR">Ruskin</span>&#x2019;s earliest extant poem, 
                    <a href="../apparatuses/the_needless_alarm_apparatus.php" target="_blank">&#x201C;The Needless Alarm&#x201D;</a>, 
                    and in <a href="../apparatuses/the_adventures_of_an_ant_apparatus.php" target="_blank" class="inactive">&#x201C;The Adventures of an Ant&#x201D;</a>.
                
      </p>
      
