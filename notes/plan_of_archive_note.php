      <div class="title">Plan of the Archive</div>
      
      <p>The archive <i>Early Ruskin Manuscripts</i> (<i>ERM</i>)
                    contains, at the most fundamental level, two sets of edited primary materials: 
                    <i>works</i> and <i>manuscripts</i>.
      </p>
      
      <ul>
         
         <li>A <i>work</i> in this edition consists of a discrete text 
                            by <span class="persName-JR">Ruskin</span> (e.g., a poem, an essay, a 
                            sermon, a mathematical proof), including all available witnesses of that 
                            text (each of these edited and annotated), and accompanied by the 
                            work&#x02BC;s explanatory apparatus and available facsimiles.
         </li>
         
         <li>A <i>manuscript</i> is a physical document manifesting 
                            <span class="persName-JR">Ruskin</span>&#x02BC;s texts, 
                            represented in the archive by its own explanatory apparatus in addition to 
                            the <i>works</i> that make up the manuscript.
         </li>
         
      </ul>
      
      <p>Organizationally, the archive turns on these basic units, 
                    as represented by a given work&#x02BC;s <a href="#WORKSPAGES">Work Pages</a> 
                    and by a given manuscript&#x02BC;s <a href="#MANUSCRIPTPAGES">Manuscript Pages</a>. 
                    The fundamental relation between these two kinds of units is the manuscript as container and the work as contained. 
                    For <span class="persName-JR">Ruskin</span>, however, the separate integrity of these units often creates a creative tension, 
                    which the archive aims to preserve through its editorial methodology (see 
                    <a href="../notes/editorial_rationale_note.php" target="_blank">Editorial and Encoding Rationale and Methodology)</a>.
      </p>
      
      <p><a href="#WORKSPAGES">Work Pages</a> and <a href="#MANUSCRIPTPAGES">Manuscript Pages</a> consist respectively 
                    of consistent sets of XML/TEI documents, discussed below. At a global level, the archive also contains editorial 
                    materials&#x2014;<a href="#NOTES">Notes</a>, <a href="#ESSAYS">Essays</a>, and 
                    <a href="#BIBLIOGRAPHY">Bibliography</a>&#x2014;that are not anchored to particular works or manuscripts, 
                    but serve as universally accessible resources, and that are indexed at the same level as works and manuscripts. 
                    These materials, like the various <a href="#WORKSPAGES">Work Pages</a> and <a href="#MANUSCRIPTPAGES">Manuscript Pages</a>, each open on a separate 
                    tab in the user's browser.
                
      </p>
      
      
      
      <div id="WORKSPAGES" class="section">
         <span class="head-section">Work Pages</span>
         
         <p>A given work may be represented by the following kinds of documents:</p>
         
         <ul>
            
            <li>the <a href="#WORKAPPARATUSPAGE">Work Apparatus Page</a>;
            </li>
            
            <li>the <a href="#WORKTEXTPAGES">Work Text Pages</a>, consisting of
            </li>
            
            <li>
               
               <ul>
                  
                  <li><a href="#WORKWITNESSES">Transcript(s) of witness(es)</a>,
                  </li>
                  
                  <li>the <a href="#GLOSSES">Glosses</a> that annotate the witness(es),
                  </li>
                  
                  <li><a href="#WORKFACSIMILES">Facsimile(s)</a> associated with the respective witness(es);
                  </li>
                  
               </ul>
               
            </li>
            
            <li><a href="#WORKDRAWINGS">Drawings</a>.
            </li>
            
         </ul>
         
         
         <p>If a work is selected by title from the indicies, the selection defaults to the <a href="#WORKAPPARATUSPAGE">Work Apparatus Page</a>. 
                        This page is headed by the editorial title of the work, 
                        a form of the title that is standardized according to rules given in 
                        the <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>, 
                        in order to insure consistent reference throughout the archive. (The <a href="#WORKAPPARATUSPAGE">Work Apparatus Page</a> includes 
                        a section, <a href="#TITLE">Title</a>, devoted to explaining <span class="persName-JR">Ruskin</span>&#x02BC;s own title or titles for the work, if any, 
                        along with the often complex subsequent history of entitling the work.)
         </p>
         
         
         
         <div id="WORKAPPARATUSPAGE" class="subsection">
            <span class="head-subsection">Work Apparatus Page</span>
            
            <p>Each work defaults to an Apparatus Page, which is divided into six (or, in some cases, seven) sections, listed in a menu at the top of the page: 
                        Witnesses, Title, Genre, Manuscripts, Date, Composition and Publication, Discussion, and, in 
                        some cases, Textual Note.</p>
            
            <ul>
               
               <li><i>Witnesses</i>, the first section on the Apparatus Page, consists of a list of hyperlinks to the available 
                                witnesses and associated facsimiles, which constitute the <a href="#WORKTEXTPAGES">Text Pages</a> for a given work.
               </li>
               
               <li><i>Title</i> declares the editorial title, followed by <span class="persName-JR">Ruskin</span>&#x02BC;s
                                form(s) of the title, if any, along with discussion of sources or other matters relevant to the entitling of the work by <span class="persName-JR">Ruskin</span> 
                                or by his editors.
               </li>
               
               <li><i>Genre</i> declares the work&#x02BC;s genre and, in some cases, subgenre. 
                                For poems, the section also supplies commentary on its prosody.
               </li>
               
               <li><i>Manuscripts</i> identifies the physical manifestations of the work (e.g., drafts, fair copies, published versions), 
                                each hyperlinked to the Apparatus Page of the manuscript in question (see 
                                <a href="">Manuscript Pages</a>).
               </li>
               
               <li><i>Date</i> contains the argument for the date assigned to the work.
               </li>
               
               <li><i>Composition and Publication</i> describes the publication history, if any, for the work, 
                                and reconstructs the compositional history, if materials are available to form such an argument.
               </li>
               
               <li><i>Discussion</i> takes up contextual questions not covered in the previous sections, such as evidence for a work&#x02BC;s sources, the occasion of its
                                composition, and so on.
               </li>
               
               <li><i>Textual Note</i> contains summmary remarks based on collations performed by the <i>ERM</i> editor and staff. 
                                The reader can also perform collations of witnesses using a tool such as <span class="orgName-NINES">NINES</span>&#x02BC;s Juxta.
               </li>
               
            </ul>
            
            
         </div>
         
         
         <div id="WORKTEXTPAGES" class="subsection">
            <span class="head-subsection">Work Text Pages and Showcase</span>
            
            <p>From the <a href="#WORKAPPARATUSPAGE">Work Apparatus Page</a>, the reader may select available witnesses and corpora connected with the work. 
                        Each of these witnesses or corpora is represented by Work Text Pages, 
                        which open in Showcase windows as a transcript of a witness paired with a corresponding facsimile of the physical witness. 
                        Acknowledgement is made if a facsimile is unavailable for a given witness.
            </p>
            
            
            <div id="SHOWCASE" class="sub-subsection">
               <span class="head-sub-subsection">Showcase</span>
               
               <p><i>Showcase</i> is the display space for Text Pages. The controls in Showcase include the following functions, ordered from left to right atop the Showcase window. 
                        (These functions are also identified by mouseover.)
               </p>
               
               <ul>
                  
                  <li>Forward arrows advance the reader through a work&#x02BC;s available facsimiles for a particular witness, provided that witness occupies more than one facsimile; 
                                and backward arrows reverse the reader through those same facsimiles. 
                                The reader may also select these available facsimiles from the pull&#x2010;down menu between the arrows.</li>
                  
                  <li>A facsimile and its transcript for a particular witness are tied together, and by default they appear side by side in Showcase. 
                                Using a windowpane control, however, the reader may fill the window with the facsimile alone, 
                                or the transcript alone, or return to the divided window.</li>
                  
                  <li>A pull&#x2010;down menu increases or decreases magnification of the facsimile.</li>
                  
                  <li>A pull&#x2010;down menu increases or decreases font size of the transcript.</li>
                  
                  <li>A toggle view advances the reader through layers of additions, deletions, overwriting, etc., as interpreted by the <i>ERM</i> team and 
                                encoded in the edited witness of a work. The baseline view is a representation of all of these layers simultaneously.
                  </li>
                  
                  <li>A toggle view separates the hands (e.g., <span class="persName-JR">John Ruskin</span>&#x02BC;s, <span class="persName-MR">Margaret Ruskin</span>&#x02BC;s, 
                                <span class="persName-JJR">John James Ruskin</span>&#x02BC;s) responsible for all or portions of a witness.
                  </li>
                  
               </ul>
               
               
            </div>
            
            
            <div id="WORKWITNESSES" class="sub-subsection">
               <span class="head-sub-subsection">Work Witnesses</span>
               
               <p>By default, when a given witness is selected by the reader, its transcript along with the corresponding facsimile appears in 
                        <a href="#SHOWCASE">Showcase</a>. 
                        For discussion of the transcription and encoding methodologies governing these transcriptions, see 
                        <a href="../notes/editorial_rationale_note.php" target="_blank">Editorial and Encoding Rationale and Methodology</a>.
               </p>
               
               <p>Transcripts of witnesses carry some editorial features, which are local to this level of the archive and cannot be accessed through the archive indices, 
                            although occasionally a hyperlink elsewhere in the archive will point directly to one of these features.</p>
               
               <ul>
                  
                  <li><i>Glosses</i> are editorial commentary linked to specific features of a transcript 
                                (as opposed to the global <a href="#NOTES">Notes</a>). This commentary cannot be conveniently discussed in the 
                                <a href="#WORKAPPARATUSPAGE">Apparatus Page</a> of a work, as the glosses depend on a specific context. 
                                Hyperlinks (reference numbers) to glosses are colored to distinguish them from the transcript as editorial. 
                                On occasion, <span class="persName-JR">Ruskin</span> annotated texts himself, 
                                and these reference numbers or characters, because they are originally present in the manuscript, appear in the same color as the transcript. 
                                <span class="persName-JR">Ruskin</span>&#x02BC;s corresponding note is distinguished from the editors&#x02BC; notes by the bracketed comment: 
                                &#x005B;Ruskin&#x02BC;s note&#x005D;. When summoned, glosses appear as an ordered list in a single document, which opens on a separate tab.
                  </li>
                  
                  <li><i>Page notations</i> identify page breaks in the witness, as reflected in the the corresponding facsimile.
                  </li>
                  
                  <li><i>Line Numbers</i> (in the case of poems) in a transcript at present record only 
                                <span class="persName-JR">Ruskin</span>&#x02BC;s own marginal numbering in a manuscript.
                  </li>
                  
               </ul>
               
               
            </div>
            
            
            <div id="WORKFACSIMILES" class="sub-subsection">
               <span class="head-sub-subsection">Work Facsimiles</span>
               
               <p>Facsimile views, in the case of bound manuscripts, open to the page(s) containing the chosen witness of the work. In most cases, this view will appear as a
                        two&#x2010;page spread. These views are identical to what the reader finds when opening the facsimile from the <a href="#MANUSCRIPTPAGES">Manuscript Pages</a>. 
                        If a given work continues beyond the two&#x2010;page spread, the reader can advance the pages using the <a href="#SHOWCASE">Showcase</a> controls.
               </p>
               
               <p>For manuscripts that were formerly bound but now disassembled owing to damage, such as <a href="../apparatuses/msia_apparatus.php" target="_blank" class="inactive">MS IA</a>, 
                        the facsimile view is often similar to a two&#x2010;page spread. Loose single-sheet manuscripts are shown one side per view, recto followed by verso.
               </p>
               
            </div>
            
         </div>
         
      </div>
      
      
      <div id="MANUSCRIPTPAGES" class="section">
         <span class="head-section">Manuscript Pages</span>
         
         <p>The only Web pages representing a manuscript are the <a href="#MANUSCRIPTAPPARATUSPAGE">Manuscript Apparatus Page</a> 
                        and the <a href="#MANUSCRIPTFACSIMILE">Manuscript Facsmile</a>. 
                        There are no Text Pages as in the case of works, as Manuscript Text Pages would effectively be identical to the sum of the Text Pages
                        belonging to the work(s) contained within the manuscript.
         </p>    
         
         
         
         <div id="MANUSCRIPTAPPARATUSPAGE" class="subsection">
            <span class="head-subsection">Manuscript Apparatus Page</span>
            
            <p>The Manuscript Apparatus Page is the default destination for any hyperlinked occurrence of a manuscript title, whether accessed through the indices, Works Pages, or various forms of commentary. 
                        The page is headed by the manuscript&#x02BC;s editorial title, as governed by rules given in the 
                        <a href="../notes/title_citation_for_major_mss_note.php" target="_blank">System of Title Citation for Major Manuscripts</a>.
            </p>
            
            <p>The Apparatus is a single page consisting of seven sections: Title, Location, Provenance, Description, Contents, Discussion.</p>
            
            <ul>
               
               <li><i>Title</i> declares the editorial title, and goes on to discuss the history of entitling the manuscript, including <span class="persName-JR">Ruskin</span>&#x02BC;s
                                own title, if any, and his editors&#x02BC; titles.
               </li>
               
               <li><i>Location</i> identifies where the manuscript is currently held, along with any helpful cataloging information.
               </li>
               
               <li><i>Provenance</i> describes the history of ownership and transmission. For many manuscripts, which share a common provenance with others, this section consists simply of a
                                link to the appropriate section of the long note on <a href="../notes/provenance_note.php" target="_blank" class="inactive">Provenance</a>. For some manuscripts, however, the Apparatus
                                requires this section in order to elaborate on a unique history.
               </li>
               
               <li><i>Description</i> supplies physical characteristics, such as size, color, and kind of binding materials. The description also include what in <i>ERM</i> is
                                termed a <i>Collation</i>. Rather than an account of the assemblage of signatures in a printed book, however, <i>collation</i> here refers to
                            <span class="persName-JR">Ruskin</span>&#x02BC;s (and, sometimes, his and/or others&#x02BC;) pagination. This pagination applies either to an originally blank, bound notebook or of a bound
                                compilation of originally separate leaves. In the case of <span class="persName-JR">Ruskin</span>&#x02BC;s own page numbering, collations must often take account of the misnumbering of
                                pages and of missing pages. The collation provides the basis for a somewhat more speculative reconstruction of <span class="persName-JR">Ruskin</span>&#x02BC;s patterns of use of the manuscript,
                                a narrative reserved for the Discussion. Page references in the collation list are active, capable of opening the corresponding Manuscript Facsimile views. In some cases, descriptions of a
                                manuscript&#x02BC;s cover and endpapers contain active links to facsimile views, as well.
               </li>
               
               <li><i>Contents</i> lists sequentially the works contained in the manuscript. The titles in the list are active, hyperlinked to their respective Work Apparatus Page.
               </li>
               
               <li><i>Date</i> gives the inclusive dates for use of the manuscript by <span class="persName-JR">Ruskin</span> and sometimes by others.
               </li>
               
               <li><i>Discussion</i>, like the corresponding section in a Work Apparatus, is a catch&#x2010;all for topics that cannot be accommodated in other sections. For example,
                                one frequently recurring topic placed here is an attempt to reconstruct <span class="persName-JR">Ruskin</span>&#x02BC;s palimpsest&#x2010;like uses and reuses of an originally bound manuscript.
                                These reconstructions propose sequences of composition rarely corresponding to the <i>sequential</i> list given in the Contents, which describes the physical order of a
                                manuscript&#x02BC;s contents: <span class="persName-JR">Ruskin</span> seldom worked straightforwardly from beginning to end of a notebook. Other topics taken up in this section include
                                discussion of the hand(s) characterizing the manuscript, its relation to similar manuscripts, and so on.
               </li>
               
            </ul>
            
            
         </div>
         
         
         <div id="MANUSCRIPTFACSIMILE" class="subsection">
            <span class="head-subsection">Manuscript Facsimile</span>
            
            <p>The complete facsimile of a manuscript&#x2014;from cover to cover, if bound, viewed as a sequence usually of two&#x2010;page spreads&#x2014;is the same as what a reader would view when selecting
                        sequentially the Work Facsimiles it contains. (The difference in the terms <i>Manuscript Facsimile</i> and <i>Work Facsimile</i> is merely one of convenience referring,
                        not to differing entities, but to viewing the same entity from different places in the archive&#x2014;the Manuscript Pages and Works Pages, respectively.)
            </p>
            
            <p>See <a href="../notes/editorial_rationale_note.php" target="_blank">Editorial and Encoding Rationale and Methodology</a>for an explanation of how manuscripts 
                        are represented in <i>ERM</i> using the &#x3008;teiCorpus&#x3009; element.
            </p>
            
         </div>
         
      </div>
      
      
      <div id="NOTES" class="section">
         <span class="head-section">Notes</span>
         
         <p><i>Notes</i> contain commentary, which can be hyperlinked from any point in the archive 
                            (unlike <a href="#GLOSSES"><i>Glosses</i></a>, 
                            which are anchored to transcripts of <a href="#WORKWITNESSES">witnesses</a>) 
                            Notes are grouped in four subtypes:
         </p>
         
         <ul>
            
            <li>bibliographical;</li>
            
            <li>biographical;</li>
            
            <li>contextual;</li>
            
            <li>geographical.</li>
            
         </ul>
         
         
      </div>
      
      
      <div>
         
         <div id="ESSAYS" class="section">
            <span class="head-section">Essays</span>
            
            <p><i>Essays</i> provide broad overviews of large topics pertinent to the archive. 
                                They are intended as pathways into the archive for readers who do not wish to enter by selecting a specific work, manuscript, or note 
                                from the indices, but who prefer to explore the archive to discover its resources supporting a broad research interest. 
                                For such readers, the essays supply richly, though not exhaustively hyperlinked guided tours of works related to major topics. As presently planned, 
                                the topical essays will include:
            </p>
            
            <ul>
               
               <li>Bibliography;</li>
               
               <li>Literature;</li>
               
               <li>Education;</li>
               
               <li>Travel;</li>
               
               <li>Science;</li>
               
               <li>Religion.</li>
               
            </ul>
            
            
         </div>
         
         
         <div id="BIBLIOGRAPHY" class="section">
            <span class="head-section">Bibliography</span>
            
            <p>The <i>Bibliography</i> is the reference point for bibliographical citations of works, published and unpublished, 
                                exclusive of the manuscripts and edited witnesses of works of which the archive itself consists. Scattered througout the archive, the reader will find 
                                bibliographical citations in short&#x2010;title form, which are hyperlinked to full citations in the Bibliography. 
                                Viewed as a full document, the Bibliography is divided into sections:
            </p>
            
            <ul>
               
               <li>Primary Sources
                                        
                  <ul>
                     
                     <li>Works by <span class="persName-JR">Ruskin</span> and the Ruskin Family in Manuscript Other than the Manuscripts Constituting This Edition
                     </li>
                     
                     <li>Works by <span class="persName-JR">Ruskin</span> in Published Editions
                     </li>
                     
                     <li>Works by Other Authors in Manuscript</li>
                     
                     <li>Works by Other Authors in Published Editions</li>
                     
                  </ul>
                  
               </li>
               
               <li>Secondary Sources
                                        
                  <ul>
                     
                     <li>Unpublished</li>
                     
                     <li>Published</li>
                     
                  </ul>
                  
               </li>
               
            </ul>
            
            
         </div>
         
         
         
