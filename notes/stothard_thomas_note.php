      <div class="title">Thomas Stothard (1755&#x2013;1834)</div>
      
      <p>Painter and illustrator. <span class="persName-TSB">Stothard</span> was one of the most prolific book illustrators of his time, 
                    and he adapted his art to many media and emerging printing technologies, 
                    while simultaneously maintaining a reputation as a history painter and playing a respected role at the <span class="orgName-ROYALACADEMY">Royal Academy</span>.
                
      </p>
      
      <p>One of <span class="persName-TSB">Stothard&#x02BC;s</span> influential patrons was <a href="../notes/rogers_samuel_note.php" target="_blank"><span class="persName-SR">Samuel Rogers</span> (1763&#x2013;1855)</a>, 
                    for whom <span class="persName-TSB">Stothard</span> provided, among other commissions, the figure vignettes to illustrate the poet&#x02BC;s <span class="date-1830">1830</span> edition of the travelogue poem, <span class="title-poem-ROGERSITALY"><i>Italy</i></span>. 
                    <span class="persName-JR">Ruskin</span> imitated the style of these vignettes, along with those designed by <a href="../notes/turner_joseph_note.php" target="_blank" class="inactive"><span class="persName-JMWT">Turner</span></a> 
                    and <a href="../notes/prout_samuel_note.php" target="_blank"><span class="persName-SP">Prout</span></a> for the same publication, in the fair copy of his own 
                    <a href="../apparatuses/account_of_a_tour_on_the_continent_apparatus.php" target="_blank"><span class="title-anthology-RUSKINACCOUNTTOUR">Account of a Tour on the Continent</span></a>.
                
      </p>
      
