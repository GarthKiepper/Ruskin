      <p> <span class="persName-SJP">Pratt</span> was a minor British writer characteristic of the &#x201C;age of sentiment.&#x201D; 
                    At the end of his life, <span class="persName-SJP">Pratt</span> was eulogized in <span class="date-1801-1834">early nineteenth&#x2010;century</span> periodicals, according to <span class="persName-JGA">Josephine Grieder,</span> 
                    as a &#x201C;philanthropist, patriot, and zealous friend of the unfortunate, as well as author,&#x201D; 
                    (<a href="../bibiliography/bibliography.php#GRIEDERPRATT" target="_blank" class="inactive">Grieder, <span class="title-essay-GRIEDERPRATT">&#x201C;The Elusive Samuel Jackson Pratt&#x201D;</span></a>, 479). 
                    <span class="date-1867-1900">Later in the nineteenth century,</span> however, 
                    a history of English literature&#x2014;one of the first to make a case for a distinct 
                    <span class="date-1837-06-20-1901-01-22">&#x201C;Victorian period&#x201D;</span> in literature&#x2014;singled out <span class="persName-SJP">Pratt</span> as exemplifying a 
                    &#x201C;style of singularly mawkish sentimentality and empty affectation&#x201D; during a time already marked by an unprecedented 
                    &#x201C;ascendency&#x201D; of a &#x201C;labored, tortuous, and essentially unnatural and untrue style&#x201D; that 
                    &#x201C;has subsisted among us . . . in every era of our poetry&#x201D;  
                    (<a href="../bibiliography/bibliography.php#CRAIKHISTORY" target="_blank" class="inactive">Craik, <span class="title-book-CRAIKHISTORY"><i>History of English Literature</i></span></a>, 2: 415, 402; 
