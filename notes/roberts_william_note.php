      <div class="title">William Roberts (1862&#x2013;1940)</div>
      
      <p><span class="persName-WR">Roberts</span> was an art critic for
                    the <span class="placeName-LONDON">London</span> <i>Times</i>, starting as second critic under the chief, Humphry Ward (1845&#x2013;1926), 
                    and advancing later to become the main art critic.
                
      </p>
      
      <p><span class="persName-WR">Roberts&#x02BC;s</span> career typified a phenomenon of professional British art history in the <span class="date-1901-1934">early twentieth century,</span> 
                    whereby, according to <span class="persName-BAA">Brian Allen,</span> &#x201C;the study of historic British painting was almost entirely in the hands 
                    of the art trade.&#x201D; <span class="persName-WR">Roberts</span> ranked as a leading so&#x2010;called &#x201C;expert&#x201D; who worked with <span class="placeName-LONDON">London</span> art dealers, 
                    authoring pamphlets designed to sell individual paintings. 
                    &#x201C;These now rather rare and often sumptuous publications are invariably anonymous,&#x201D; <span class="persName-BAA">Allen</span> remarks, 
                    &#x201C;undoubtedly written by scholars who were cautious about protecting their anonymity.&#x201D; 
                    Although he and others like him operated outside the institutional art establishment, 
                    &#x201C;<span class="persName-WR">Roberts</span> . . . would, for five pounds, write certificates of authenticity for any <span class="date-1701-1800">eighteenth&#x2010;century</span> British picture placed in front of him&#x201D; 
                    (<a href="../bibiliography/bibliography.php#PAULMELLONSLEGACY" target="_blank" class="inactive"><i>Allen, &#x201C;Paul Mellon and Scholarship in the History of British Art&#x201D;</i></a>, 45).
                     
         
      </p>
      
