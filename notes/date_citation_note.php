      <div class="title">The System of Date Citation</div>
      
      <p>On the Apparatus Page for any given work, the &#x201C;Date&#x201D; section begins with a declaration of a date or a date range, 
                    which is used to place the work in the chronological index of the archive
                    (see <a href="../notes/plan_of_archive_note.php" target="_blank">The Plan of the Archive</a>). This opening declaration in the &#x201C;Date&#x201D; section 
                    is followed by a discussion of the evidence for dating the work. The choice of date for indexing purposes awards priority to a date inscribed in the manuscript 
                    by <span class="persName-JR">Ruskin</span> himself or by his parents, if that date is reasonably trustworthy; otherwise, the opening declaration uses a date or date range of composition.
      </p>
      
      <p>The date thus selected for indexing purposes is somewhat arbitrary since, as the discussion in the &#x201C;Date&#x201D; section of an apparatus clarifies in any given instance,
                    the date that <span class="persName-JR">Ruskin</span> or his parents inscribed may refer to a variety of stages in the composition, specifying possibly when he wrote a work, or when he fair&#x2010;copied it, 
                    or the occasion when he presented it to a recipient. For example, a date of &#x201C;<span class="date-1828-01-01">1 January 1828</span>&#x201D; that <span class="persName-JR">Ruskin</span> inscribed on a
                    <a href="../notes/new_years_poems_note.php" target="_blank" class="inactive">New Year&#x02BC;s Poem</a> refers obviously to the occasion of the fair copy&#x02BC;s presentation, 
                    and <span class="date-1828-01-01">1 January 1828</span> is used to index the poem in the archive. In such an instance, as the discussion of a work will clarify, however, <span class="persName-JR">Ruskin</span> would typically have drafted and fair&#x2010;copied
                    the poem in <span class="date-1827-12-01-1827-12-31">December 1827</span>.
      </p>
      
      <p>For indexing purposes, less credence is apt to be awarded to dates inscribed in the manuscript by later hands&#x2014;typically by
                    <a href="../notes/wedderburn_note.php" target="_blank" class="inactive"><span class="persName-AW">Alexander Wedderburn</span> (d. <span class="date-1931">1931</span>)</a> 
                    or <a href="../notes/collingwood_w_g_note.php" target="_blank" class="inactive"><span class="persName-WGC">W. G. Collingwood</span> (<span class="date-1854-1932">1854&#x2013;1932</span>)</a>. If these dates are questionable, 
                    the inscription is merely mentioned and discussed in the &#x201C;Date&#x201D; section.
      </p>
      
      <p>In some cases, even dates that <span class="persName-JR">Ruskin</span> himself inscribed can be very misleading. For example, he dated several poems in <a href="../apparatuses/msiii_apparatus.php" target="_blank">MS III</a> 
                    as <span class="date-1829-03-09">9 March 1829</span>. He is unlikely to have composed, or even fair&#x2010;copied, so many poems in a single day. This may be a case in which <span class="persName-JR">Ruskin</span>&#x02BC;s inscription of a date 
                    refers, rather, to a wished&#x2010;for date of fair&#x2010;copying, a performative episode turning on tensions arising from his parents&#x02BC; attempts to control his writing, 
                    in conflict with his ardent desire to please them with his compositions 
                    (see <a href="../apparatuses/of_rocks_first_apparatus.php#DISCUSSION.php" target="_blank" class="inactive">&#x201C;Of rocks first and of caverns now I sing&#x201D;: Discussion</a>).
      </p>
      
      <p>The compositional date is given&#x2014;and the piece is ordered sequentially
                    within a given year, according to that date&#x2014;only when a datable
                    rough draft or some other hard evidence about its composition is available. In
                    cases for which only the fair copy remains, one may often assume only a few
                    months, at most, to have elapsed between beginning composition and
                    fair&#x2010;copying. Parts of some longer poems such
                    as <a href="../apparatuses/eudosia_apparatus.php" target="_blank" class="inactive"><span class="title-poem-RUSKINEUDOSIA">&#x201C;Eudosia&#x201D;</span></a> appear, in fact, to have been
                    fair&#x2010;copied almost as soon as <span class="persName-JR">Ruskin</span> drafted them. Such assumptions
                    are not uniformly safe, however, since <span class="persName-JR">Ruskin</span>&#x02BC;s anthologies include
                    poems that good evidence may date as much as a year earlier than when he
                    compiled the anthology.
      </p> 
      
